            <h1>In Through the Out Door (1979)</h1>
            <br />

            <p>
                - In Through the Out Door é o oitavo álbum do Led Zeppelin, lançado em 15 de agosto de 1979.
            </p>
            <br />

            <p>
                O disco foi gravado na Polar Studios em Estocolmo entre o final de 1978 e início de 1979, sendo o terceiro álbum da banda a ser
                lançado pelo seu próprio selo, a Swan Song Records. O álbum é marcado pela mistura de sons e ritmos trazidos de outros estilos
                musicais, como o country e o reggae. Marcado também pelo fato dos teclados e sintetizadores de John Paul Jones se sobressaírem
                além dos acordes de Jimmy Page que vivia uma crise artística graças ao vício em heroína. Um fato que marcou enormemente o lançamento
                do álbum, foi a edição com 6 capas diferentes: a cena no bar fotografada por 6 ângulos opostos.
            </p>
            <br />

            <p>
                O álbum foi um grande sucesso comercial, particularmente nos Estados Unidos, onde alcançou a posição nº #1 na parada da Billboard
                em apenas sua segunda semana na parada, bem como alcançou a posição nº #1 no Reino Unido, Canadá e Nova Zelândia e o Top #10 ou #15
                em vários outros países. - In Through the Out Door foi o último lançamento de estúdio da banda a chegar ao topo das paradas na
                América.
            </p>
            <br />

            <p>
                O álbum foi nomeado pelo grupo para descrever suas lutas recentes em meio à morte do filho de Robert Plant , Karac, em 1977, e o
                exílio fiscal que a banda tirou do Reino Unido. O exílio resultou na impossibilidade da banda de fazer turnê em solo britânico por
                mais de dois anos, e tentar voltar à mente do público era, portanto, como "tentar entrar pela porta de 'fora'".
            </p>
            <br />

            <p>
                O álbum original apresentava um truque incomum: o álbum tinha uma capa externa feita para se parecer com um saco de papel marrom
                comum, "como papel de pão", e a capa interna era preta e arte de linha branca que, se lavada com água, se tornaria permanentemente
                totalmente colorida. Também havia seis capas diferentes apresentando um par diferente de fotos (uma de cada lado), e a capa
                externa de papel pardo significava que era impossível para os compradores de discos saberem qual capa estavam comprando. Todas as fotos
                retratavam a mesma cena em um bar (em que um homem queima uma carta de Querido John), e cada foto foi tirada do ponto de vista separado de
                alguém que apareceu nas outras fotos.
            </p>
            <br /><br />

