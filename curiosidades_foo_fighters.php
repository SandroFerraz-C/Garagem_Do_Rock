                    <p>- Um dos maiores sucessos da banda, a música Learn to Fly é também uma das que Dave Grohl menos gosta. Ela foi escrita em 1999,
                        quando o grupo tinha que viajar de avião frequentemente e alguns dos integrantes tinham medo de voar.</p>
                </div>
                <br /><br />

                <button class="btn16" data-toggle="collapse" data-target="#demo16">Curiosidade 16</button>

                <div id="demo16" class="collapse">
