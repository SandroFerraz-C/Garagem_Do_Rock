                        Cesena, na Itália. Comovido e agradecido, Grohl respondeu em italiano e fez o show na pequena cidade.</p>
                </div>
                <br /> <br />

                <button class="btn21" data-toggle="collapse" data-target="#demo21">Curiosidade 21</button>

                <div id="demo21" class="collapse">
