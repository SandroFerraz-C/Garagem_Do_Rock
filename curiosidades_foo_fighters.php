                <button class="btn18" data-toggle="collapse" data-target="#demo18">Curiosidade 18</button>

                <div id="demo18" class="collapse">
                    <br />
                    <p>- Dave não tem formação. Em entrevista ele disse que se não tivesse tocando, ele não sabe o que seria. "Não terminei o colégio,
                        sempre trabalhei em coisas como construção civil, umas P... de pizzarias, lojas de móveis. Eu um jovem norte-americano comum",
                        disse em entrevista. Tudo mudou quando ele entrou no Nirvana.</p>
                </div>
                <br /><br />

                <button class="btn19" data-toggle="collapse" data-target="#demo19">Curiosidade 19</button>

                <div id="demo19" class="collapse">
                    <br />
                    <p>- Dave Grohl tem muitos amigos famosos. Alguns já se foram como o vocalista do - Motorhead, Lemmy Kilmister, para quem
                        ele fez uma tatuagem em homenagem. Quem também é seu grande amigo é o ex-Beatle Paul McCartney. Quando Dave caiu no palco
                        e quebrou o pé, foi Paul e sua família que organizaram a cirurgia em Londres. Recentemente, Paul tocou bateria em uma faixa de
                        "Concrete and Gold", novo álbum do Foo Fighters.</p>
                </div>
