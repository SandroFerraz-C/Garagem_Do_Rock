            <p> - <span lang="en-US">Who Made Who</span> é o décimo segundo álbum do AC/DC, lançado no dia <time datetime="1986-05-24">24 de Maio de 1986</time>.</p>

            <p>O disco é uma coletânea da banda e foi a trilha sonora do filme de Stephen King, - <span lang="en-US">Maximum Overdrive</span> -. Possui a faixa inédita "<span lang="en-US">Who Made Who</span>" e duas faixas instrumentais "D.T." e "<span lang="en-US">Chase The Ace</span>". Foram colocadas nesse álbum clássicos que estouraram no
