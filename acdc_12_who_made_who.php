            <p>Esse álbum contém a música "<span lang="en-US">Ride On</span>", que foi uma escolha deles para homenagear Bon Scott, e duas músicas do álbum - <a href="acdc_11_fly_on_the_wall.php" target="blank"><span lang="en-US">Fly On The Wall</span></a> de 1985 "<span lang="en-US">Sink The Pink</span>" e "<span lang="en-US">Shake Your Foundations</span>".</p>

            <p>Esse álbum possui também dois clipes, são eles das faixas "<span lang="en-US">Who Made Who</span>" música principal do álbum e um clipe especial para a faixa super clássica "<span lang="en-US">You Shook Me All Night Long</span>", sendo essa música umas das mais importantes na carreira do AC/DC e umas das
