            <h1>História do Green Day</h1>

            <p>
                Green Day é uma banda de punk rock dos Estados Unidos formada em 1986 em Berkeley, Califórnia. A banda é composta por 3 membros:
                Billie Joe Armstrong (guitarra e vocais), Mike Dirnt (baixo e vocais) e Tré Cool (bateria). A banda foi formada no início de
                1986 com o nome de Sweet Children, com o baterista Al Sobrante. Em 1989, a banda mudou para o nome atual, logo após lançou o
                seu primeiro álbum de estúdio - 39/Smooth.
            </p>

            <p>
                Foi um dos grupos que nasceram no clube 924 Gilman Street, lugar frequentado por bandas de punk rock. Suas primeiras publicações
                foram feitas pela gravadora Lookout! Records. Graças às vendas de sucesso de seus primeiros álbuns, obteve-se um número significativo
                de fãs. Alguns anos mais tarde, em 1994, o grupo assinou com a Reprise Records. Esta gravadora lançou o aclamado Dookie, o novo álbum do
                grupo que levou o som do final dos anos 1990 para a nova geração. Além disso, o álbum se tornou um sucesso internacional e já vendeu
                mais de 20 milhões de cópias em todo o mundo.
            </p>

            <p>
                A banda já vendeu cerca de 85 milhões de cópias em todo o mundo e 25 milhões só nos Estados Unidos. Em 2010 estreou uma adaptação
                musical do álbum American Idiot, na Broadway. O musical foi indicado ao Tony Awards, incluindo Melhor Musical, vencendo dois e
                recebeu críticas positivas. Em meados de 2011 a revista Kerrang! nomeou o Green Day como a segunda banda mais influente dos últimos
                30 anos, atrás apenas de Metallica. Ficaram em 91° na lista dos "100 Maiores Artistas de Todos os Tempos" do canal de televisão
                VH1. Em 2014 foram introduzidos para o Rock and Roll Hall of Fame 2015.
            </p>

            <p>
                O grupo ganhou cinco Grammy: Melhor Álbum Alternativo para - Dookie, Melhor Álbum de Rock para - American Idiot, Gravação do Ano para
                - Boulevard of Broken Dreams, pela segunda vez Melhor Álbum de Rock para - 21st Century Breakdown e Melhor Álbum de Show Musical para
                o teatro musical de - American Idiot (musical).
            </p>

            <p>
            Mike Dirnt e Billie Joe Armstrong se conheceram aos 10 anos de idade, pouco antes do pai de Billie morrer de câncer. Dirnt foi um dos 
            poucos amigos de Billie que chegou a conhecer seu pai. Quando Mike completou 15 anos, ele se mudou para a casa de Billie e, os dois, para se 
            sustentar, começaram a trabalhar de ajudantes de garçom no Rod's Hickory Pitt. No ano de 1987, Mike e Billie fundaram uma banda, e a
             batizaram de Sweet Children. O primeiro show deles aconteceu no dia 17 de outubro daquele ano, no Rod's Hickory Pit em Vallejo, Califórnia, 
             onde a mãe de Armstrong trabalhava. Em 1988, Armstrong e Dirnt começaram a trabalhar com o ex-baterista do Isocracy, John Kiffmeyer, também 
             conhecido como Al Sobrante. Kiffmeyer atuou como baterista e gerente de negócios da banda, organizando shows e ajudando-lhes a estabelecer
              uma base de fãs.
            </p>

            <p>
            Pouco tempo depois, fizeram um show em um clube chamado Gilman Street, nesta mesma época, Tré Cool tocava na banda The Lookouts. 
            Algum tempo depois, Larry Livermore, dono da Lookout! Records, viu a banda tocar, e logo eles conseguiram assinar contrato com a 
            gravadora. Em 1989, o Green Day grava seu primeiro EP, intitulado 1,000 Hours. Antes de 1,000 Hours ser lançado, eles abandonaram o 
            nome Sweet Children, de acordo com Livermore isso foi feito para evitar confusão com outra banda local, Sweet Baby. Por conta disso,
             mudaram o nome para "Green Day".
           </p>
