            <h1>Legião Urbana (1985)</h1>

            <p>
                - Legião Urbana é o álbum de estreia da banda de rock brasileira Legião Urbana, lançado em 2 de janeiro de 1985, pela EMI Music..
            </p>

            <p>
                A banda tinha como objetivo produzir um trabalho "simples, com poucas firulas musicais e muita autenticidade". Renato Russo gravava
                sua voz com dois microfones valvulados; Um à frente da boca e outro acima dele, para capturar a reverberação. A bateria de Marcelo
                era gravada por microfones estrategicamente posicionados pelas paredes e janelas do estúdio. José trabalhou para satisfazer o
                desejo da banda de não fazer um som nem muito cru, nem muito artificial.
            </p>

            <p>
                O resultado final do disco não agradou totalmente a Renato Russo. Ele queria que o trabalho soasse como se houvesse sido gravado
                ao vivo, com todos os membros tocando juntos. Contudo, apenas "Petróleo do Futuro" atingiu este patamar.
                <br /><br />
                "Geração Coca Cola" era considerada a canção mais importante pelos executivos da gravadora. Mas Jorge mostrou apenas a letra
                para eles, temendo que a instrumentação "pesada" e "contestadora" colocasse em risco o contrato. Na verdade,
                esperava um folk; por isso, Jorge sugeriu que a banda se inspirasse em Bob Seger e gravasse a faixa com violões. Vários formatos
                foram tentados, incluindo versões humorísticas e tocadas como para uma festa, mas o que acabou sendo colocado no disco foi uma
                versão "menos crua, mas totalmente fiel" ao que a banda queria.
            </p>

            <p>
                No final do ano de 1985, o álbum foi premiado nas categorias "Melhor Álbum do Ano", "Melhor Vocalista" (Renato Russo) e
                "Melhor Letrista" (Renato Russo), pela extinta Revista Bizz da Editora Abril.
                <br /><br />
                Alvaro Neder, escrevendo para o Allmusic, deu ao álbum duas estrelas e meia de cinco. Ele observou que "como muitas bandas
                jovens, desde cedo eles claramente não tinham a confiança necessária para fazer valer as suas opiniões sobre os de sua gravadora",
                e descreveu "Ainda é Cedo" como "sem dúvida o primeiro triunfo absoluto da banda.
            </p>
