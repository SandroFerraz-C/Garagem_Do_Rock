            <p>
                - A Tempestade é o sétimo álbum da Legião Urbana, lançado em 20 de setembro de 1996.
            </p>

            <p>
                É o último disco lançado pela Legião Urbana com Renato Russo ainda vivo, sendo que após o lançamento do álbum o cantor morreu em
                11 de outubro de 1996. No Brasil foram vendidos mais de 1,1 milhão de cópias, e foi o quarto mais vendido da banda, sendo
                certificado com disco de diamante pela ABPD.
            </p>

            <p>
                A Tempestade foi gravado a partir de janeiro de 1996 nos AR Estúdios, na Barra da Tijuca. A banda teve um desentedimento com o
                local assim que iniciaram os trabalhos, pois descobriram que as instalações não haviam sido reservadas exclusivamente para eles.
                Renato fez disso mais um motivo para não acompanhar as gravações, ao mesmo tempo em que o tecladista de apoio Carlos Trilha
                passava a acompanhar praticamente todo o trabalho do grupo. Diferentemente do disco anterior, não houve uso de
                <a href="#"> Pro Tools</a> nesta obra.
            </p>

            <p>
                O álbum foi planejado para ser um disco duplo, mas Renato era contra por temer um preço final muito elevado. Os outros dois
                integrantes acabaram aceitando lançar somente A Tempestade (nome na capa) quando perceberam que seu colega provavalmente não
                viveria muito tempo. Na época, Renato já aparentava estar bastante magro e debilitado. Marcelo gostava mais do nome - O Livro dos Dias,
                termo que aparece na primeira parte do encarte. O livreto trazia também uma frase de Oswald de Andrade: "O Brasil é uma república federativa
                cheia de árvores e gente dizendo adeus". É o único disco da banda a não conter a frase Urbana Legio Omnia Vincii ("Legião Urbana vence tudo",
                em latim).
            </p>

            <p>
                Devido ao seu estado de sáude, Renato gravou apenas a voz-guia das faixas, não tendo voltado ao estúdio para registrar as
                versões definitivas, que deveriam ir para o produto final. A única exceção é a faixa "A Via Láctea", com elementos de
                autobiografia e que ele queria que fosse enviada às rádios. Jorge Davidson, diretor artístico que levou a banda à EMI-Odeon,
                e que na época atuava na Sony, foi convidado por Renato a visitá-lo em seu apartamento e ouvir o disco pronto. Ele recomendou
                que Renato regravasse suas vozes, mas o cantor voltou a se recusar. Foi a última vez que os dois se encontraram.
            </p>

            <p>
                Renato acompanhou pouco dos trabalhos presencialmente, mas era atualizado por telefone diariamente por Dado. Além disso,
                recebia por meio do roadie Reginaldo Ferreira as fitas de Dado, que eram retornadas ao colega também pelas mãos de Reginaldo.
                <br />
                Renato constantemente reprovava o trabalho dos colegas. Disse a Dado ao ouvir o material quase todo pronto que "a única coisa que
                servia dali era o clique do metrônomo" e chegou a brigar com o Carlos por Carlos reclamar dele ligar diariamente de madrugada para
                expressar desaprovação pelo trabalho dele.
            </p>

            <p>
                Ainda, Marcelo comentou que evitava ficar em estúdio além do necessário, pois o clima no local era triste.
            </p>
