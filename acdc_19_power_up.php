                semana de extreia. Tornou-se o terceiro álbum da banda a alcançar o número #1 na parada, seguindo -
                <a href="acdc_09_for_those_about_to_rock_(we_salute_you).php" target="blank"><span lang="en-US">For Those About To Rock (We Salute You)</span></a> e
                - <a href="acdc_17_black_ice.php" target="blank"><span lang="en-US">Black Ice</span></a>.
                Além disso, as faixas do álbum acumularam um total de 7,8 milhões de <span lang="en-US">streams on-demand</span> na semana que terminou em <time datetime="11-28">28 de novembro</time>.
                <br /><br />
                O álbum estreou em #1° lugar na parada de álbuns do Reino Unido em <time datetime="2020-11-13">13 de novembro de 2020</time>, com 62.000 vendas nas paradas,
                substituindo - <span lang="en-US">Kylie Minogue's Disco</span> como o maior lançamento da semana de abertura de <time datetime="2020">2020</time> até agora.
