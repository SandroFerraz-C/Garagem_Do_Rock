            <h1>Power Up (2020)</h1>

            <p>- FPower Up (por vezes abreviado para PWR/UP e estilizado como PWRϟUP ) é o Décimo-Setimo álbum do AC/DC, lançado em 13 de Novembro 2020,
                através a Columbia Records e Sony Music Austrália.</p>

            <p>- Power Up marca o retorno do vocalista Brian Johnson, do baterista Phil Rudd e do baixista Cliff Williams, todos os quais deixaram o
                AC/DC antes, durante ou depois da turnê de aberturade seu álbum anterior -
                <a href="acdc_18_rock_or_bust.php" target="blank">Rock or Bust</a> de (2014).
                Este é também o primeiro álbum da banda desde a morte do co-fundador e guitarrista base Malcolm Young em 2017 e serve como uma
                homenagem a ele, de acordo com seu irmão Angus. O álbum foi geralmente bem recebido pelos críticos musicais e alcançou o número
                um em 21 países.
            </p>

            <p>Após uma série de provocações enigmáticas no <a href="https://pwrup.acdc.com" target="blank">site da banda</a>, o título do álbum
                foi revelado como - Power Up em 7 de outubro de 2020;
                <br />
                Também após uma série de teasers nos dias anteriores, o áudio da música para o primeiro single da banda, "Shot in the Dark", foi
                lançado no mesmo dia. Um videoclipe para a música foi lançado em 26 de outubro de 2020. A canção liderou a parada de canções de
                rock da Billboard Mainstream por duas semanas, um clipe curto (53 segundos) de uma segunda música, "Demon Fire",
                foi estreada antes do lançamento do álbum em 30 de outubro de 2020 e foi lançada mais uma vez em 8 de dezembro de 2020, um dia
                antes da estreia mundial da música completa.
            </p>

            <p>Na Austrália, - Power Up estreou em número #1 nas paradas ARIA e, no processo, AC/DC se tornou a primeira banda a ter um álbum
                australiano número #1 em cada uma das cinco décadas (1980 a 2020). O álbum registrou 21.535 vendas em sua primeira semana,
                superando o resto dos 15 primeiros álbuns juntos. Em 12 de dezembro, ele passou quatro semanas no número #1, tornando-se o álbum
                com mais tempo sendo o número #1 na Austrália em 2020.
                <br /><br />
                Nos Estados Unidos, - Power Up estreou como número #1 na parada da Billboard 200, com 117.000 unidades de álbuns vendidos em sua
                semana de extreia. Tornou-se o terceiro álbum da banda a alcançar o número #1 na parada, seguindo -
                <a href="acdc_09_for_those_about_to_rock_(we_salute_you).php" target="blank">For Those About To Rock (We Salute You)</a> e
                - <a href="acdc_17_black_ice.php" target="blank">Black Ice</a>.
                Além disso, as faixas do álbum acumularam um total de 7,8 milhões de streams on-demand na semana que terminou em 28 de novembro.
                <br /><br />
                O álbum estreou em #1° lugar na parada de álbuns do Reino Unido em 13 de novembro de 2020, com 62.000 vendas nas paradas,
                substituindo - Kylie Minogue's Disco como o maior lançamento da semana de abertura de 2020 até agora.
                <br /><br />
                Na Alemanha, estreou em #1° lugar, vendendo quase 160.000 cópias, o melhor início do ano para um álbum. Ele permaneceu no número #1
                por um total de 5 semanas não consecutivas, e se tornou o álbum mais vendido de 2020.
            </p>
