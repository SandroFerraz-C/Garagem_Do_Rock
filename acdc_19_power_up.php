            <p>- <span lang="en-US">Power Up</span> (por vezes abreviado para <abbr title="Power Up">PWR/UP</abbr> e estilizado como PWRϟUP ) é o Décimo-Setimo álbum do AC/DC, lançado em <time datetime="2020-11-13">13 de Novembro 2020</time>,
                através a <span lang="en-US">Columbia Records</span> e <span lang="en-US">Sony Music Austrália</span>.</p>

