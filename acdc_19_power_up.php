                lançado no mesmo dia. Um videoclipe para a música foi lançado em <time datetime="2020-10-26">26 de outubro de 2020</time>. A canção liderou a parada de canções de
                <span lang="en-US">Rock</span> da <span lang="en-US">Billboard Mainstream</span> por duas semanas, um clipe curto (53 segundos) de uma segunda música, "<span lang="en-US">Demon Fire</span>",
                foi estreada antes do lançamento do álbum em <time datetime="2020-10-30">30 de outubro de 2020</time> e foi lançada mais uma vez em <time datetime="2020-12-08">8 de dezembro de 2020</time>, um dia
                antes da estreia mundial da música completa.
            </p>

            <p>Na Austrália, - <span lang="en-US">Power Up</span> estreou em número #1 nas paradas <abbr title=" Australian Recording Industry Association">ARIA</abbr> e, no processo, AC/DC se tornou a primeira banda a ter um álbum
                australiano número #1 em cada uma das cinco décadas (<time datetime="1980">1980</time> a <time datetime="2020">2020</time>). O álbum registrou 21.535 vendas em sua primeira semana,
