            <h1>Desperado (1973)</h1>

            <p>- Desperado é o segundo álbum da banda Eagles.</p>

            <p>- Desperado foi gravada no Island Studios em Londres, Inglaterra, e lançada em 1973. As canções de Desperado são
                baseadas em temas do Velho Oeste. Os membros da banda aparecem na capa do álbum vestidos como uma gangue fora
                da lei; Desperado continua sendo o único álbum dos Eagles em que os membros da banda aparecem na capa.</p>

            <p>Embora a faixa-título seja uma das canções características dos Eagles, ela nunca foi lançada como single. A canção " Desperado "
                ficou em #494º lugar na lista da Rolling Stone de 2004 das "500 maiores canções de todos os tempos". O álbum rendeu dois singles,
                "Tequila Sunrise" e "Outlaw Man", que alcançaram os números #64 e #59, respectivamente. O álbum alcançou a posição #41 na parada
                de álbuns da Billboard e foi certificado com ouro pela RIAA em 23 de setembro de 1974 e platina dupla em 20 de março de 2001.</p>

            <p>Depois de um primeiro álbum de sucesso comercial, Frey queria que o segundo álbum fosse um com o qual eles pudessem ser levados a
                sério como artistas, e se interessou em fazer um álbum conceitual. O conceito original era para canções sobre anti-heróis; de acordo
                com Glenn Frey, ele estava tocando junto com Don Henley, Jackson Browne e JD Souther após um show de Tim Hardin quando eles tiveram a
                ideia de fazer um álbum sobre anti-heróis. Uma inspiração foi um livro sobre pistoleiros do Velho Oeste dado a Browne por Ned Doheny em seu
                aniversário de 21 anos, e Browne mostrou o livro a eles e sugeriu o tema. O livro inclui histórias sobre Bill Dalton e Bill Doolin; daí veio
                a música "Doolin-Dalton" sobre a gangue Doolin-Dalton. No entanto, eles ficaram sem ideias depois de escrever "Doolin-Dalton" e "James Dean"
                sobre o ator homônimo. A ideia de anti-heróis então se tornou o Desperado com o tema faroeste, e a música "James Dean" seria usada para o
                próximo álbum On the Border.</p>
