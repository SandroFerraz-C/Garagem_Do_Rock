            <h1>Whoosh! (2020)</h1>
            <br />

            <p>
                Whoosh! é o vigésimo primeiro álbum do Deep Purple, lançado em 7 de agosto de 2020.
            </p>
            <br />

            <p>
                O grupo colaborou com o produtor Bob Ezrin, que também trabalhou em seus dois álbuns anteriores. Eles gostaram da gravação e
                produção. Seu lançamento foi promovido por uma série de declarações à imprensa do cantor Ian Gillan , como "Another album ?! Whoosh? !!
                Gordon Bennett !!!". Ele explicou que o título do álbum foi escolhido por suas qualidades onomatopaicas e "quando visto por uma extremidade
                de um radiotelescópio, descreve a natureza transitória da humanidade na Terra". Ele também disse que os fãs deveriam simplesmente ouvir o
                álbum como uma experiência agradável.
            </p>
            <br />

            <p>
                O álbum foi originalmente programado para ser lançado em 12 de junho de 2020, mas foi posteriormente adiado devido à pandemia de
                COVID-19. Gillan disse que isso ocorre porque as linhas de distribuição de mídia física devem esperar até que os bloqueios sejam amenizados
                e as restrições suspensas.
            </p>
            <br />

            <p>
                O álbum recebeu críticas favoráveis. Várias publicações notaram que o álbum continha faixas relativamente curtas e elogiaram o
                estilo econômico de composição. A revista NME (New Musical Express) disse que o álbum não parecia nada com a música contemporânea
                de 2020, mas sugeriu que "talvez isso seja uma coisa boa".
            </p>
            <br />

            <p>
                Com uma posição de pico no número 4, foi o álbum de estúdio de maior sucesso da banda no Reino Unido em 46 anos.
            </p>
