                aparição em junho de 2017 no podcast da rádio Sirius "Rolling Stone Music Now", Rob Halford disse que a banda pode ter se
                inspirado no AC / DC em algumas faixas após apoiá-los em uma turnê europeia em 1979. - British Steel foi gravada em dezembro de
                1979 no Tittenhurst Park, casa do ex- Beatle Ringo Starr, após uma falsa partida no Startling Studios, também localizado no
                terreno do Tittenhurst Park, devido à banda preferir a casa de Starr ao próprio estúdio de gravação. A amostragem digital ainda
                não estava amplamente disponível na época da gravação, então a banda usou a gravação analógica.
            </p>

