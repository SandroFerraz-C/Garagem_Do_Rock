            <h1>V (1991)</h1>
            <br />

            <p>
                - V é o quinto álbum da Legião Urbana, lançado em 1991.
            </p>
            <br />

            <p>
                Em comparação ao álbum anterior, - As Quatro Estações, teve uma vendagem bem inferior, com 465 mil cópias (cinco vezes menos que
                o disco anterior). Foi o sexto disco mais vendido da banda e acabou certificado como Disco de Platina Triplo. A turnê do - V foi
                a mais curta da banda, iniciada em Julho de 1992 e interrompida em fins de setembro do mesmo ano, devido à situação insustentável
                das crises alcoólicas de Renato Russo.
            </p>
            <br />

            <p>
                O álbum reflete duas situações: a crise econômica causada pelo então Plano Collor e a dependência química do vocalista da banda,
                Renato Russo. Em 1990, o cantor descobriu ser portador do vírus da AIDS. Porém, ao contrário de Cazuza, ex-vocalista do
                <a href="barao_vermelho.php" target="blank"> Barão Vermelho</a>, Renato nunca assumiu em público a doença.
            </p>
            <br />

            <p>
                - V é considerado um trabalho de rock progressivo. Indagado sobre como seria o disco, Renato afirmou em uma entrevista: "Ih,
                tem umas coisas medievais, uns instrumentais. O primeiro lado é uma viagem. Vão dizer assim: 'Legião repete fórmula e lança disco
                progressivo (risos)...'". Em outra entrevista, para Zeca Camargo na MTV em 1993, Renato disse que "O que a gente quis passar para
                o - V era um tédio e um marasmo. Aquele disco foi feito lento de propósito".
            </p>
            <br />

            <p>
                Pela primeira vez, a banda não utilizou os estúdios da EMI-Odeon para criar o disco. Em vez disso, optaram pela Estúdio Mega,
                também no Rio de Janeiro, onde não tinham exclusividade e não podiam ficar pelo tempo que quisessem. Além disso, contaram com um
                baixista de apoio, Bruno Araújo, que já os acompanhava desde a turnê anterior. Ele não participou da turnê subsequente, contudo,
                devido a uma briga com Fred Nascimento, violonista de apoio. Ambos eram muito próximos, mas a discussão acabou custando o emprego
                dos dois na banda.
            </p>
            <br /><br />

            <p>Duração do album: 49 min. 37 seg.</p>
