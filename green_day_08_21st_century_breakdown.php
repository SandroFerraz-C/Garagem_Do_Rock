            <h1>21st Century Breakdown (2009)</h1>
            <br />

            <p>
                "21st Century Breakdown" é o oitavo álbum edo Green Day, lançado em 15 de maio de 2009, através da Reprise Records.
            </p>
            <br />

            <p>
                O - Green Day começou a trabalhar no disco em janeiro de 2006 e quarenta e cinco canções foram escritas pelo vocalista / guitarrista
                Billie Joe Armstrong em outubro de 2007, mas os membros da banda não trabalharam no estúdio até janeiro de 2008.
            </p>
            <br />

            <p>
                O álbum tem um rock muito parecida com o álbum anterior - American Idiot (2004). Armstrong descreveu o álbum como um "instantâneo da
                era em que vivemos, enquanto questionamos e tentamos entender a manipulação egoísta que está acontecendo ao nosso redor, seja o
                governo, religião, mídia ou francamente qualquer forma de autoridade". Os singles "Know Your Enemy" e "21 Guns" exemplificam os temas
                de alienação e raiva politicamente motivada presentes no álbum.
            </p>
            <br />

            <p>
                Para lidar com o registro, eles se voltaram para produtor Butch Vig, mais conhecida por produzir - Nirvana's Nevermind (1991). A
                resposta crítica à - 21st Century Breakdown foi geralmente positiva. Alcançou o melhor desempenho do Green Day nas paradas
                até hoje, alcançando o #1° lugar nas paradas de álbuns de vários países, incluindo a Billboard 200 dos Estados Unidos, os 100
                melhores álbuns europeus e a parada de álbuns do Reino Unido. O álbum ganhou o Prêmio Grammy de Melhor Álbum de Rock no 52º Grammy
                Awards em 31 de janeiro de 2010. Em dezembro de 2010, - 21st Century Breakdown Já tinha vendido 1.005.000 cópias nos Estados Unidos
                e mais de 4 milhões em todo o mundo.
            </p>
