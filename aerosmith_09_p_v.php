            <h1>Permanent Vacation (1987)</h1>


            <p>
                Permanent Vacation é o nono álbum de estúdio da banda Aerosmith. Lançado em 18 de agosto de 1987, e possui 12 canções. </p>
            <br />


            <p>
                Este álbum é considerado por muitos fãs como um divisor de águas entre o período das drogas e o período do novo Aerosmith livre das drogas.
                Por este e outros motivos, trouxe a banda de volta as paradas de sucesso dos Estados Unidos com os hits "Dude (Looks Like a Lady)"
                (14º lugar nas paradas), "Rag Doll" (17º lugar nas paradas), e o top 10 "Angel", que chegou ao top 3º lugar nas paradas, e foi muito
                elogiada por críticos.
                <br /><br />
                O album conta com uma excelente performance de Joey Kramer na bateria, e também um ótimo solo de Joe Perry.
            </p>
            <br />
            <p>
                Este álbum também possui um cover, a faixa 11 "I'm Down", escrita por John Lennon e Paul McCartney, e gravada originalmente pelos Beatles.
            </p>
            <br />
            <p>
                Permanent Vacation recebeu múltiplas certificações de platina. Em 1995 chegou ao status de 5x Platina nos Estados Unidos.
                Só nos Estados Unidos o álbum vendeus mais de 5 milhões de cópias
            </p>
