            <h1>7800° Fahrenheit (1985)</h1>
            <br />

            <p>
                7800° Fahrenheit é o segundo álbum de estúdio da banda de hard rock Bon Jovi, lançado em 27 de Março de 1985.
            </p>
            <br />

            <p>
                O título do álbum é uma referência ao suposto ponto de fusão do rock, equivalente a 4.313° Celsius; como a escala Fahrenheit é usada
                principalmente nos Estados Unidos, 7800 ° Fahrenheit sugere "rocha quente americana". 7800 ° Fahrenheit permaneceu mapeados por 104 semanas
                na Billboard 200 álbums chart e foi certificado de platina pela Recording Industry Association of America (RIAA) em 19 de fevereiro de 1987. [5] Os singles " Only Lonely " e " In and Out of Love ", ambos colocados nas paradas da Billboard Hot 100 .
            </p>
            <br />


            <p>
                Gravado em seis semanas entre janeiro e março de 1985, no Warehouse Studios em Filadélfia, o álbum marcou a colaboração final entre
                Bon Jovi e o produtor Lance Quinn. É o único álbum do Bon Jovi com composições de quatro membros da banda; "Secret Dreams" é a única
                música do Bon Jovi até agora para a qual o baterista Tico Torres recebeu crédito de composição.
            </p>
            <br />

            <p>
                Apesar de ser uma das favoritas dos fãs, a banda estava insatisfeita com o som do álbum e essencialmente o rejeitou, uma vez que
                solidificaram seu status de superstars mundiais com Slippery When Wet e New Jersey. É o álbum menos representado em suas setlists ao longo
                da carreira: nada de 7800 ° Fahrenheit foi realizado após a New Jersey Syndicate Tour.
            </p>
            <br />
            <p>
                "Eu sempre esqueci o segundo álbum", observou Jon Bon Jovi em 2007. "Sempre esqueci, sempre esquecerei. Não tínhamos tempo para
                fazê-lo e não sabíamos quem éramos... Fizemos tudo o que o produtor Lance Quinn disse. Ele era um guitarrista brilhante e tinha
                feito discos com o Talking Heads , então você ouviu."
                <br />
                <br />
                "Todos nós estávamos passando por momentos difíceis em um nível pessoal", explicou ele no momento da Slippery When Wet' s lançamento.
                <br />

                "E a tensão afetou a música que produzimos. Não foi uma experiência agradável... Lance Quinn não era o homem certo para nós, e isso aumentou
                a sensação de que estávamos indo mal. Nenhum de nós quer viver aquele estado mental nunca mais. Deixamos o registro para trás e seguimos em
                frente.""
            </p>
