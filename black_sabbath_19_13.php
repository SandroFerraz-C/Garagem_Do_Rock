            <h1>13 (2013)</h1>

            <p>
                - 13 é o décimo nono álbum do Black Sabbath e o nono com Ozzy Osbourne na voz.
            </p>

            <p>
                A formação inicial de Black Sabbath começou a trabalhar num novo álbum de estúdio com o produtor Rick Rubin. A produção foi adiada porque
                Osbourne estava a meio de acabar o seu álbum solo Down to Earth, e os outros membros da banda acabaram eventualmente por ir participar noutros
                projectos, incluindo GZR e Heaven & Hell. O novo trabalho foi anunciado em novembro 2011, quando a banda informou que estava de novo reunida.
                - 13 começou a ser gravado em agosto de 2012 em Los Angeles. Para as gravações, o baterista Brad Wilk, juntou-se com Ozzy Osbourne,
                Geezer Butler e Tony Iommi, depois do cofundador Bill Ward ter decidido não participar no álbum, devido a uma "disputa contratual".
            </p>

            <p>
                O álbum recebeu boas criticas no geral; No Metacritic tem a pontuação de 72/100, baseada em 32 análises. Na semana de estreia, vendeu 155 mil cópias
                nos Estados Unidos e atingiu a liderança da tabela musical Billboard 200. Também alcançou o N #1 na tabela de vendas do Reino Unido durante
                a primeira semana de vendas, sendo o primeiro álbum da banda a chegar ao topo desde - <a href="black_sabbath_02_paranoid.php" target="blank">Paranoid</a> de (1970).
            </p>

            <p>
                O primeiro single do disco, "God Is Dead?", rendeu ao Black Sabbath seu segundo premio no Grammy Award na categoria "Melhor Performance de Metal"
                em 2014. O single foi lançado em 19 de abril de 2013.
            </p>
