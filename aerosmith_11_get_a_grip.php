            <h1>Get a Grip (1993)</h1>


            <p>
                - Get a Grip é o décimo primeiro álbum do Aerosmith.
            </p>

            <p>
                Algumas músicas muito polémicas foram lançadas neste álbum. Uma delas é o single "Eat The Rich" (Comam os Ricos em português), e uma das
                músicas mais conhecidas da banda, o single "Crazy", que contava com as atrizes Alicia Silverstone e Liv Tyler
                (Filha do vocalista Steven Tyler, que na época tinham menos de 20 anos). Outros sucessos como "Cryin" e "Amazing" foram revelados neste álbum.
                <br /><br />
                - Get a Grip tem 14 faixas em aproximadamente 66 minutos de puro Hard Rock e suas letras cheias de situações pessoais e
                reflexões dos membros da banda, críticas ao sistema, aventuras, sexo, drogas, Rock N’ Roll e amor.
            </p>

            <p>
                Nesse trabalho o Aerosmith estava com sua lendária e duradoura formação: STEVEN TYLER, JOE PERRY,
                BRAD WHITFORD, TOM HAMILTON e JOEY KRAMER. A se destacar também algumas participações especiais como Lenny Kravitz e Don Henley (Eagles).
                <br />
                As faixas foram compostas pelos integrantes da banda, porém, algumas com parcerias.
            </p>
