            <h1>Seventh Son of a Seventh Son (1988)</h1>
            <br />

            <p>
                - Seventh Son of a Seventh Son é o sétimo álbum do Iron Maiden; Foi lançado em 11 de abril de 1988 pela gravadora EMI.
            </p>
            <br />

            <p>
                - Seventh Son of a Seventh Son foi o último a apresentar a formação da era Piece Of Mind com o guitarrista Adrian Smith antes
                de sua saída, que depois só voltou para a banda em 1999. Junto com - Somewhere in Time, - Seventh Son of a Seventh Son, é um
                dos álbuns em que a banda usa sintetizadores e teclados. Ademais, foi o segundo disco da banda a chegar ao topo das paradas
                musicais inglesas.
            </p>
            <br />

            <p>
                - Seventh Son é um álbum conceitual que traz a lenda do sétimo filho do sétimo filho, que teria poderes sobrenaturais, partindo
                da história de uma criança enviada para ser um representante do bem ou do mal na terra. No lado lírico, o álbum gira em torno
                de diversas questões filosóficas: bem contra o mal, visões proféticas, misticismo, reencarnação e vida após a morte.
                Estilisticamente, - Seventh Son of a Seventh Son desenvolve o estilo de - Somewhere in Time (1986) e continua a exploração
                lírica da banda de temas relacionados ao poder, misticismo e ocultismo.
            </p>
            <br />

            <p>
                O álbum recebeu fortes críticas positivas desde seu lançamento, com o AllMusic dando nota 4.5 de 5, dizendo que a adição de
                teclados "restaurou a crise que algumas vezes era perdida na produção brilhante do álbum anterior" e que posicionava-se "entre
                seus melhores trabalhos". O Sputnikmusic classificou com uma nota 4 de 5, e, enquanto afirmava que "a banda tem lançamentos
                melhores, como - Powerslave e - Somewhere in Time", eles acrescentavam que "liricamente, era um dos melhores trabalhos do Maiden".
                Em 2005, o álbum foi ranqueado na posição 305 no livro - The 500 Greatest Rock & Metal Albums of All Time da revista Rock Hard.
            </p>
