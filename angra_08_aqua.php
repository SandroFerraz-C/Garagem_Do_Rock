            <h1>Aqua (2010)</h1>


            <p>- Aqua é o nono álbum de estúdio da banda brasileira de heavy metal Angra.</p>

            <p>É um álbum conceitual que gira em torno da peça - A Tempestade - de William Shakespeare, e marca a volta de Ricardo Confessori;
                foi o último trabalho de estúdio com o baterista.</p>

            <p>O álbum foi lançado de forma independente no Brasil, já que a banda desejava ter total controle sobre o processo criativo do material.
                Foi o último álbum que conta com o vocalista Edu Falaschi, que deixou a banda em 24 de maio de 2012.</p>

            <p>A letra de "Awake from Darkness" foi escrita pelo baixista Felipe Andreoli e descreve o momento em que Prospero se dá conta de que
                está em um pequeno barco com Miranda, no escuro, traído por seu irmão e amigos, e completamente sem saída. Ele se pergunta como poderá
                reverter a situação, e aceita seu destino. Então ele percebe que, na escuridão, existe um sopro de luz: seu bom e fiel amigo
                Gonzalo lhe deixou comida, roupas e seus livros de magia. Prospero então alcança a ilha, e retoma a esperança de superar sua condição.</p>

            <p>A letra de "Ashes", a faixa de encerramento, foi escrita por Kiko Loureiro e usa o último ato da peça, no qual Prospero reflete sobre o
                que fez e perdoa todos na ilha. Ele percebe que seu comportamento e forma de pensar não seriam um jeito pacífico de se viver e que o
                perdão seria a única maneira. Ele então deixa seus livros de magia para trás e começa uma nova vida. A letra termina com a última frase
                da peça: "Somos feitos da mesma essência que nossos sonhos".</p>
