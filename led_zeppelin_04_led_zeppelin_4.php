            <h1>Led Zeppelin IV (1971)</h1>

            <p>
                - O quarto álbum do Led Zeppelin foi lançado em 8 de novembro de 1971 e nunca recebeu um título oficial, por isso é conhecido
                apenas por Led Zeppelin IV.
            </p>

            <p>
                Após os comentários negativos da crítica a respeito do álbum - Led Zeppelin III, o guitarrista e líder da banda, Jimmy Page,
                decidiu que quarto disco do grupo não teria nome, e decidiu que cada um dos quatro integrantes devia escolher um símbolo qualquer
                para representá-lo na capa, e que nada mais estaria escrito nela. A banda começou a gravar o disco em dezembro de 1970 e usou
                vários estúdios na Inglaterra e nos Estados Unidos, com as gravações sendo acompanhadas por membros dos The Rolling Stones,
                outro grupo que estava em destaque na época. A principal faixa do álbum, "Stairway to Heaven", foi composta por Jimmy em
                parceria com o vocalista Robert Plant enquanto a banda estava em turnê pelos Estados Unidos, e mesmo nunca tendo sido lançada
                como single, tornou-se a canção de maior sucesso daquele ano, e é considerada até hoje uma das melhores gravações musicais de
                todos os tempos.
            </p>

            <p>
                Assim que foi lançado, o disco alcançou o segundo lugar da Billboard 200 nos Estados Unidos, enquanto "Stairway to Heaven"
                tornou-se um sucesso nas rádios e embalou o álbum inteiro, que em dois meses tornou-se o mais comprado do ano e, hoje em dia,
                já vendeu mais de vinte e três milhões de cópias só nos Estados Unidos, além de ter vendido cerca de quarenta milhões de cópias
                a nível mundial, sendo assim um dos álbuns mais vendidos da história, além de ter recebido aclamação da crítica especializada
                e ser apontado até hoje como um dos discos de rock and roll mais importantes de todos os tempos.
            </p>

            <h2>Capa e escolha dos símbolos</h2>
            <br />

            <div class="capa" align='center'>
                <img src="img/led zeppelin/simbulos.jpg">
            </div>

            <p>
                Cada um dos quatro integrantes da banda escolheu um símbolo para representá-lo nas capas e no encarte interno, e apesar do
                disco também ser conhecido como "Runas", apenas os dois símbolos do meio são realmente runas, sendo os outros dois símbolos
                mágicos ou de múltiplos sentidos.
            </p>

            <p>
                O símbolo relacionado ao guitarrista Jimmy Page geralmente é associado a uma palavra da língua grega, ZoSo, e significa salvação,
                entrando no campo espiritual, a imagem não tem uma conotação alfabética nem linguística, tendo sido desenhada pelo próprio músico.
                A fonte do símbolo é de conhecimento público, mas o significado continua sendo um mistério até os dias de hoje; o símbolo apareceu pela
                primeira vez no livro Ars Magica Arteficii, de 1557, escrito pelo alquimista Girolamo Cardano, onde é identificado como um símbolo
                composto por signos do zodíaco. O símbolo também foi reproduzido no Dicionário de Ocultismo e Símbolos Alquímicos, de Fred Gettings,
                publicado em 1982. O símbolo era muito utilizado para representar o planeta Saturno em rituais de magia, e Jimmy o escolheu por ser do
                signo de Capricórnio, um signo comandado por Saturno; A parte parecido com a letra Z é comumente associado a Saturno na
                astrologia, e a parte oSo é parecido com o símbolo alquímico do Mercúrio, também muito associado a Saturno. O que o símbolo
                representa para Jimmy, no entanto, é um mistério, uma vez que ele nunca revelou publicamente o seu significado total.
            </p>

            <p>
                O símbolo do baterista John Bonham, mostra três círculos interligados, e representa a trindade familiar entre o homem, a mulher
                e a criança. Também se assemelha à uma bateria vista de cima. O fato de os símbolos do John Bonham e do John Paul Jones, o
                baixista da banda, serem extremamente semelhantes e serem a imagem invertida um do outro não é por acaso; No jazz, um dos
                estilos musicais de muita influência no grupo, o baixista e o baterista formam partes interligadas de uma seção rítmica.
                Além disso, esse símbolo também aparecia no rótulo da cerveja Ballantine, que era a favorita de Bonham, assim, na hora de
                escolher o símbolo que o representaria, ele decidiu pegar esse emprestado por achar que também teria um efeito cômico, e o
                círculo no meio do símbolo de John Paul Jones também representa uma pessoa com confiança e competência.
            </p>

            <p>
                O vocalista Robert Plant adotou como símbolo inspirado na deusa egípcia Ma'at, mostrando uma pena, que significa verdade,
                justiça e lealdade, envolta por um círculo impenetrável que significa vida. De acordo com a mitologia egípcia, Anúbis, o deus
                do julgamento e da morte, pegava o escaravelho-sagrado (que significa o coração) daqueles que morriam e colocava em uma
                balança juntamente com uma pena de Ma'at; se o escaravelho fosse mais pesado que a pena, a alma da pessoa iria para o inferno,
                e se fosse mais leve que a pena, a alma iria para o céu, e Robert achou que esse paradoxo era uma perfeita alusão à música da
                banda. Além dessas quatro imagens, o encarte interno traz um quinto símbolo, composto por três pirâmides invertidas,
                escolhido pela cantora Sandy Denny, que participa da canção "The Battle of Evermore".
            </p>

            <p>
                A capa frontal foi inspirada numa pintura a óleo que Jimmy encontrou na cidade de Reading, na Inglaterra, e é interpretada de
                várias maneiras, mas a teoria mais aceita diz que a imagem de um camponês velho não aguentando o peso dos galhos que está
                levando tem a intenção de dizer que as pessoas deviam plantar e cuidar da Terra, ao invés de colher em excesso e destruir o
                planeta. A ilustração interior, conhecida como The Hermith (O Eremita), foi inspirado na carta homônima no Baralho de Tarot.
                A face na página em que está a letra de "Stairway to Heaven" também foi escolhida por Jimmy, que a encontrou em uma edição da extinta
                revista The Studio, publicada em 1983.
            </p>
