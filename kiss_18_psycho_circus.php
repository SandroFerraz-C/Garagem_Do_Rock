            <h1>Psycho Circus (1998)</h1>
            <br />

            <p>
                - Psycho Circus é o décimo oitavo álbum do Kiss, lançado em 1998.
            </p>
            <br />

            <p>
                O álbum é o primeiro a envolver todos os quatro membros originais desde - Dynasty de 1979 (embora o guitarrista Ace Frehley e o
                baterista Peter Criss apareçam apenas em algumas faixas selecionadas). A capa alterna entre um logotipo preto do Kiss e o título
                do álbum com fotos de um palhaço e os membros da banda, "You Wanted the Best" é a única música do Kiss em que os vocais principais
                são compartilhados por toda a formação.
            </p>
            <br />

            <p>
                Bruce Fairbairn foi contratado para produzir o álbum e afirmou que queria ficar longe de truques e tendências como participações
                especiais, apenas "tentou permanecer fiel ao que o Kiss faz de melhor e pelo que as pessoas se lembram deles". O álbum foi gravado
                principalmente no A&M Studios de Los Angeles e no One on One Recording Studios, com as performances sendo gravadas primeiro com equipamento
                analógico e depois digitalizado para mixagem e overdubs. Depois, foi mixado por Mick Guzauski em seu estúdio em Nova York e masterizado
                por Fairbarn e parceiro de longa data do Kiss, George Marinoem Sterling Sound, Nova York. Fairbairn elogiou a banda por ser colaborativa,
                declarando que "tinha o potencial de ser um pesadelo porque são todos personagens díspares e fortes, musicalmente e como pessoas".
            </p>
            <br />

            <p>
                Psycho Circus estreou em #3° na Billboard 200, vendendo 110.000 cópias em sua primeira semana de lançamento, e foi certificado ouro
                pela RIAA em 22 de outubro de 1998.
                <br /><br />

                Rolling Stone disse, "Psycho Circus é um álbum de ritmos de plataforma, riffs de guitarra de montanha-russa e refrões para cantar -
                é muito mais respeitável do que qualquer um dos fracassos estranhos dos anos sem maquiagem." Entertainment Weekly disse: "Este é o
                rock com R maiúsculo, capturado em toda a sua glória cafona ... Este novo esforço prova que, no circo dos dinossauros do hard rock,
                Kiss continua sendo seu Barnum & Bailey". "Bem dentro dos padrões usuais de comoção cafona da banda", escreveu Clark Collis no The
                Daily Telegraph. "Típico da besta é 'Eu juro fidelidade ao estado do Rock & Roll' - três minutos e meio de pompa rock hilariantemente
                bombásticoque simplesmente esmaga os pensamentos de todos os desenvolvimentos musicais pós- glamour sob uma bota de salto alto pintada
                de prata"
            </p>
            <br /><br />

            <p>Duração do album: 44 min. 24 seg.</p>
            <p>Gravadora(s): Mercury Records.</p>
            <p>Produção: Bruce Fairbairn.</p>
