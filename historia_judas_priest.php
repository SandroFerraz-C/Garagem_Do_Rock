            <p>
                Iniciaram sua carreira fazendo covers das bandas - Spirit e - Quick Silver Messenger Service, para mais tarde e com ajuda de
                Alan Eade (proprietário de um pequeno estúdio de gravação) criaram suas primeiras músicas; "Good Time Woman" e "We'll Stay Together".
                Com elas, fizeram uma série de concertos em pequenos clubes da Inglaterra como parte da chamada - Judas Priest Tour. Em uma dessas
                apresentações, especificamente a realizada em 25 de novembro de 1969 no The George Hotel de Walsall, estavam na plateia alguns executivos
                da Harvest Records e da Immediate Records, bem como o vocalista do Led Zeppelin, Robert Plant. Graças a Plant, a banda firmou nessa mesma
                noite um contrato de três anos com o selo Immediate, pelo qual sairia seu disco de estreia. Todavia, em poucos meses o selo quebrou,
                desencadeando um desânimo nos integrantes e causando a separação do grupo.
            </p>

            <p>
                Em meados de 1970 Atkins regressou a Birmingham, onde, segundo suas próprias palavras "numa noite em frente ao Holy Joe's em Wednesbury
                escutei uma banda que soava muito bem; seu nome era 'Freight' e era formada por K. K. Downing, Ian Hill e John Ellis. "Ao notar que não
                tinham vocalista, Atkins se ofereceu para integrar a banda e acabou sendo aceito. Com sua chegada, apresentou-lhes suas canções escritas
                com seu grupo anterior e também se propôs a utilizar o nome Judas Priest.
            </p>

            <p>
                Esta segunda formação formada por Atkins na voz, Downing na guitarra, Hill no baixo e Ellis na bateria, iniciaram a composição de
                novas canções focadas num estilo mais blues rock e hard rock. Desta maneira, em 16 de março de 1971 deram seu primeiro concerto
                ( Essington St. John's Hall ) em Staffordshire, que iniciava a turnê - The Return of the Priest Tour. Esta série de concertos
                permitiu-os tocar em vários clubes e teatros do Reino Unido, geralmente como apoio das bandas Slade, Budgie e Warhorse, e em
                alguns eventos compartilharam o palco com o guitarrista Gary Moore. Em julho do mesmo ano, Atkins escreveu as canções "Holy is
                the Man" e "Mind Conception", que foram lançadas numa demo de maneira independente e que, segundo o mesmo, só dez pessoas
                ouviram nessa época.
            </p>

            <p>
                Com seus primeiros discos nos anos 1970 a banda angariou uma base crescente de fãs, e após o lançamento dos álbuns - British Steel
                (1980) e - Screaming for Vengeance (1982), o quinteto ganhou fama mundial, obtendo várias certificações de ouro e platina com o
                decorrer dos anos. Com mais de quarenta anos de carreira e dezessete álbuns de estúdio lançados, o Judas Priest já vendeu cerca de
                45 milhões de discos mundialmente. Em 2009 receberam o Grammy Award, vencendo na categoria 'Best Metal Performance' com a canção
                "Dissident Aggressor".
            </p>

            <p>
                Antes do lançamento do álbum - Turbo, Rob Halford deu entrada em um hospital de reabilitação em novembro de 1985. Tudo começou por
                uma grave depressão devido aos anos em que teve que manter em segredo sua sexualidade, além do seu consumo excessivo de drogas e
                álcool. Ao final de novembro e durante os ensaios do que seria a turnê do álbum - Turbo, tomou uma grande quantia de analgésicos
                com a intenção de se matar. Apesar de ter sobrevivido, foi internado em um hospital para desintoxicar-se e, mais tarde, entrou em
                um centro de reabilitação. Finalmente em fevereiro de 1986 recebeu alta e começou um extenso trabalho com sua voz, com o intuito
                de fomentar novamente sua carreira. Em 2001 o produtor Tom Allom comentou sobre o caso: "Rob tinha essa voz incrível que qualquer
                cantor de metal desejaria ter e está melhor do que nunca desde que parou de beber".
            </p>


            <p>
                Por fim, o Judas Priest tem recebido dezenas de prêmios e homenagens ao longo dos anos, tanto para seus álbuns como para o conjunto.
                Destacam-se a inclusão no Hall da Fama da revista Kerrang! em 2007 e os prêmios Revolver Golden Gods em 2010 e o Golden Gods Icon, ambos
                concedidos pela revista Metal Hammer. Em 2006, junto a Queen, Kiss e Def Leppard, foram os primeiros artistas a ser induzidos ao VH1
                Rock Honors, cerimônia que honra as bandas que exerceram influência no rock.
            </p><br />

