            <h1>A Revolta dos Dândis (1987)</h1>

            <p>- A Revolta dos Dândis é o segundo álbum da banda Engenheiros do Hawaii. Foi gravado e lançado em 1987 e marca uma mudança na
                sonoridade da banda, até então próxima ao ska de bandas como The Police e Paralamas do Sucesso.</p>

            <p>O disco marcou a estreia do guitarrista Augusto Licks, enquanto Humberto Gessinger assume o baixo, isso foi responsável pelos acordes
                de baixo, dando ao disco uma sonoridade pós-punk, que investe mais arduamente em uma linha de baixo do que guitarras, dando também uma
                impressão de ska. "Quem Tem Pressa", "Não Se Interessa" foi a única faixa cuja gravação não contou com a colaboração do guitarrista,
                já que a mesma foi composta apenas de baixo, bateria e vocal.</p>

            <p>Seus principais sucessos foram a faixa-título (dividida em duas partes, sendo que a primeira obteve maior sucesso), "Terra de Gigantes"
                (que contou com um videoclipe), e "Infinita Highway" (canção cujo título deu origem ao nome da turnê que promoveu o lançamento deste disco:
                Infinita Tour 87/88).</p>

            <p>Em 2008, "A Revolta dos Dândis" foi tema do programa: Discoteca MTV, que abordava os discos de maior importância para o rock nacional.
                Foi também incluído em uma lista da "Superinteressante" dos principais álbuns do rock brasileiro. Em 2017, o trio clássico foi reunido
                virtualmente em entrevista na comemoração dos 30 anos do álbum no podcast Rock Brasil 3.0, da rádio Unisinos FM, do Rio Grande do Sul.</p>

            <p>Foi nesse disco que começaram as primeiras brigas da banda com a crítica. Os Engenheiros eram chamados de elitistas, e até fascistas.
                Acusações causadas pelas citações presentes nas letras de Humberto que iam de Albert Camus a Jean-Paul Sartre. Humberto e Carlos responderam
                as acusações:</p>

            <p>Humberto: "Às vezes, a citação não precisa ser entendida. No mundo de hoje não tem diferença entre Albert Camus e Mike Tyson.
                São dois produtos de consumo. Eu saboreio Camus como saboreio Mike Tyson. A maioria do povo brasileiro entende mais de existencialismo
                do que de boxe. Cito Camus porque está mais próximo de mim. Acho que as pessoas entendem o que é 'dândi', pelo menos tanto quanto eu.
                A "obra aberta" possibilita que uma música seja entendida em todos os níveis. Os Titãs conseguem isso. Caetano, o mais genial de todos,
                não consegue. Talvez nem a gente consiga. A nível de "intelectuália" citar Camus é kitsch e demodé. Pra agradar a crítica eu citaria
                Levi Strauss na baía de Guanabara".</p>

            <p>Carlos: "Fascista não é fazer citações pretensamente intelectuais, mas deixar de fazer por julgar que as pessoas não vão entender. Isso é
                intelectual e elitista"...</p>
            <br /><br />

