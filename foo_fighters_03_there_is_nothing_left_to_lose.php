            <h1>There Is Nothing Left to Lose (1999)</h1>
            <br />

            <p>
                "There Is Nothing Left to Lose" é o terceiro álbum da banda Foo Fighters, lançado em 2 de novembro de 1999.
            </p>
            <br />

            <p>
                O álbum foi o primeiro do Foo Fighters a ganhar o prêmio Grammy de "Best Rock Album" e a canção "Learn to Fly" o prêmio de
                "Best Short Form Music Video".
                <br />
                O álbum teve uma das canções, "Breakout" como trilha sonora do filme - Me, Myself & Irene. </p>
            <br />

            <p>
                Antes da gravação, o guitarrista Franz Stahl foi demitido da banda, pois o frontman Dave Grohl sentiu que o guitarrista não havia
                encontrado seu lugar na banda. Nesse ponto, Grohl decidiu que a banda seria apenas um tripé para o álbum, junto com o baixista Nate Mendel e
                o baterista Taylor Hawkins. Tendo acabado de se escravizar no estúdio gravando o álbum anterior, The Colour and the Shape, e perdendo dois
                membros da banda no processo, ele decidiu comprar uma casa em Alexandria, Virginia e gravar o disco em seu porão sem a presença de uma gravadora
                durante a produção. Isso foi ajudado pela saída do Foo Fighters da Capitol Records depois que o presidente Gary Gersh deixou o selo.
                Grohl batizou sua casa como Studio 606, dizendo: "É apenas um daqueles números que estão por toda parte. Como quando você acorda no meio da
                noite e são 6h06, ou vê uma placa que diz 606". Grohl montou tudo com a ajuda de Adam Kasper, que eventualmente co-produziu o álbum. O maior
                desafio, de acordo com Grohl, foi fazer o disco soar bem sem programas de computador como Pro Tools ou AutoTune.
            </p>
            <br />

            <p>
                Dave Grohl disse: "[...] moro em Los Angeles há cerca de um ano e meio, apenas sendo um bêbado, me fo... todas as noites e fazendo
                merdas horríveis, e finalmente enjoei daquele cheiro de carro novo. Então comprei esta grande casa na Virgínia e disse a todos que
                estava construindo um estúdio no porão. Era literalmente um porão com sacos de dormir nas paredes!".
            </p>
