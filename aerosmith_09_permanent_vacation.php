            <h1>Permanent Vacation (1987)</h1>


            <p>
                - Permanent Vacation é o nono álbum do Aerosmith. Lançado em 18 de agosto de 1987, e possui 12 canções.
            </p>


            <p>
                O álbum marcou uma virada na carreira da banda. Foi o primeiro a empregar compositores fora da banda, em vez de apresentar
                músicas compostas exclusivamente por eles. Isso veio por sugestão do executivo John Kalodner. Ele também incentivou a banda a
                trabalhar com o produtor Bruce Fairbairn , que permaneceu com eles por mais dois álbuns. Foi também o primeiro álbum do
                Aerosmith a ser promovido por um videoclipe pesado na MTV. Embora - <a href="aerosmith_08_done_with_mirros.php" target="blank"> Done with Mirrors</a>
                tivesse a intenção de marcar o retorno do Aerosmith, - Permanent Vacation é frequentemente considerado seu verdadeiro retorno, já que
                foi o primeiro álbum verdadeiramente popular da banda desde sua reunião. "Rag Doll", "Dude (Looks Like a Lady)" e "Angel" tornaram-se
                grandes sucessos (todos os três colocados no Top 20) e ajudaram - Permanent Vacation a se tornar o maior sucesso da banda em uma década.
            </p>


            <p>
                Este álbum também possui um cover, a faixa 11 "I'm Down", escrita por John Lennon e Paul McCartney, e gravada originalmente pelos Beatles.
            </p>


            <p>
                - Permanent Vacation vendeu mais de cinco milhões de cópias nos Estados Unidos; No Reino Unido, foi o primeiro álbum do Aerosmith
                a obter as certificações Prata (60.000 unidades vendidas) e Ouro (100.000 unidades vendidas) pela British Phonographic Industry,
                obtendo-as em julho de 1989 e março de 1990, respectivamente.
            </p>
