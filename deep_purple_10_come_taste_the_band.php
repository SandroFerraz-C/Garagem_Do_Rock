            <h1>Come Taste the Band (1975)</h1>

            <p>- Come Taste the Band é o décimo álbum do Deep Purple, originalmente lançado em outubro de 1975.</p>

            <p>O álbum foi co-produzido e concebido pela banda e antigo associado Martin Birch. É o único registro de estúdio com Tommy Bolin,
                que substituiu Ritchie Blackmore na guitarra. Musicalmente, o álbum é mais comercial que os lançamentos anteriores, tendendo a um
                rígido foco de hard rock com toques de soul e funk.</p>

            <p>Sobre o título do álbum, Glenn Hughes, no documentário Phoenix Rising, conta que Tommy Bolin, após uma noite em que ambos beberam
                bastante, proferiu a seguinte frase: "Come taste the wine, come hear the band" ("Venha provar o vinho, venha ouvir a banda").
                Hughes então teria feito a junção das duas frases, originando daí o nome do álbum.</p>

            <p>O álbum vendeu razoavelmente bem após o lançamento (# 19 nas paradas do Reino Unido, e # 43 nos Estados Unidos). O álbum foi
                certificado de prata em 1 de novembro de 1975 pelo BPI, vendendo 60.000 cópias no Reino Unido.</p>

            <p>Nos últimos anos, o álbum recebeu algumas reavaliações críticas "por seus próprios méritos", em parte devido às contribuições de
                Bolin para o álbum. Gillan (que deixou a banda há pouco mais de dois anos), por outro lado, afirmou que não vê o álbum como um
                verdadeiro álbum do Deep Purple. Lord elogiou a qualidade do álbum anos depois em entrevistas, afirmando que "ouvindo agora, é um
                álbum surpreendentemente bom", embora reconhecendo, "a pior coisa que você pode dizer sobre ele é que, na opinião da maioria das
                pessoas, não é um álbum do Deep Purple".</p>
