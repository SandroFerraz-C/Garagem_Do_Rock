            <h1>A Matter of Life and Death (2006)</h1>

            <p>
                - A Matter of Life and Death é o décimo quarto álbum do Iron Maiden. Lançado em 28 de agosto de 2008.
            </p>

            <p>
                É o terceiro disco gravado com a atual formação, precedido por - Brave New World e - Dance of Death. Dois singles foram lançados,
                "The Reincarnation of Benjamin Breeg" e "Different World". O disco vem em duas versões: uma simples e outra com um DVD bônus.
            </p>

            <p>
                A banda negou que estivesse fazendo um álbum conceitual, mas a maioria das faixas lida com guerra e religião. Em especial,
                "Brighter Than a Thousand Suns" é inspirada no Projeto Manhattan (com o título sendo uma citação do Bagavadguitá usada por J.
                Robert Oppenheimer pouco após a detonação da primeira bomba nuclear), "The Pilgrim" se baseia na jornada do Mayflower, e
                "The Longest Day" conta sobre o Dia D.
            </p>

            <p>
                A capa do álbum foi criada por Tim Bradstreet, um artista americano mais conhecido por seu trabalho nos quadrinhos Hellblazer e
                Punisher. Fã de longa data do Iron Maiden, Bradstreet descreveu a oportunidade como "um dos meus sonhos mais loucos". Ele tinha
                um prazo apertado, recebendo como referência as canções para ouvir e as principais artes feitas pela Peacock Designs UK, que
                dirigiu a arte do livreto. Junto com o redesenho de alguns dos elementos originais - que apresentavam o tanque descrito por
                Bradstreet como " a Guerra da Coréia" quando o cano da arma é remendado de outro veículo - Bradstreet acrescentou Eddie como um
                general liderando um exército de esqueletos. A capa traseira originalmente apresentaria Eddie baleado nas costas, mas então a
                banda optou por apresentar um detalhe do ícone do tanque com a cabeça de Eddie junto com armas cruzadas.
            </p>
