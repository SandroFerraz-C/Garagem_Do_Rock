            <h1>Use Your Illusion II (1991)</h1>
            <br />

            <p>
                "Use Your Illusion II" é o quarto álbum de estúdio do Guns N' Roses, lançado em 17 de setembro de 1991, simultaneamente com Use Your Illusion I.
            </p>
            <br />

            <p>
                As vendas dos - Illusion somaram-se 960 mil cópias no primeiro dia, recorde nunca superado por nenhuma banda ou artista solo na história
                da música. - Use Your Illusion II é o mais popular dos dois lançamentos. Foi o último com o guitarrista base Izzy Stradlin e também o
                último a contar com alguma participação de Steven Adler.
            </p>
            <br />

            <p>
                Possuindo faixas com letras mais sérias e pesadas, como "You Could Be Mine" e "Shotgun Blues", passando por músicas de protesto
                como "Civil War" e também contendo musicas como "Knocking On Heaven's Door" (Bob Dylan) e "So Fine". E músicas longas como
                "Estranged" e "Locomotive". O videoclipe de "Estranged" custou à banda cerca de 4 milhões de dólares.
                <br />
                Ecom mais influências do hard rock que o primeiro álbum, - Use Your Illusion II vendeu até hoje cerca de 19 milhões de cópias.
            </p>
            <br />

            <p>
                A capa de Use Your Illusion II é semelhante à de Use Your Illusion I, sendo diferente apenas a cor azul no lugar da amarela.
                Ambas as capas são baseadas em um detalhe da tela do artista renascentista Rafael "A Escola de Atenas" e foram confeccionadas pelo
                artista estoniano-americano Mark Costabi.
            </p>
