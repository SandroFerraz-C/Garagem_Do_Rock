            <h1>Redeemer of Souls (2014)</h1>
            <br />

            <p>
                - Redeemer of Souls é o décimo sétimo álbum do Judas Priest.
            </p>
            <br />

            <p>
                É o primeiro álbum sem o guitarrista K. K. Downing, que saiu da banda em 2011 e foi substituído pelo novo guitarrista Richie Faulkner.
                Também marca o último álbum da banda a ser produzido por Glenn Tipton. O álbum vendeu cerca de 32.000 cópias nos Estados Unidos em sua
                primeira semana de lançamento para aterrissar na posição #N6 na Billboard 200 chart, a segunda posição mais alta nas paradas da banda nos
                EUA. Isso marca a estréia do primeiro álbum no top #10 da banda nos Estados Unidos. Vendeu 110.000 cópias nos Estados Unidos apenas no mês de
                fevereiro de 2016.
            </p>
            <br />

            <p>
            Halford descreve o som do álbum como "pesado". - "É pesado; É algo que achamos que nossos fãs ficarão emocionados. Sabemos que 
            temos uma reputação a manter e sabemos que temos que entregar algo realmente forte e sólido. O álbum está cheio de todas
             as grandes coisas que você ama sobre Judas Priest - Eu não acho que posso dizer nada mais do que isso sem ser pendurado, puxado e
              esquartejado".
            </p>
            <br />

            <p>
            Sobre se a tecnologia mudou o processo de composição da banda, Halford disse: "É perigoso andar por aí com um flash drive em um 
            monte de teclas. Em grande medida, isso não muda realmente. A tecnologia é incrível em termos das vantagens que ela traz para a
             música agora, algumas boas, outras muito ruins. É tudo uma questão de disciplina e autoconfiança, determinação, querer fazer o 
             melhor que você pode fazer e não aceitar nada que esteja abaixo do normal. sempre tive essa atitude no Priest. Sempre nos sentimos 
             muito fortes sobre qualquer faixa que vai para nossos fãs. Ainda estamos fazendo como sempre fizemos: aumentando os riffs e encontrando 
             uma melodia vocal para acompanhá-la, eu entrando em mim maravilhoso mundo do Thesaurus de Rogete tentando criar uma nova letra e uma nova 
             ideia. E é isso que temos feito há quatro décadas".
            </p>
            <br /><br />

            <p>Duração do album: 61 min. 58 seg.</p>
            <p>Gravadora(s): Epic Records.</p>
            <p>Produção: Glenn Tipton e Mike Exeter.</p>
