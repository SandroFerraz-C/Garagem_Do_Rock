            <h1>Que País É Este (1987)</h1>
            <br />

            <p>
                - Que País É Este é o terceiro álbum da Legião Urbana, lançado em 1987.
            </p>
            <br />

            <p>
                No Brasil foram vendidos mais de 1 milhão de cópias do álbum, sendo o #3° mais vendido da banda e premiado com Disco de Diamante
                pela ABPD. O encarte do disco traz uma charge do baterista Marcelo Bonfá retratando a constante troca de guitarristas pela qual a
                banda passou até recrutar Dado Villa-Lobos.
            </p>
            <br />

            <p>
                Além da pressão da gravadora (intensificada pelo fato de que a banda já havia extrapolado o prazo estabelecido em contrato de entregar
                três discos em 36 meses), o próprio vocalista e violonista Renato Russo se cobrava para gravar logo algumas canções da época do Aborto
                Elétrico ("Que País É Este", "Conexão Amazônica" e "Tédio") antes que o Capital Inicial - outra banda fundada por ex-membros do Aborto -
                o fizesse. Isso contribuiu para que a ideia de um disco de inéditas fosse esquecida em favor de uma espécie de antologia. Na época, a banda
                negava, contudo, que tivesse enfrentado pressão por parte da gravadora.
            </p>
            <br />

            <p>
                Durante a turnê deste álbum, aconteceu o famoso show em Brasília no Estádio Mané Garrincha, para um público de 50 mil pessoas, em junho de
                1988. A apresentação se iniciou com uma hora de atraso. Na quarta canção, "Conexão Amazônica", Renato Russo foi atacado por um homem da
                plateia, que foi retirado pelos seguranças. Conforme o show decorria, o público começou a atirar objetos nos músicos e Renato respondia
                com provocações. Depois de uma hora, a banda deixou o palco e a plateia, frustrada, iniciou um empurra-empurra que resultou em um
                pisoteamento; 380 pessoas precisaram de atendimento médico após o episódio e a banda chegou a ser processada pelo Governo do Distrito
                Federal.
                <br /><br />

                Renato chegou a declarar que não voltaria mais a Brasília depois do incidente, mas voltou atrás pouco depois. Um mês depois, quando a
                banda se apresentou no Maracanãzinho, o público atirou margaridas ao palco em resposta ao incidente na capital brasileira.
                <br /><br />

                O episódio acabou aumentando a fobia de palco de Russo e tornando o trabalho da banda mais introspectivo, o que refletiria no álbum seguinte.
            </p>
