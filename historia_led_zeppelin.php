            <p>
                Depois de mudar seu antigo nome de New Yardbirds, para Led Zeppelin assinou um contrato favorável com a Atlantic Records, que lhes
                ofereceu uma considerável liberdade artística. O grupo não gostava de lançar suas canções como singles, pois viam os seus álbuns
                como indivisíveis e completas experiências de escuta. Embora inicialmente impopular com os críticos, o grupo conseguiu um impacto
                comercial significativo nas vendas com - Led Zeppelin (1969), - Led Zeppelin II (1969), - Led Zeppelin III (1970), o quarto álbum
                - Led Zeppelin IV (1971), - Houses of the Holy (1973), e - Physical Graffiti (1975). O quarto álbum, com a música "Stairway to Heaven",
                está entre as obras mais populares e influentes do rock e ajudou a consolidar a popularidade do grupo.
            </p>


            <p>
                Em seu primeiro ano, o Led Zeppelin completou quatro turnês nos Estados Unidos e no Reino Unido, e também lançou dois Álbuns,
                - Led Zeppelin I e - Led Zeppelin II. O segundo Gravado quase inteiramente na estrada em vários estúdios norte-americanos, teve sucesso
                comercial ainda maior do que o primeiro, e alcançou a posição de número #1 nos Estados Unidos e Reino Unido. O álbum desenvolveu,
                principalmente, mais o estilo musical do blues rock criado em seu disco de estreia, criando uma obra com um som direto que era
                "pesado e duro, brutal e direto", e que seria muito influente e muitas vezes imitado. Steve Waksman sugeriu que - Led Zeppelin II
                foi "o ponto de partida para o heavy metal".
            </p>

            <p>
                Em 1970, Page e Plant se retiraram para Bron-Yr-Aur, uma casa remota no País de Gales, para começar a trabalhar em seu terceiro
                álbum, - Led Zeppelin III. Foi uma virada decisiva do heavy metal direto para uma música mais leve. O resultado foi um estilo mais
                acústico fortemente influenciado pela música folk, celta, o country e sons de rock suave da Costa Oeste. O álbum inicialmente
                recebeu reações mistas, com os críticos e fãs surpresos com a mudança principalmente elétrica dos arranjos dos dois primeiros
                álbuns. Alcançou o número #1 no Reino Unido e nas paradas dos Estados Unidos, onde venderia seis milhões de cópias, mas sua permanência
                nos gráficos seria a menor de seus cinco primeiros álbuns. A faixa de abertura do álbum, "Immigrant Song", foi lançado como um single nos
                Estados Unidos em novembro de 1970, contra a vontade da banda, e ficou entre as #20 melhores canções nos gráficos da Billboard.
            </p>

            <p>
                Na década de 1970, o Led Zeppelin atingiu novos patamares de sucesso comercial e de crítica de um dos grupos mais influentes da
                época, superando suas conquistas anteriores. A imagem da banda também mudou quando os membros começaram a usar elaboradas roupas
                extravagantes. O quarteto começou a viajar em um avião a jato proprio, um Boeing 720 (apelidado de Starship), alugar seções
                inteiras de hotéis (incluindo a Continental Hyatt House, em Los Angeles, conhecido popularmente como o "Riot House"), e
                tornou-se o assunto frequente de repetidas histórias de devassidão. Uma dessas histórias envolveu John Bonham dirigindo uma
                motocicleta num piso alugado do Riot House, enquanto a outra envolvia a destruição de um quarto no Hilton de Tóquio, levando
                o grupo a ser banido do estabelecimento para a resto da vida. Embora os músicos desenvolveram uma reputação de destruidores de
                suas suítes de hotel e a fama de jogar aparelhos de televisão para fora das janelas, alguns sugerem que estes relatos têm sido
                exagerados. O jornalista musical Chris Welch argumenta que "as viagens [do Led Zeppelin] geraram muitas histórias, mas era um
                mito de que [eles] estavam constantemente envolvidos em atos de destruição arbitrária e comportamentos lascivos".
            </p>

            <p>
                O Led Zeppelin é amplamente considerado como uma das mais bem sucedidas, inovadoras e influentes bandas de rock na história da
                música. O crítico de rock Mikal Gilmore disse: "Led Zeppelin — talentoso, complexo, ávido, bonito e perigoso — fez um dos corpos
                mais duradouros da composição e interpretação na música do século XX, apesar de tudo o que tinham para dominar, incluindo a si
                mesmos".
            </p>
