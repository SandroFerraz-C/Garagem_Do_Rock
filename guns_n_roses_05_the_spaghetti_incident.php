            <h1>The Spaghetti Incident? (1993)</h1>
            <br />

            <p>
            "The Spaghetti Incident?" é o quinto álbum gravado pelo Guns N' Roses antes da longa pausa, antecessor do Chinese Democracy.
         </p>
            <br />

            <p>
            Marca também o fim da era clássica do Guns. O disco consiste de covers de músicas punk e rock dos anos 70 e 80. Muitas das faixas 
            foram gravadas com o guitarrista original Izzy Stradlin, durante as sessões do - Use Your Illusion I e - II. Essas faixas foram 
            previamente destinadas a serem uma combinação dos - Use Your Illusion, composto por três (ou possivelmente até mesmo quatro) discos, 
            em vez dos dois discos separados que ele acabou sendo. Foi o primeiro álbum gravado sem Izzy Stradlin, já que o mesmo abandonou a banda no 
            fim de 1991, sendo substituído por Gilby Clarke, e o último a ter Slash, Duff Mckagan e Matt Sorum.
            </p>
            <br />

            <p>
            A recepção de - The Spaghetti Incident? não foi tão fervorosa quanto a dos últimos álbuns. Vendeu cerca de 190 mil cópias na primeira 
            semana, alcançando a #4° posição na Billboard 200, bem menos que os outros álbuns da banda e o álbum foi duramente criticado pelas 
            grandes revistas. Até hoje o álbum vendeu cerca de 1,5 milhões de cópias só nos EUA e 6 milhões em todo o mundo.
            <br />
            Apesar da recepção ruim, houve também outros motivos para a baixa quantidade de vendas do álbum, como a não realização de uma turnê
             para promover o álbum, além de a maioria dos integrantes da banda estarem envolvidos com projetos paralelos e só terem sido lançados 
             três singles, sendo que dois deles eram versões para rádios.
            </p>
            <br />

            <p>
            Um cover não anunciado ( Não continha a faixa no encarte do álbum ) da música "Look at Your Game, Girl" de Charles Manson foi incluído 
            no álbum. A faixa foi mantida em segredo, incluindo não estar em fitas previamente enviadas aos revisores. O empresário da banda Doug Goldstein 
            afirmou: "Há uma faixa bônus no álbum, mas Axl quer que a faixa fale por si" e, "Não foi feito para os críticos ou qualquer outra pessoa. 
            Foi um bônus para os fãs". A inclusão da música causou polêmica, com grupos de defesa da lei e de direitos das vítimas expressando indignação 
            com sua inclusão.
            </p>
