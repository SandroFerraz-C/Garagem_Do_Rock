            <h1><span lang="en-US">BALLBREAKER</span> (<time datetime="1995">1995</time>)</h1>

            <p>Foi o primeiro álbum do AC/DC em cinco anos, desde <a href="acdc_14_the_razor's_edge.php" target="blank"><span lang="en-US">The Razors Edge</span></a> de (1990).</p>

            <p>O álbum marcou o retorno do baterista Phil Rudd, que tocou com o AC/DC de <time datetime="1975">1975</time> a <time datetime="1983">1983</time>. Rudd havia saído durante as gravações de - <a href="acdc_10_flick_of_the_switch.php" target="blank"><span lang="en-US">Flick of the Switch</span></a> devido a problemas com drogas e sua incompatibilidade com Malcolm Young.</p>

            <p>
            <blockquote>De acordo com o livro de <cite>Arnaud Durieux - <span lang="en-US">AC/DC: Maximum Rock & Roll</span> -</cite>, Rudd compareceu ao show do AC/DC em Auckland em <time datetime="1991-11">novembro de 1991</time> e, após um encontro amigável com a banda nos bastidores,
                fez uma audição para se juntar novamente a banda. Durieux relata que a banda eventualmente convidou Rudd para voltar e ele aceitou em <time datetime="1994-08">agosto de 1994</time>, para grande desgosto de Slade ( Que era na época o baterista da banda ) e estava gravando
                demos com a banda em Londres.</blockquote>
            </p>

            <p>
            <blockquote> Slade disse ao <span lang="en-US"><cite>Rock Hard France</cite></span> em <time datetime="2001-06">junho de 2001</time> que ele estava tão desapontado e enojado que não tocou em sua bateria por três anos. Slade voltaria a se juntar à banda para sua performance na #57ª Cerimônia Anual de Prêmios Grammy e na <span lang="en-US">Rock or Bust World Tour</span> 2015 após as questões legais de Rudd envolvendo porte ilegal de drogas e ameaça de matar seu ex-assistente pessoal, ambos os quais o forçaram sua saida da banda uma segunda vez.</blockquote>
            </p>

            <p>Os três singles liberada a partir deste álbum foram “<span lang="en-US">Hard as a Rock</span>,” “<span lang="en-US">Cover You in Oil</span>,” e “<span lang="en-US">Hail Caesar</span>”. O álbum foi produzido por Rick Rubin. Rubin tinha anteriormente trabalhado com a banda para produzir a canção / single “<span lang="en-US">Big Gun</span>”, que foi lançada
                dois anos antes como trilha sonora do filme <span lang="en-US">Last Action Hero</span>.</p>

            <p>O álbum alcançou o top #10 em muitos países, incluindo #4° lugar nos Estados Unidos, #6° no Reino Unido e liderando as paradas da Finlândia, Suécia e Suíça. Atualmente é certificado 2x platina nos Estados Unidos, com vendas superiores a dois milhões de discos.</p>

            <p>
            <blockquote><cite>Jancee Dunn</cite> da <span lang="en-US">Rolling Stone</span> concedeu ao álbum duas de cinco estrelas e escrevendo: <q>A longevidade deles pode ser creditada a dois fatores: Nostalgia e o fato de que o AC/DC ainda vê o mundo através da mente de um adolescente excitado de 15 anos</q>.</blockquote>
            </p>
