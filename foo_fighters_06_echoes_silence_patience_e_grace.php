            <h1>Echoes, Silence, Patience & Grace (2007)</h1>
            <br />

            <p>
                "Echoes, Silence, Patience & Grace" é o sexto álbum do Foo Fighters, lançado em 25 de setembro de 2007, através Roswell e RCA Records.
            </p>
            <br />

            <p>
                O álbum é conhecido por uma mistura de rock regular e faixas acústicas com dinâmica variável, que emergiu da variedade de estilos
                empregados nas demos que a banda produziu. É também a segunda vez que a banda trabalha com o produtor Gil Norton, a quem o vocalista
                Dave trouxe para explorar plenamente o potencial de suas composições e ter um registro que soou diferente de seus trabalhos anteriores.
                Grohl tentou se concentrar em canções com mensagens que ressoassem com seu público, escrevendo letras reflexivas que se inspiraram no
                nascimento de sua filha.
            </p>
            <br />

            <p>
                A recepção crítica a "Echoes, Silence, Patience & Grace" foi geralmente positiva, com elogios à variedade sonora e composição,
                embora alguns críticos considerassem o álbum inconsistente e pouco inspirado. O álbum liderou as paradas no Reino Unido, Austrália,
                Nova Zelândia e Áustria, e teve três singles de sucesso, "The Pretender", "Long Road to Ruin" e "Let It Die". "Echoes, Silence, Patience & Grace" foi
                indicado a 5 prêmios Grammy, ganhando o de Melhor Álbum de Rock, e também foi premiado com o Brit Award de Melhor Álbum Internacional.
            </p>
            <br />

            <p>
                A preparação do álbum foi extensa: primeiro Grohl teve seu começo habitual desenvolvendo demos com o baterista Taylor Hawkins, mas
                pela primeira vez Grohl tentou inserir vocais e letras neste estágio inicial de composição. Depois de terminar a composição com o
                guitarrista Chris Shiflett e o baixista Nate Mendel, Grohl passou duas semanas com Norton discutindo "arranjos, harmonia e melodia"
                e reduzindo as idéias das músicas, e então a banda passou quatro semanas ensaiando, tocando "uma música por dia, do meio-dia à meia-noite".
                Hawkins afirmou que "basicamente tocamos cada uma dessas músicas 100 vezes diferentes, tentando cada pequena coisa de maneiras diferentes"
                e que foi a primeira vez desde - The Color and the Shape “que Dave teve que lidar com alguém na sala questionando todas as suas ideias”,
                dada a abordagem mais descontraída do produtor anterior Nick Raskulinecz. Grohl afirmou que as escolhas eram para as "músicas mais poderosas
                e dramáticas", e que havia um esforço para "fazer tudo soar o mais natural possível - assim como nos álbuns que crescemos ouvindo", citando
                Artistas da década de 1970, como - Neil Young e - Wings, tiveram grande influência. Shiflett acrescentou que pela primeira vez ele tocou
                guitarra em algumas faixas, enquanto Grohl "geralmente trabalha com todas as partes" da composição.
            </p>
