							<p>
								O instrumento mais comum neste estilo é a

								<a href="#">Guitarra</a>,
								sempre presente nas bandas, podendo possuir um único instrumentista, ou dois com funções diferenciadas,

								(<a href="#">Guitarrista Base</a> e <a href="#">Guitarra Solo</a>).
							</p>

							<p>
								Normalmente, as bandas, além do instrumento predominante, a guitarra, são formadas por um:

								<a href="#">Contrabaixo</a> (após <time datetime="1950">1950</time>, um
								<a href="#">Baixo Elétrico</a>) e uma <a href="#">Bateria</a>.

								Nos primórdios do <span lang="en-US">Rock and Roll</span>, também se utilizava o <a href="#">Piano</a> e/ou o <a href="#">Saxofone</a>
								frequentemente como instrumentos bases, mas estes foram substituídos ou suplantados geralmente pela guitarra a
								partir da metade da decada de <time datetime="1950">1950</time>.
							</p>

							<p>
								A batida é essencialmente um Blues com <span lang="en-US">Country</span> com um<a href="#">contratempo</a>
								acentuado, este último quase fornecido por uma <a href="#">Caixa-Clara</a>.
								A enorme popularidade e eventual visão no mundo inteiro do <span lang="en-US">Rock and Roll</span> deu-lhe um impacto social único.
								Muito além de ser simplesmente um gênero musical, como visto nos filmes e na televisão e de acordo com a mídia
								que se desenvolvia na época, influenciou estilos de vida, moda, atitudes e linguagem.
							</p>

