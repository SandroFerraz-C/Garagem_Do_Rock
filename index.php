						<header class="content-header">
							<h2 class="sub-title">Cultura adolescente.</h2>
						</header>
						<br />

						<p>
							Vários historiadores do <span lang="en-US">Rock</span> afirmam que o <span lang="en-US">Rock and Roll</span> foi um dos primeiros gêneros
							musicais a definir uma faixa etária. Isso deu aos adolescentes uma sensação de pertencimento, mesmo quando estavam sozinhos.
						</p>

						<p>
							O <span lang="en-US">Rock and Roll</span> é frequentemente identificado com o surgimento da cultura adolescente entre a primeira geração
							<span lang="en-US">baby boomer</span> (Termo usado para pessoas nascidas entre <time datetime="1946">1946</time> e <time datetime="1965">1965</time>;
							Anterior a Geração "X"), que tinha maior riqueza relativa e tempo de lazer e adotou o <span lang="en-US">Rock and Roll</span> como parte de uma subcultura distinta.
						</p>

						<p>
							Isso envolveu não apenas música, absorvida via rádio, compra de discos, <span lang="en-US">jukeboxes</span> e programas de TV como
							<span lang="en-US">American Bandstand</span>, mas também seestendeu a filmes, roupas, cabelos, carros e motos e linguagem distinta.
							A cultura jovem exemplificada pelo <span lang="en-US">Rock</span> foi uma fonte recorrente de preocupação para as gerações mais velhas, que se preocupavam com a
							delinquência juvenil e a rebelião social, principalmente porque em grande parte a cultura era compartilhada por diferentes grupos raciais e sociais.
						</p>

