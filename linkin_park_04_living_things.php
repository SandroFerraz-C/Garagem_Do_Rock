            <h1>Living Things (2012)</h1>
            <br />

            <p>
                - Living Things é o quinto álbum do Linkin Park.
            </p>
            <br />

            <p>
                O álbum foi lançado em 26 de junho de 2012 pela Warner Bros. Records. A produção do disco ficou a cargo do vocalista Mike Shinoda
                e de Rick Rubin, que também haviam co-produzido os dois álbuns anteriores do grupo, - Minutes to Midnight (2007) e - A Thousand Suns (2010).
            </p>
            <br />

            <p>
                A banda anunciou, através de várias entrevistas, que o álbum anunciava uma nova era para o grupo, combinando o antigo nu metal e
                rap rock com sons mais eletrônicos de trabalhos recentes, numa tentativa de, segundo eles, criar um som mais original. Ao fim de
                2012, o - Living Things já tinha vendido pelo menos 750 mil cópias nos Estados Unidos.
            </p>
            <br />

            <p>
                Em uma entrevista concedida em 21 de março de 2012 para a revista Kerrang!, Chester Bennington falou que a banda retornaria a
                "um território familiar" neste álbum, dizendo que "neste [novo] álbum, nós incorporamos muitos sons de guitarra com grandes refrões
                e coisas mais eletrônicas para transmitir um som bem forte sem que soassem muito como metal. Isso soará mais familiar para as
                pessoas do que o - A Thousand Suns foi, onde nós ficamos tipo 'Dane-se, nós vamos ser malucos'". Bennington também comentou que
                as letras do novo álbum seriam mais pessoais e evitariam política, completando dizendo "Nós escrevemos bastante sobre
                relacionamentos". Bennington e Shinoda deram declarações similares à revista Spin, comentando: "Nós sabemos que temos a
                habilidade e as ferramentas para pegar essas ideias e transformá-las em algo que nós estamos de fato procurando, ao invés de
                cair de cabeça nisso e descobrir que isso realmente soa como nu-metal. Isso sempre será nojento para nós, mas nós podemos pegar
                elementos dussi e reinventar a vibe, para torná-lo mais atual". A banda anunciou então as novas canções e uma colaboração com
                o músico canadense Owen Pallett. Mike e Chester também anunciaram que haviam adotado novas influências e assuntos para o disco,
                especialmente sobre pessoas. Bennington disse à rádio Live 105 que a banda estava "abraçando tudo que eles haviam feito no
                passado", tirando o que "tinha de melhor" dos quatro álbuns anteriores e "combinando-os neste novo álbum".
            </p>
            <br /><br />

