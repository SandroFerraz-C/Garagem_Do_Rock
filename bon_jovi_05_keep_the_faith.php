            <h1>Keep the Faith (1992)</h1>
            <br />

            <p>
                Keep The Faith é o quinto álbum de estúdio (cujo nome original era "Revenge") da banda Bon Jovi, lançado nos Estados Unidos em 1992.
            </p>
            <br />

            <p>
                É o primeiro sucesso comercial da banda considerado por muitos como o "verdadeiro" primeiro álbum do grupo, contendo letras mais políticas
                do que românticas, o álbum traz uma mensagem crítica da sociedade. Nele a banda assume também um novo visual, mudando os cabelos, entrando
                de fato na década de 90, onde eles deixam o glam de lado e começam a exibir traços de hard. O disco vendeu mais de doze milhões de cópias
                pelo mundo.
            </p>
            <br />

            <p>
                É o último álbum de estúdio do Bon Jovi a apresentar todos os cinco membros originais, já que o baixista Alec John Such foi demitido da banda em 1994.
                <br />
                Afastando-se de suas raízes iniciais de glam metal em álbuns anteriores, introduziu um groove mais "rock n roll" e afastou-se da bateria
                pesada e solos de guitarra selvagens, introduziu um novo som com canções mais longas que consistiam em baladas de piano e guitarra de longos solos.
            </p>
            <br />

            <p>
                Keep The Faith alcançou o quinto lugar na parada Billboard 200 dos Estados Unidos e foi certificado com dupla platina pela Recording
                Industry Association of America (RIAA). O álbum produziu vários sucessos para a banda, incluindo " Keep the Faith ", " Bed of Roses " e " In These Arms ".
            </p>
            <br />

            <p>
                Após a conclusão da turnê New Jersey, a banda entrou em um hiato. Durante o tempo livre, Jon Bon Jovi escreveu a trilha sonora de
                Young Guns II, que foi lançado em 1990, e Richie Sambora lançou seu primeiro álbum solo chamado Stranger in This Town.
                Nessa época, Jon Bon Jovi demitiu seu empresário de longa data, Doc McGhee, e criou o Bon Jovi Management. Jon Bon Jovi decidiu
                assumir um papel maior e ter mais responsabilidades dentro da banda. Em outubro de 1991, a banda foi para a ilha caribenha de St.
                Thomas discutir planos para o futuro; os problemas internos da banda foram resolvidos e eles estavam prontos para um retorno.
            </p>
