                na qual conseguiram um contrato com ampla liberdade artística. Projetado pelo próprio guitarrista e pelo engenheiro de som
                Glyn Johns, o trabalho exibia um som distintamente pesado, algo incomum no final dos anos 1960. Devido sua capa, originalmente
                concebida por George Hardie, cuja imagem apresenta um dirigível Zepelim pairando nas nuvens em chamas, os membros tiveram problemas
