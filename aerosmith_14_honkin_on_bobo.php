               - Honkin' on Bobo é o décimo quarto álbum do Aerosmith, lançado em 30 de Março de 2004 pela Columbia Records.
            </p>

            <p>
                O álbum inclui 11 covers de blues e blues rock das décadas de 1950 e 1960, com uma nova música, "The Grind". O álbum homenageia
                as primeiras influências do Aerosmith e apresenta um som mais cru, uma reminiscência de seu trabalho dos anos 1970, quando
                comparado com seus esforços comerciais mais recentes. - Honkin' on Bobo foi produzido por Jack Douglas, que foi o produtor do
                Aerosmith na grande maioria de sua produção dos anos 1970.
            </p>

            <p>
                O álbum vendeu mais de 160.000 cópias em sua primeira semana, alcançando a quinta posição na Billboard 200. - Honkin 'on Bobo foi
