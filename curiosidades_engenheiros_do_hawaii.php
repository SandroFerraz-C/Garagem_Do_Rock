                        começava a se popularizar, os Engenheiros sempre se mantiveram sóbrios, o que realmente não era muito comum nesse meio.</p>

                    <p>Mas também tinham sua próprio droga: eram viciados no trabalho. Escapavam da realidade tocando nos shows, de quinta a
                        domingo, e nas gravações de disco, de segunda a quarta”.</p>
