                <button class="btn2" data-toggle="collapse" data-target="#demo2">Curiosidade 02</button>

                <div id="demo2" class="collapse">
                    <br />
                    <p>- O nome da banda Engenheiros do Hawaii veio de uma brincadeira que os membros da banda fizeram com os estudantes de engenharia
                        da mesma faculdade que eles, em 1985. Os integrantes da banda eram estudantes de arquitetura e tinham algumas rixas com o pessoal
                        da engenharia. O nome veio como uma "zuação" aos "rivais" da faculdade que iam para a faculdade de bermudas de surfistas.</p>
                </div>
                <br /><br />

                <button class="btn3" data-toggle="collapse" data-target="#demo3">Curiosidade 03</button>

                <div id="demo3" class="collapse">
                    <br />
                    <p>- A banda também pegou fama de infantil por conta das diversas brincadeiras e sacanagens que os integrantes faziam entre
                        si e com a equipe, principalmente Maltz e Gessinger.</p>
                </div>
                <br /><br />

                <button class="btn4" data-toggle="collapse" data-target="#demo4">Curiosidade 04</button>

                <div id="demo4" class="collapse">
                    <br />
                    <p>- “Pois é, talvez eles fossem emocionalmente frágeis, passando essa imagem de infantis, mas foram bastante responsáveis
                        diante dos fãs, procurando fazer discos e shows que não se repetiam, sem cair no comodismo ou na irresponsabilidade.</p>
                </div>
                <br /> <br />

                <button class="btn5" data-toggle="collapse" data-target="#demo5">Curiosidade 05</button>

                <div id="demo5" class="collapse">
                    <br />
                    <p>- Quando surgiram e começaram a incomodar, os Engenheiros foram tachados (entre outros insultos) de ser uma “banda de direita”
                        e de “direitistas” por seus desafetos na cena do rock;
                        <br />
                        A banda chegou a fazer campanha de graça para Leonel Brizola.
                    </p>
                </div>
                <br /><br />

                <button class="btn6" data-toggle="collapse" data-target="#demo6">Curiosidade 06</button>

                <div id="demo6" class="collapse">
                    <br />
                    <p>- Foi Brizola que cedeu a foto ( Que era sua) do papa João Paulo II tomando chimarrão usada na capa do disco “O Papa é Pop”,
                        o mais vendido dos Engenheiros. A banda também deve a Brizola a inclusão da versão da música “Era Um Garoto Que Como Eu Amava
                        os Beatles e os Rolling Stones”, dos Incríveis, usada para preencher um dos atrasos do candidato em um comício em Betim,
                        Minas Gerais.</p>

                    <p>A partir de então, a canção se tornou um dos maiores sucessos da banda.</p>
                </div>
