                    <p>- “Se um critico ou jornalista escrevesse uma crítica dura, não havia interesse da banda de se explicar. Esse desinteresse
                        dos rapazes por formar amizades também era encarado como arrogância, deixando tudo mais difícil”.</p>
                </div>
