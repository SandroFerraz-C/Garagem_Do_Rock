                <button class="btn11" data-toggle="collapse" data-target="#demo11">Curiosidade 11</button>

                <div id="demo11" class="collapse">
                    <br />
                    <p>- Humberto Gessinger compôs a canção 'Infinita Highway' aos 12 anos de idade, época em que estava mergulhado na literatura de
                        Camus e Sartre, suas maiores influências.</p>
                </div>
                <br /><br />

                <button class="btn12" data-toggle="collapse" data-target="#demo12">Curiosidade 12</button>

                <div id="demo12" class="collapse">
                    <br />
                    <p>- Nas viagens pelo país, a diversão do trio era comprar discos raros em sebos. Gessinger ficou famosos por quase nunca
                        sair de seu quarto de hotel durante as turnês.</p>
                </div>
                <br /> <br />

                <button class="btn13" data-toggle="collapse" data-target="#demo13">Curiosidade 13</button>

                <div id="demo13" class="collapse">
                    <br />
                    <p>- A banda Engenheiros do Hawaii brinca com a técnica de “Backward Masking”, popularmente conhecida como “mensagens invertidas”
                        na música “Ilusão de Ótica”. Essa técnica é utilizada principalmente para reproduzir “mensagens subliminares”. Humberto entoa os
                        versos “Hei, não roda assim! Não gosto que rode assim!”, incitando a ouvir do modo inverso, e ao se fazer isso, ouve-se o seguinte:
                        “porque você está ouvindo isso ao contrário, o que você está procurando, heim!?!?”</p>
                </div>
                <br /><br />

                <button class="btn14" data-toggle="collapse" data-target="#demo14">Curiosidade 14</button>

                <div id="demo14" class="collapse">
                    <br />
                    <p>- O álbum "A Revolta dos Dândis" foi concebido para ser o primeiro de uma “trilogia das cores”. A relação entre a capa
                        amarela, unida aos discos subsequentes "Ouça o que eu Digo, Não Ouça Ninguém" (vermelha) e "Várias Variáveis" (verde)
                        foi feita para representar a bandeira do Rio Grande do Sul.</p>
                </div>
                <br /><br />

                <button class="btn15" data-toggle="collapse" data-target="#demo15">Curiosidade 15</button>

                <div id="demo15" class="collapse">
                    <br />
                    <p>- Gessinger também foi criticado pelo excesso de trocadilhos, metáforas e rimas aparentemente sem sentido em suas letras.
                        Sobre isso, ele já disse que suas obras são “abertas” e permitem diversas interpretações.</p>
                </div>
                <br /><br />

                <button class="btn16" data-toggle="collapse" data-target="#demo16">Curiosidade 16</button>

                <div id="demo16" class="collapse">
                    <br />
                    <p> - Um outro objetivo do nome da banda foi contrariar a moda do pop/rock na época, de batizar bandas com nomes grandiosos –
                        "Titãs", "Cavaleiros do Apocalipse", "Virgens Nucleares", "Legião Urbana", "Replicantes", "Garotos de Rua". Segundo Gessinger,
                        seria, ainda, uma forma de reduzir a expecatativa dos fãs em relação ao trabalho da banda, dando um ar "jocoso" e despretensioso à
                        sua obra. </p>
                </div>
