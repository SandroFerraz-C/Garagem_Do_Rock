            <h1>História do Deep Purple</h1>

            <p>
                Deep Purple é uma banda britânica de rock formada em Hertford, Hertfordshire, em 1968. Juntamente com as bandas
                <a href="black_sabbath.php" target="blank">Black Sabbath</a> e <a href="led_zeppelin.php" target="blank">Led Zeppelin</a>,
                o Deep Purple é considerado um dos pioneiros do heavy metal e do hard rock moderno, embora alguns de seus integrantes tenham
                tentado não se categorizar como apenas um destes gêneros. A banda também incorporou elementos do barroco, da psicodelia,
                do blues e do rock progressivo ao seu som. Foram listados pelo Livro Guiness dos Recordes "como a banda com o som mais alto ao
                vivo no mundo", e venderam mais de 100 milhões de álbuns ao redor do mundo.
            </p>

            <p>
                A banda passou por diversas mudanças de formação, além de um hiato de oito anos (1976-84). As formações do
                período 1968-76 foram comumente chamadas de fases I, II, III e IV. Sua segunda formação, a mais bem-sucedida
                comercialmente, contou com Ian Gillan (vocal), Ritchie Blackmore (guitarra), Jon Lord (teclado), Roger Glover
                (baixo) e Ian Paice (bateria). Esta formação esteve em atividade de 1969 a 1973, e foi reunida de 1984 a 1989 e,
                brevemente, em 1993, antes que os atritos entre o guitarrista Ritchie Blackmore e os outros membros da banda se
                tornassem intransponíveis. A formação atual é composta por Paice, Gillan, Glover, o guitarrista Steve Morse
                (membro desde 1994) e o tecladista Don Airey (que entrou em 2002 após o afastamento de Jon Lord).
            </p>

            <p>
                Ao final do show de 15 de março de 1976, em Liverpool, David Coverdale desabafa com Lord: não havia mais
                clima para continuar com o Deep Purple. Lord desabafa de volta: não havia mais um Deep Purple para continuar.
                Acabou assim, em clima de confidência, a banda criada oito anos antes e que chegou a figurar no Guinness dos
                recordes como a mais barulhenta do mundo. Oito meses depois, Bolin morreria de overdose no Resort Hotel de
                Miami, após uma apresentação. E durante oito anos o Deep Purple permaneceria fora do ar.
            </p>

            <p>
                Nesse período, os membros da banda fariam suas próprias carreiras e plantariam as bases para os futuros desenvolvimentos do Deep Purple.
                Por ordem de saída:
            </p>

