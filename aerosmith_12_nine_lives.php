            <h1>Nine Lives (1997)</h1>

            <p>
                - Nine Lives é o décimo segundo álbum do Aerosmith, lançado em 1997.
            </p>

            <p>
                Este foi o Décimo Segundo disco da banda, o disco vendeu cerca de 3 milhões de cópias em todo o mundo,
                foi indicado o Grammy Awards de melhor Álbum de Rock e teve sucessos nas rádios como "Falling in Love (Is Hard on the Knees)",
                "Hole in My Soul", "Full Circle" e "Pink".
            </p>

            <p>
                O álbum foi produzido por Aerosmith e Kevin Shirley, e foi o primeiro álbum de estúdio da banda lançado pela Columbia Records
                desde - <a href="aerosmith_07_Rock_in_a_hard_place.php" target="blank">Rock in a Hard Place</a> de 1982. Ele alcançou a posição
                número #1 na Billboard 200. Um dos singles do álbum, "Pink", ganhou um Grammy de Melhor Performance de Rock por grupo ou com
                vocal solo. Até, - <a href="aerosmith_15_music_from_another_dimension.php" target="blank">Music from Another Dimension!</a>,
                - Nine Lives foi o álbum mais longo, estreitamente vinculado ao anterior
                - <a href="aerosmith_11_get_a_grip.php" target="blank">Get a Grip</a> ambos com tempo total de trilha de 62 minutos.
            </p>

            <p>
                Em sua crítica ao AllMusic, Stephen Thomas comparou - Nine Lives aos álbuns anteriores do Aerosmith afirmando:
                “- Nine Lives, em contraste, é superlaborado, com o Aerosmith fazendo um esforço consciente para soar moderno e vibrante, o
                que ironicamente simplesmente os faz soar cansados.” Ele também criticou a produção problemática do álbum, dizendo:
                "Não apenas as performances são superficiais, mas as canções não são cativantes, não importa o quanto tente, "Falling in Love (Is Hard on the Knees)"
                nunca desenvolve um gancho, e não é uma exceção".
                <br />
                Elysa Gardner da Rolling Stone foi mais favorável em sua análise de 1997, concluindo:" Para aqueles que simplesmente podem
                'seguir uma coleção de músicas do Aerosmith sem ter sua cota de baladas poderosas, - Nine Lives não decepciona".
            </p>
