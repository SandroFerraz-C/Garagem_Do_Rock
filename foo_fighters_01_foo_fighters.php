            <p>
                Após a morte de Kurt Cobain em Abril de 1994, Grohl considerou abandonar a música, mas eventualmente resolveu gravar como
                "experiência catártica" suas composições. Em Outubro do mesmo ano, Grohl entrou no Robert Lang's Studio em Seattle com o amigo e produtor
                musical Barrett Jones. No espaço de uma semana, Grohl gravou as faixas cantando e tocando todos os instrumentos, com exceção de uma
                guitarra em "X-Static" por Greg Dulli do grupo americano Afghan Whigs. Para manter o anonimato, Grohl escolheu o nome "Foo Fighters" para
                criar a ilusão de que era uma banda em vez de "aquele cara do Nirvana".
            </p>
            <br />

            <p>
