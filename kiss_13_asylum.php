            <p>
                - Asylum é o primeiro álbum a apresentar o guitarrista principal Bruce Kulick como membro oficial da banda. Kulick substituiu o
                ex-guitarrista Mark St. John em algumas faixas do álbum anterior - Animalize (1984) durante a ausência do último devido a artrite
                reativa. Posteriormente, Kulick ocupou o lugar de St. John durante a maior parte da turnê - Animalize. Esta nova formação de
                Paul Stanley, Gene Simmons, Eric Carr e Kulick duraria até a morte de Carr em novembro de 1991, enquanto Kulick permaneceria com
                a banda até a reunião da formação original em 1996.
            </p>
            <br />

            <p>
                A capa do álbum retrata os quatro membros da banda com lábios coloridos, refletindo as cores dos lançamentos solo de 1978: vermelho
                para Simmons, roxo para Stanley, azul para Kulick (substituindo Frehley) e verde para Carr (substituindo Peter Criss ). A arte da
                contracapa é igualmente estilizada e colorida, retratando Kiss contra um fundo branco. A arte da capa do CD remasterizado de - Asylum
                em 1997 apresentava cores mais ousadas e ligeiramente alteradas do que as do lançamento original.
