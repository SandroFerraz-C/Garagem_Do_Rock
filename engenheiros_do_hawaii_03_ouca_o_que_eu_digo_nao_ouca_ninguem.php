            <h1>Ouça o Que Eu Digo: não Ouça Ninguém (1988)</h1>

            <p>- Ouça o que Eu Digo: Não Ouça Ninguém é o terceiro álbum dos Engenheiros do Hawaii, lançado em 1988.</p>

            <p>
                Os principais sucessos do disco foram "Somos Quem Podemos Ser", "A Verdade a Ver Navios", "Cidade em Chamas" e a faixa-título, "Ouça o que Eu Digo: Não Ouça Ninguém".
                Outras canções ganharam destaque maior anos depois, como "Nunca se Sabe", "Tribos e Tribunais" e "Pra Entender".
            </p>

            <p>Por estar entre os arrebatadores ( no sentido de aceitação pública) "A Revolta Dos Dândis" e "O Papa É Pop", "Ouça O Que Eu Digo:
                Não Ouça Ninguém" sempre será um grande álbum subestimado dos Engenheiros do Hawaii. </p>

            <p>No sentido sonoro, o álbum tem predominantemente "folk rock" do álbum anterior cede espaço a uma mescla de "hard rock", com ênfase
                em "riffs" distorcidos, e climas "floydianos" (remetendo à fase das trilhas sonoras "More" e "Obscured By Clouds", nas canções mais "suaves")
                oriundos da intersecção entre o acústico e o sintetizado. A belíssima "Cidade Em Chamas" possui andamento e solos que remetem ao trabalho da
                banda Iron Maiden.</p>

            <p>Depois, o mesmo processo pode ser observado nas letras, onde os versos, muito antes da "era digital", começam a estabelecer "links"
                com os outros trabalhos da banda. Esta espécie de "auto-referência" iria custar aos Engenheiros o fim de sua lua-de-mel
                (sim, ela existiu!) com a crítica especializada, que não perdoou tais "exercícios de arrogância" (como se em algum momento tal
                perdão tivesse alguma importância para a banda): "As chances estão contra nós/ Mas nós estamos por aí...".</p>

            <p>Quando, nos anos 1990, o álbum originalmente lançado em vinil, foi reeditado no formato CD, foi "assassinada", de maneira irremediável,
                toda a arte gráfica e seus "jogos semióticos" que complementavam, de forma essencial, o "conceito" do álbum como um todo, tributo que
                Gessinger pagava aos seus ídolos do rock progressivo inglês.</p>
            <br /><br />

            <p>Duração do álbum: 36 min. 05 seg.</p>
            <p>Gravadora(s): BMG.</p>
            <p>Produção: Maluly.</p>
