            <h1>História do Eagles</h1>

            <p>
                The Eagles é uma banda de rock americana formada em Los Angeles em 1971. Com cinco singles em primeiro lugar e seis álbuns em
                primeiro lugar, seis prêmios Grammy e cinco American Music Awards, os Eagles foram um dos artistas musicais de maior sucesso dos
                anos 1970 na América do Norte. Os membros fundadores Glenn Frey (guitarras, vocais), Don Henley (bateria, vocais), Bernie Leadon
                (guitarras, vocais) e Randy Meisner (baixo, vocais) foram recrutados por Linda Ronstadt como membros da banda, alguns em turnê
                com ela e todos tocando em seu terceiro álbum solo, antes de se aventurarem por conta própria.
            </p>

            <p>
                A banda começou a se formar quando Linda Ronstadt e o então empresário do grupo, John Boylan contrataram os músicos Glenn Frey e
                Don Henley na primavera de 1971. Randy Meisner e Bernie Leadon se juntariam ao grupo para fazerem a turnê de verão. Os Eagles originais
                tocariam apenas uma vez juntos ao vivo como apoio à Ronstadt (em um concerto em julho na Disneylândia), porém todos os quatro apareceram
                em seu álbum de 1971 de mesmo nome. Após o período com Ronstadt e com seu incentivo, eles decidiram formar sua própria banda, assinando em
                seguida com a Asylum Records, um novo selo iniciado por David Geffen. Geffen e seu parceiro Elliot Roberts inicialmente administraram a banda.
            </p>

            <p>
                Os Eagles são uma das bandas mais vendidas do mundo, tendo vendido mais de 200 milhões de discos, incluindo 100 milhões vendidos
                apenas nos Estados Unidos. Eles foram classificada como número #75 na Rolling Stone ' 2004 lista dos 100 Maiores Artistas de Todos os
                Tempos.
            </p>

            <p>
                A ideia de chamar a banda de "Águias" surgiu durante uma excursão de grupo influenciada por tequila no Deserto de Mojave. Os
                relatos sobre a origem do nome, entretanto, variam; Don Felder (que ainda não havia se juntado ao The Eangles e não estava no
                deserto) atribuiu a Leadon a origem do nome quando se lembrou de ter lido sobre a reverência dos Hopis pela águia, enquanto JD
                Souther sugeriu que a ideia surgiu quando Frey gritou para fora, "Eagles!" quando viram águias voando acima. Steve Martin, um
                amigo da banda desde seus primeiros dias no The Troubadour, relata em sua autobiografia que ele sugeriu que eles deveriam ser
                referidos como "The Eagles", mas Frey insiste que o nome do grupo é simplesmente "Eagles". Geffen e o parceiro Elliot Roberts
                geriram inicialmente a banda; eles foram substituídos mais tarde por Irving Azoff enquanto os Eagles estavam gravando seu terceiro
                álbum.
            </p>

            <p>
                O álbum de estreia homônimo do grupo foi gravado na Inglaterra em fevereiro de 1972 com o produtor Glyn Johns. Johns ficou
                impressionado com o canto harmônico da banda, e ele foi creditado por moldar a banda em "uma banda de country-rock com aquelas
                harmonias agudas". Lançado em 1 de junho de 1972, Eagles foi um grande sucesso, rendendo três singles no Top 40. O primeiro
                single e faixa principal, " Take It Easy ", foi uma canção escrita por Frey com seu então vizinho e roqueiro country Jackson
                Browne. Browne escreveu a primeira estrofe da música, mas ficou paralisada na segunda estrofe após a linha "Estou parado em uma
                esquina emWinslow, Arizona. "Frey completou o verso, e Browne continuou para terminar a música. A canção alcançou o número #12
                na Billboard Hot 100 e impulsionou os Eagles ao estrelato. O single foi seguido pelo blues" Witchy Woman " e a balada de soft
                country rock " Peaceful Easy Feeling ", alcançando o número #9 e número #22.
            </p><br />

