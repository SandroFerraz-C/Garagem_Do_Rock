            <h1>Night in the Ruts (1979)</h1>


            <p>- Night in the Ruts é o sexto álbum do Aerosmith, lançado em novembro de 1979, pela Columbia Records.</p>

            <p>O guitarrista Joe Perry deixou a banda no meio da gravação. O álbum foi inicialmente produzido no espaço de ensaio da banda
                - Warehouse por Jack Douglas, que havia produzido os quatro álbuns anteriores do Aerosmith, mas posteriormente a Columbia Records
                trouxe Gary Lyons para substituir Douglas como o produtor.</p>

            <p>A gravação do álbum começou na primavera de 1979, e desde o início houve atrasos. Impedido pelo uso desenfreado de drogas, o
                vocalista Steven Tyler teve dificuldade em completar as letras e os vocais. O baixista Tom Hamilton relembrou: "Trabalhamos no
                álbum, mas não conseguimos finalizá-lo. Era para sair em junho e se chamar - Off Your Rocker -, mas não havia letras.
                Foi uma grande crise." Os membros da banda também estavam em apuros financeiros, com o guitarrista Joe Perry devendo à
                banda $80.000 pelo serviço de quarto, que ele planejava pagar gravando um álbum solo. A relação entre Aerosmith e Jack Douglas
                também ficou gelado e instável quando o produtor se divorciou de sua esposa, de quem a banda gostava. Isso, combinado com as
                vendas fracas de - Draw the Line, levou a Columbia a intervir, com Douglas refletindo no livro de memórias da banda - Walk This Way -,
                "A gravadora finalmente colocou muita pressão sobre eles. Foi: "Olhe para estes números de vendas. Tente outro golpe ou haverá problemas.
                David Krebs, [empresário do Aerosmith] achava que eu não exercia mais controle sobre a banda, o que era verdade. Ninguém o fez.</p>

            <p>Com o álbum ainda inacabado, a banda foi enviada em turnê para gerar receita, já que havia queimado o orçamento previsto. Este
                lançamento prematuro durante os meses de verão empurrou o lançamento do álbum para o final do ano. "Nosso empresário agendou
                uma turnê", observou Hamilton, "deixando-nos apenas tempo suficiente para fazer o álbum, com base em quanto tempo nos levou
                no passado, mas na verdade precisávamos de muito mais tempo. Então, tivemos que sair em turnê antes que os vocais terminassem,
                e isso estava se arrastando. Todo mundo estava super frustrado com isso. É irônico, porque estávamos na estrada, tocando em
                estádios para um grande número de pessoas, e ainda assim a banda estava se preparando para morrer."</p>
