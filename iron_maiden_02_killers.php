            <p>
                - Killers é o segundo álbum do Iron Maiden, lançado em 02 de fevereiro de 1981.
            </p>
            <br />

            <p>
                Foi o primeiro a contar com o guitarrista Adrian Smith e o último com o vocalista Paul Di'Anno, demitido depois de problemas com
                suas performances nos palcos devido a seu abuso de álcool e uso de cocaína. Foi ainda o primeiro álbum a contar com o produtor
                veterano Martin Birch, que produziu todos os álbuns do Iron Maiden até 1992.
            </p>
            <br />

            <p>
                - Killers é o único álbum do Iron Maiden a contar com duas canções instrumentais e foi escrito quase exclusivamente por Steve Harris,
                com algumas assistências do resto da banda (a faixa-título e "Twilight Zone" são as únicas músicas que recebem créditos adicionais). Cada
                canção, com exceção de "Murders in the Rue Morgue" e "Prodigal Son", foram escritas anos antes do lançamento do álbum de estreia,
                embora nenhuma fosse gravada profissionalmente.
            </p>
            <br />

            <p>
                A turnê de apoio ao álbum, - Killer World Tour, levou a banda aos seus primeiros shows nos EUA, o primeiro no The Aladdin, Las Vegas
