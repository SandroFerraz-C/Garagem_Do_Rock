            <h1>História do Engenheiros do Hawaii</h1>

            <p>
                Engenheiros do Hawaii foi uma banda brasileira de rock, formada em 11 de janeiro de 1985 na cidade de Porto Alegre por Humberto Gessinger
                (vocal e baixo), Carlos Stein (guitarra), Marcelo Pitz (baixo) e Carlos Maltz (bateria), que alcançou grande popularidade com suas canções
                líricas e críticas. Antes de chegarem à sua formação final, a banda passou por algumas mudanças, sendo Gessinger o último integrante da
                formação original a permanecer na banda até a "pausa" ocorrida em 2008, por motivos particulares da banda.
            </p>

            <p>
                Quatro estudantes da Faculdade de Arquitetura da UFRGS - Humberto Gessinger (vocal e guitarra), Carlos Stein (guitarra),
                Marcelo Pitz (baixo) e Carlos Maltz (bateria) - resolveram formar uma banda para uma apresentação em um festival da faculdade,
                que aconteceria por protesto à paralisação de aulas. O primeiro show da banda foi em 11 de janeiro de 1985. Escolheram o nome
                Engenheiros do Hawaii para satirizar os estudantes de engenharia que andavam com bermudas de surfista, com quem tinham uma certa
                rivalidade. Começaram a surgir propostas para novos shows e, depois, algumas apresentações em palcos alternativos de Porto Alegre,
                juntamente com uma série de shows pelo interior do Rio Grande do Sul. A banda, em menos de quatro meses de carreira, já consegue gravar duas
                músicas na coletânea Rock Grande do Sul (1985) com diversas bandas gaúchas. Quando a banda seguiu com seus ensaios, durante a greve da
                faculdade, Carlos Stein realizou uma viagem que acabou inviabilizando sua permanência no grupo, e, tempos depois, ele passa a integrar a
                banda "Nenhum de Nós". Meses passaram, e os Engenheiros do Hawaii gravam o seu primeiro álbum: "Longe Demais das Capitais", em 1986.
                O norte musical do disco apontava para um som voltado à música pop, muito próximo ao ska de bandas como o The Police e Os Paralamas do Sucesso.
                Destacam-se as canções "Toda Forma de Poder", que foi tema das novelas "Hipertensão" da Rede Globo e "Vitória" da RecordTV.
            </p>

            <p>
                Antes de começarem as gravações do segundo disco, Marcelo Pitz deixa a banda por motivos pessoais. Com Gessinger assumindo o
                baixo, entra o guitarrista Augusto Licks, que havia trabalhado com Nei Lisboa, conhecido músico gaúcho. Os Engenheiros lançam o
                disco "A Revolta dos Dândis", em 1987. A banda muda o direcionamento temático, iniciando uma trilogia baseada no rock progressivo,
                com discos com repetições de temas gráficos e musicais e letras em que ocorre a auto-citação. Os arranjos musicais são influenciados pelo
                rock dos anos 60, as letras são críticas, com ocorrência de várias antíteses, paradoxos e citações literárias de filósofos, como Albert
                Camus e Jean-Paul Sartre. Destaque para os hits "Infinita Highway", "Terra de Gigantes", "Refrão de Bolero" e a faixa título, dividida em
                duas partes. Começam os shows para grandes plateias nos centros urbanos do país, como o festival Alternativa Nativa, realizado entre 14 e
                17 de junho de 1987. A partir desta data, os Engenheiros encheriam ginásios e estádios pelo Brasil afora. Porém, houve polêmicas e a banda
                chegou a ser acusada de elitista e fascista pelo conteúdo de suas letras. As polêmicas se intensificaram quando membros da banda se
                apresentaram com camisetas estampadas com a Estrela de Davi e a Suástica. O disco seguinte, Ouça o que Eu Digo: Não Ouça Ninguém, de 1988,
                pode ser visto como uma continuidade do anterior, tanto pelo trabalho da capa do álbum como pelo tema e estilo de suas canções. Destaque para
                as músicas "Somos Quem Podemos Ser", "Cidade em Chamas", "Tribos & Tribunais", a faixa-título e "Variações Sobre o Mesmo Tema", esta última
                uma homenagem à banda Pink Floyd, com sua estética progressiva e dividida em três partes. O álbum também marca a saída dos Engenheiros da
                cidade de Porto Alegre, indo morar no Rio de Janeiro. Consolidada a nova formação, os Engenheiros lançam Alívio Imediato, de 1989, quarto
                disco da banda e o primeiro registro ao vivo. Suas canções mostram uma retrospectiva de suas principais canções e as novas perspectivas a
                serem incorporadas, em especial o som mais eletrônico, presente na faixa título e na música "Nau à Deriva", ambas gravadas em estúdio e as
                demais gravadas ao vivo no Canecão, no Rio de Janeiro.
            </p>

            <p>
                Em 2008 termina, pelo menos temporariamente, as atividades d'os Engenheiros do Hawaii. O vocalista e líder da banda, Humberto
                Gessinger, declarou no site oficial da banda que os planos de retorno da banda são somente no ano de 2017 (O que não aconteceu). Lembrando que já é a
                quinta vez que Gessinger adia o retorno dos Engenheiros. Entre 2008 e 2012 Gessinger se dedicou ao "Pouca Vogal", parceria com
                Duca Leindecker, vocalista do "Cidadão Quem." Juntos eles cantavam novas baladas e grandes sucessos de suas bandas. Gessinger
                atualmente se dedica à sua carreira solo, que começou em 2013 com o lançamento do CD "Insular", e continuou com o CD/DVD "Insular"
                ao vivo em 2014. No ano de 2016 ele começa uma nova turnê denominada "Louco Pra Ficar Legal", trecho da música "Pra ficar legal", de 2002.
                Também lançou 5 livros: "Meu Pequeno Gremista e Pra Ser Sincero"- "123 Variações Sobre Um Mesmo Tema" no final de 2009, "Mapas do Acaso" -
                "45 Variações Sobre Um Mesmo Tema" em fevereiro de 2011, "Nas Entrelinhas do Horizonte" em abril de 2012 e "Seis Segundos de Atenção"
                em agosto de 2013.
            </p>
