            <h1> Sabbath Bloody Sabbath (1973)</h1>
            <br />

            <p>
                Sabbath Bloody Sabbath é o quinto álbum de estúdio da banda de heavy metal inglesa Black Sabbath.
            </p>
            <br />

            <p>
                Foi o primeiro álbum da banda a ser elogiado pela crítica da época, sendo aclamado pela revista americana Rolling Stone como "um
                grande sucesso" e "um grande trabalho". Isto foi um grande impacto positivo para a banda, já que a mesma Rolling Stone já teria
                dirigido críticas duras à seus primeiros quatro trabalhos.
            </p>
            <br />

            <p>
                A banda levou mais adiante as influências do rock progressivo, que já apareciam com menos intensidade no álbum anterior, Black Sabbath
                Vol. 4. O tecladista Rick Wakeman, do Yes, fez algumas participações nesse álbum nas músicas "Sabbra Cadabra" e "Who Are You?", tocando sob
                o pseudônimo "Spock Wall".
            </p>
            <br />

            <p>
                A banda planejava gravar este álbum, e alugaram uma casa em Bel Air para começarem a gravar, porém devido à sérios problemas com drogas
                e fadiga, não obtiveram sucesso algum compondo, e então retornaram para a Inglaterra e alugaram o Castelo Clearwell, situado na Floresta de
                Dean, local que, segundo os membros da banda, os faziam mais inspirados, e após algum tempo ensaiando, Tony Iommi criou o riff da música
                "Sabbath Bloody Sabbath", e as músicas começaram a surgir.
            </p>
            <br />

            <p>
                O álbum foi gravado no Morgan Studios em Londres. A banda passou por uma transição drástica em suas músicas, misturando o peso original da
                banda à sons de sintetizadores e toques de progressivo, como em músicas como "Spiral Architect" e "Who are You?", o que seria uma marca até
                o álbum Never Say Die!.
            </p>
            <br />

            <p>
                O álbum alcançou o 4º lugar nas paradas britânicas, e o 11º lugar nas paradas americanas. Durante a turnê mundial desse álbum a banda ganhou
                grande popularidade após tocar para 200.000 pessoas no Festival California Jam em 1974, ao lado de bandas como Deep Purple, Emerson,
                Lake and Palmer, Eagles, Earth, Wind & Fire e outros.
            </p>
