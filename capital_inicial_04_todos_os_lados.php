            <p>
                Neste álbum o Capital Inicial volta a fazer um rock eletrizante, "digno de seu pioneirismo", como definiram alguns críticos.
                Na verdade, o disco deveria ter sido lançado em março de 1990. Mas como foram convidados para tocar na segunda edição do
                Hollywood Rock, em janeiro, realizado em São Paulo e no Rio, e pressionados pela gravadora, os integrantes do Capital são
                obrigados a acelerar todo o processo (o disco foi gravado e mixado em um mês) e anteciparm o lançamento do disco.
            </p>

            <p>
                A banda, como diz o título do álbum, atira para "Todos os Lados": convocou o compositor Alvin L, que compôs a faixa-título,
                "Mickey Mouse em Moscou" (que fala sobre a queda do Muro de Berlim), "Belos e Malditos" (baseada em letra de Renato Russo) e
                várias outras, como "Mambo Club" e "Abismo". Também houve uma parceria com Humberto Gessinger, do
                <a href="engenheiros_do_hawaii.php" target="blank">Engenheiros do Hawaii</a>
                ("Olhos Abertos") e a regravação de uma música dos Mutantes, "2001". Apesar de ter sido bem recebido pela crítica e a boa
                execução de "Mickey Mouse em Moscou", "Todos os Lados" e "Belos e Malditos", este disco também não teve sucesso, devido à crise
                econômica do período e à ascensão da música sertaneja, parando nas 30 000 cópias.
            </p>
