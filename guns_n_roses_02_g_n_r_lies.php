            <h1>G N' R Lies (1988)</h1>
            <br />

            <p>
                "G N'R Lies", também conhecido como - Lies, é o segundo álbum do Guns N' Roses. A capa simula uma primeira página de jornal, com
                algumas músicas como notícias.
            </p>
            <br />

            <p>
                Lançado em 1988, - Lies, seguiu as altas estatísticas de Appetite for Destruction. Alcançou a #2ª posição nas paradas, foi cinco
                vezes platina e foi eleito pelo Grammy como "melhor performance de hard rock", chegando ao ouro com a Musica "Patience". O
                lançamento deste CD, entretanto, foi ofuscado devido aos incidentes provocados no clube Monsters Of Rock, meses antes, em que
                dois fãs foram mortos pisoteados em um tumulto, durante o show da banda. Foi o último álbum gravado com o baterista Steven Adler,
                antes de este ser demitido da banda.
            </p>
            <br />

            <p>
                Este álbum é composto de músicas do EP independente de 1986 - Live ?!*@ Like a Suicide, as primeiras quatro faixas consistem desse EP
                e também de novas gravações de estúdio, sendo quatro músicas acústicas. "Used To Love Her" e "Patience" foram músicas que fizeram
                sucesso comercial nas rádios.
                As últimas quatro músicas foram gravadas com violões. Eles foram escritos e gravados em apenas algumas sessões de estúdio, que o produtor
                Mike Clink chamou de "um daqueles momentos mágicos da história do rock and roll .
                "You're Crazy" foi lançada em - Appetite for Destruction, e neste álbum aparece com a letra original e um andamento mais lento.
                "One in a Million" foi criticada por homossexuais e organizações de direitos humanos, e levou à exclusão da banda do Band Aid.
                Desde o seu lançamento, o álbum já vendeu cerca de 5 milhões de cópias em todo o mundo.
            </p>
            <br />

            <p>
                A capa é uma paródia humorística de jornais tablóides , assim como o encarte . A arte da capa do álbum passou por várias pequenas
                modificações quando o título foi lançado em CD. Em primeiro lugar, no canto inferior esquerdo que dizia "LIES LIES LIES",
                originalmente lia-se "O espancamento de mulheres existe há 10.000 anos." Em segundo lugar, em vez de "Elefante dá à luz um anão",
                a manchete original diz: "Senhoras, bem-vindas à idade das trevas." Muitas cópias do lançamento do LP original também continham
                uma imagem sem censura de uma modelo nua na capa interna do LP.
            </p>
