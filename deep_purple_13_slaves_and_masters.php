                gravada por Paul Rodgers e a banda de Kenney Jones The Law, mas eles também não a lançaram. A versão original de Turner pode ser
                encontrada nos bootleg Demos '88-91 ' e na versão The Law no bootleg The Law II . Turner regravou a música para seu álbum Eat Up and Wait
                (1998).
