                Slaves and Masters é o décimo terceiro álbum do Deep Purple, e foi lançado em 5 de Outubro de 1990.
            </p>
            <br />

            <p>
                Após seu lançamento, Slaves and Masters alcançou a posição #87ª na parada US Billboard 200. O álbum vendeu dramaticamente abaixo das expectativas,
                em comparação com o álbum anterior do Deep Purple, The House of Blue Light com Gillan, que alcançou a #34ª posição nos Estados Unidos. Uma música das
                sessões de gravação dos Slaves and Masters foi reorganizada para a trilha sonora do filme Fire, Ice and Dynamite de 1990. Jon Lord não tocou
                na música, que foi tocada pelos outros quatro membros da formação.
            </p>
            <br />

            <p>
                Apesar das vendas de álbuns abaixo do esperado, o Deep Purple fez uma turnê relativamente bem-sucedida em 1991, especialmente para a perna
                europeia da banda. Turner ainda era membro do grupo quando eles começaram a escrever e gravar seu próximo álbum em 1992, mas sob coação de
                empresários que estavam planejando uma turnê de 25º aniversário, Deep Purple decidiu trazer de volta Gillan para a formação de seu estúdio
                de 1993 álbum The Battle Rages On ... Um punhado de faixas de trabalho originalmente destinadas ao seguimento de Slaves and Masters apareceria
                em lançamentos solo subsequentes de Turner.
            </p>
            <br />

            <p>
