            <h1>T.N.T. (<time datetime="1975">1975</time>)</h1>

            <p>- T.N.T é o segundo álbum do AC/DC. Levou apenas três semanas para ser gravado e seu sucesso foi tamanho que a gravadora se interessou em lançá-lo no mercado internacional.</p>

            <p>Após o sucesso do single "<span lang="en-US">Baby, Please Don't Go</span>" e do álbum - <a href="acdc_01_high_voltage.php" target="blank"><span lang="en-US">High Voltage</span></a>, o AC/DC retornou ao Albert Studios em Sydney para gravar seu segundo LP com os produtores George Young e
                Harry Vanda. - TNT marcou uma mudança na direção do álbum de estréia do AC/DC, - <a href="acdc_01_high_voltage.php" target="blank"><span lang="en-US">High Voltage</span></a>, que foi lançado em <time datetime="1975-02-17">17 de fevereiro de 1975</time>;
                Enquanto <a href="acdc_01_high_voltage.php" target="blank">High Voltage</a> destaca algumas experiências com os estilos de suas canções e tinha uma variedade de pessoal enchendo várias funções, - TNT viu a banda abraçar plenamente a
                fórmula para que se tornaria famosa: blues com base de <span lang="en-US">Rock and Roll</span>. Eles também simplificaram seu sistema de pessoal e o usariam a partir de então, que era Angus tocando exclusivamente a guitarra solo, Malcolm Young tocando guitarra base,
                e o baterista e o baixista sendo os únicos a tocar bateria e baixo respectivamente nos álbuns.</p>

            <p>Com a excelente aceitação do público, Malcolm Young e Bon Scott tiveram que convencer aos outros membros da banda de que seria melhor que se mudassem para a Inglaterra. Assim sendo, em <time datetime="1976-02">fevereiro de 1976</time>
                eles estavam morando numa casa no subúrbio londrino de Barnes, onde apesar de pouco conforto, podiam tocar até altas horas e no volume que quisessem.</p>

            <p>Levou pouquíssimo tempo desde a mudança até eles começarem a tocar nos mais famosos clubes de Londres, dos quais o que mais se destaca é o <span lang="en-US">Marquee Club</span> (um templo do <span lang="en-US">Rock</span> que já foi palco de grandes bandas desde <span lang="en-US">Rolling Stones</span> até o Sepultura).</p>

            <p>Na faixa de abertura, Bon Scott toca um solo de Gaita de Fole, instrumento o qual ele aprendeu a tocar na Escócia, quando jovem.</p>
