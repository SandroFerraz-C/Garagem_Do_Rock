            <h1>Somewhere in Time (1986)</h1>
            <br />

            <p>
                - Somewhere In Time é o sexto álbum do Iron Maiden, lançado no dia 29 de setembro de 1986.
            </p>
            <br />

            <p>
                A temática principal do encarte do álbum é baseada no filme Blade Runner estrelado pelo ator Harrison Ford, e as músicas falam
                sobre as relações do ser humano com o tempo. O álbum é conhecido por ser o primeiro da banda a usar sintetizadores. "Wasted Years"
                e "Stranger in a Strange Land" foram lançadas como singles do álbum, mas a música mais executada pela banda durante a suas turnês
                foi a "Heaven Can Wait", escrita por Steve Harris. Em 1995 foi relançado junto com um CD bônus.
            </p>
            <br />

            <p>
                - Somewhere in Time é o primeiro trabalho de estúdio da banda após a extensa - World Slavery Tour de 1984–85, que foi fisicamente
                desgastante para o grupo, durando 331 dias e compreendendo 187 shows. O esgotamento resultante da turnê é creditado como o principal fator
                na completa falta de contribuições do vocalista Bruce Dickinson, cujo material foi rejeitado pelo resto da banda. Dickinson escreveu
                várias canções "baseadas em acústicas", explicando que "eu senti que tínhamos que criar nosso Led Zeppelin IV... tínhamos que passar
                para outro nível ou ficaríamos estagnados e nos afundariamos", embora o baixista e escritor principal Steve Harris "achasse que havia
                perdido o enredo completamente", presumindo que "ele provavelmente estava mais esgotado do que qualquer um no final daquela última turnê
                ". Por outro lado, o registro também é notável pelo número de canções "totalmente formadas" escritas pelo guitarrista Adrian Smith,
                que escreveu os dois singles do álbum: "Wasted Years" e "Stranger in a Strange Land", "Wasted Years" é a única música do álbum
                que não contém sintetizadores.
            </p>
            <br />

            <p>
                Desde o seu lançamento, - Somewhere in Time foi certificado de platina pela RIAA, tendo vendido mais de um milhão de cópias nos Estados
                Unidos. - Somewhere on Tour foi a turnê de apoio do álbum .
            </p>
