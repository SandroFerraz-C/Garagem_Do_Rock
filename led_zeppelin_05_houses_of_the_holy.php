            <h1>Houses of the Holy (1973)</h1>
            <br />

            <p>
                - Houses of the Holy é o quinto álbum do Led Zeppelin, lançado pela Atlantic Records em 28 de março de 1973.
            </p>
            <br />

            <p>
                O título do álbum é uma dedicação da banda á seus fãs que apareceram em locais que eles batizaram de "Casa do sagrado".
                Ele representa um ponto de virada musical para Led Zeppelin, como eles começaram a usar mais camadas e técnicas de produção em
                gravar suas canções
            </p>
            <br />

            <p>
                Foi o último álbum do grupo lançado pela Atlantic Records. Desde seu lançamento o disco foi certificado pela Recording Industry
                Association of America (RIAA), nos Estados Unidos, com 11 Discos de Platina, por ter vendido uma quantidade superior a 11 milhões
                de cópias. - House of the Holy foi classificado em #148° lugar na lista dos #500 melhores álbuns da revista Rolling Stone.
            </p>
            <br />

            <p>
                Muito desse álbum foi gravado em 1972 numa propriedade rural em Berkshire, na antiga propriedade de Mick Jagger, por meio do
                estúdio de gravação Rolling Stones Mobile Studio. O álbum marca uma mudança no estilo do som da banda. Os riffs de guitarra se
                tornaram mais trabalhosos, com técnicas e influências do blues. Apresenta estilos não vistos nos álbuns anteriores da banda,
                como por exemplo, "D'yer Mak'er", que possui uma influência do reggae (A pronúncia de D'yer Mak'er é a mesma de Jamaica em inglês);
                "No Quarter" apresenta uma introdução com sintetizadores e um solo de piano do baixista John Paul Jones, "The Crunge" é um tributo
                funk a James Brown; A música que encerra o álbum "The Ocean" é dedicada ao "o oceano" de fãs que assistiam ao shows do Led Zeppelin.
            </p>
            <br />

            <p>
                A capa do álbum representa a "Calçada dos Gigantes" na Irlanda do Norte. Representa uma família vinda do mar subindo a calçada.
                "Houses of the Holy" é também o nome de uma música gravada durante as sessões deste álbum, mas não lançada até - Physical Graffiti,
                álbum de 1975. Outras músicas gravadas, mas que não aparecem neste álbum são "Walter's Walk", "The Rover" e "Black Country Woman".
            </p>
            <br /><br />

            <p>Duração do album: 40 min. 58 seg.</p>
            <p>Gravadora(s): Atlantic Records.</p>
            <p>Produção: Jimmy Page.</p>
