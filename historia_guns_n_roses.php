            <h2>Rock and Roll Hall of Fame</h2>

            <p>
                Em 11 de abril de 2012, Axl Rose emitiu um comunicado na página oficial da banda <a href="https://www.facebook.com/gunsnroses">Guns N' Roses</a>
                no Facebook dizendo que não iria à cerimônia de indução e que não gostaria de ser homenageado. Após a cerimônia, Axl postou, na
                página da banda <a href="https://www.facebook.com/gunsnroses">Guns N' Roses</a> no Facebook, um pedido de desculpas à cidade de Cleveland
                (onde a cerimônia ocorreu), e detalhou os motivos de sua recusa do prêmio: "Eu ainda não sei exatamente o que é o Hall of Fame, ou
                como ou por que ele ganha dinheiro, para onde vai o dinheiro, quem escolhe os jurados, e por que algum jurado pode escolher, dentre
                todas as pessoas do mundo que contribuíram para o gênero, quem é suficientemente "roque" para entrar no Hall".
            </p>

            <p>
                Em 14 de abril de 2012, os ex-integrantes da banda Slash, Duff McKagan, Gilby Clarke, Steven Adler e Matt Sorum se reuniram no
