            <p>
                A estreia nos palcos da formação clássica ( Axl Rose, Slash, Duff, Izzy Stradlin e Steven Adler), aconteceu em 6 de Junho de 1986,
                no Troubador, em Hollywood, para cerca de 150 pessoas. Após isso, a banda seguiu para Seattle, onde teve a sua turnê de estreia,
                conhecida por - Hell Tour. No caminho entre Los Angeles e Seattle, a van onde viajavam quebrou, sem dinheiro, não restou alternativa
                a não ser abandonar o veículo e pedir carona. Com isso, a banda demorou mais de dois dias para chegar, atrasando seu primeiro
                compromisso em Seattle e causando, como consequência, o cancelamento da turnê inicial do Guns N' Roses pelos Estados Unidos,
                fazendo com que os membros da banda tivessem que vender parte dos equipamentos para voltar para casa.
            </p>

            <p>
                Em 1986, contornando as dificuldades, gravaram um EP (disco de menor duração) com quatro músicas. O nome do disco, lançado de forma
                independente, foi - Live ?!*@ Like a Suicide, composto por quatro faixas de fitas demo da banda com o ruído do público sobreposto.
                Continha covers do - Rose Tattoo ("Nice Boys") e do - Aerosmith ("Mama Kin"), juntamente com duas composições originais: "Reckless Life"
                e "Move to the City", ambas coescritas pelo membro fundador do Hollywood Rose, Chris Weber.
            </p>

            <p>
                As dificuldades não durariam muito. Appetite for Destruction foi lançado em julho de 1987 e se tornou, em pouco tempo, o primeiro
                disco de estreia de uma banda de hard rock a alcançar o #1° lugar nas paradas e também foi o disco de estreia que mais vendeu na
                história da música, com cerca de 18 milhões de cópias vendidas somente nos Estados Unidos. À medida que o disco vendia mais e mais,
                a banda prosseguia com seus shows, abrindo para bandas maiores até então, como - Iron Maiden e - Rolling Stones, e posteriormente
                encabeçando seus próprios shows na "Appetite for Destruction Tour".
