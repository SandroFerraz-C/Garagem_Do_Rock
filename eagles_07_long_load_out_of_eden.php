            <h1>Long Road Out of Eden (2007)</h1>

            <p>- Long Road Out of Eden é o sétimo álbum de estúdio da banda de rock americana Eagles, lançado em 2007 pela Lost Highway Records como um
                álbum duplo.</p>

            <p>Quase seis anos em produção, é o primeiro álbum de estúdio da banda desde 1979, "The Long Run". Nesse meio tempo, a banda gravou
                quatro faixas de estúdio originais para o álbum ao vivo Hell Freezes Over (1994), " Hole in the World " para The Very Best Of (2003)
                e Joe Walsh -penned "One Day at a Time" para a Farewell 1 Tour: Live from Melbourne DVD (2005), que Walsh mais tarde regravou para
                seu álbum Analog Man de 2012.</p>

            <p>O álbum produziu dois singles nas paradas Hot Country Songs: um cover de "How Long" de JD Souther e "Busy Being Fabulous", ambos os
                quais foram os 30 maiores sucessos nas paradas country, bem como os 20 maiores sucessos no Hot Gráficos de faixas contemporâneas
                adultas. O álbum produziu cinco sucessos consecutivos nas paradas Hot Adult Contemporary Tracks com "How Long", "Busy Being Fabulous",
                "No More Cloudy Days", "What Do I Do With My Heart" e "I Don't Want to Ouça mais".</p>

            <p>O álbum estreou em 1° lugar nos Estados Unidos após uma mudança de última hora nas regras da Billboard (que também bloqueou o tão
                antecipado Blackout de Britney Spears de liderar as paradas) e ganhou dois prêmios Grammy por "How Long" e o instrumental "I Dreamed There Was No War".
                O álbum se tornou o sexto álbum nº 1 da banda e foi o álbum mais vendido do ano. Desde então, vendeu 3,5 milhões de cópias apenas nos EUA.
                Sendo um álbum duplo com duração superior a 90 minutos, o álbum foi certificado 7x Platinum pela Recording Industry Association of America
                para embarques de 7 milhões de discos.</p>

            <p>Em uma entrevista de 2007 à CNN, o membro da banda Don Henley declarou: "Este é provavelmente o último álbum dos Eagles que faremos."
                Quando questionado sobre a possibilidade de um álbum seguinte em novembro de 2010, o membro da banda Timothy B. Schmit disse:
                "Minha primeira reação seria: de jeito nenhum. Mas eu disse isso antes do último, então você nunca sabe realmente . As bandas são uma
                entidade frágil e você nunca sabe o que vai acontecer. Demorou muito para fazer esse último álbum, ao longo de alguns anos, na verdade, e
                exigiu muito de nós. Tiramos um ano de folga em um ponto. Não tenho certeza se somos capazes de fazer isso de novo. Eu não fecharia a porta
                sobre isso, mas não sei." Em uma entrevista de 2010 para undercover.fm, Joe Walsh disse que a banda pode ser capaz de fazer mais um álbum
                antes de a banda "encerrar".</p>
