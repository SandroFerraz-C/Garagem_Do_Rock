                Steve Harris afirma que começou a trabalhar em "Blood Brothers" nessa época, mas não conseguiu completá-la a tempo de inseri-la no
                - Virtual XI.
            </p>

            <p>
                A arte e título do disco fazem referência ao romance homônimo escrito por Aldous Huxley. É o último álbum de estúdio da banda em
