            <p>
                - Brave New World é o décimo segundo álbum do Iron Maiden, lançado em 30 de maio de 2000.
            </p>

            <p>
                Foi o primeiro álbum de estúdio da banda desde - Seventh Son of a Seventh Son (1988) a apresentar a formação da era Piece Of Mind
                (1983) após o regresso do vocalista Bruce Dickinson, marcando ainda o retorno do guitarrista Adrian Smith iniciando a formação
                com três guitarras e também foi o primeiro disco do grupo gravado com a formação em sexteto. O álbum contém dez faixas, entre elas
                os singles "The Wicker Man" e "Out of the Silent Planet".
            </p>

            <p>
                Em Brave New World a banda mostra um trabalho similar do que tinha feito nos discos anteriores, - The X Factor e - Virtual XI.
                É um álbum com um som mais progressivo, marcando ao mesmo tempo um retorno ao metal tradicional de suas origens, com algumas
                músicas lembrando trechos de temas de álbuns como - Powerslave e - Fear of the Dark. Foi bem aceito por público e crítica,
                vendendo mais de 307.000 cópias nos Estados Unidos até 2008, além de receber Certificação de Ouro do Brasil e no Reino Unido.
            </p>

            <p>
