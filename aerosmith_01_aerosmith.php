            <h1>Aerosmith (1973)</h1>


            <p>
                - Aerosmith é o álbum de estreia da banda de rock estadunidense Aerosmith, lançado em 5 de janeiro de 1973. O álbum foi gravado em
                duas semanas na Intermedia Studio em Boston, Massachusetts.
            </p>

            <p>
                O álbum não foi um sucesso quando foi lançado em janeiro de 1973. A canção "Dream On" foi lançado como um single em 27 de junho de
                1973 e alcançou a posição #59 na Billboard Hot 100.
            </p>

            <p> Depois de entrar em uma parceria com Frank Connelly e Krebs David, Leber Steve convidou membros de duas gravadoras -
                Atlantic Records e Columbia Records - para ver um show do Aerosmith em Max's Kansas City. Clive Davis, presidente da Columbia,
                ficou impressionado com o grupo e o Aerosmith assinou com a Columbia, no verão de 1972.
            </p>

            <p>
                O álbum não foi um sucesso quando foi lançado em janeiro de 1973. Para a decepção da banda, não foi avaliado na revista
                <a href="https://www.rollingstone.com" target="blank">Rolling Stone</a>.
                Além disso, a Columbia lançou o - Aerosmith ao mesmo tempo que o álbum de estreia de Bruce Springsteen, para o qual eles fizeram um
                grande esforço promocional. Os críticos compararam a banda desfavoravelmente aos <a href="the_rolling_stones.php" target="blank">Rolling Stones</a>
                e aos New York Dolls .
            </p>

            <p>
