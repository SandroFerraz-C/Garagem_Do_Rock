            <h1>Born Again (1983)</h1>
            <br />

            <p>
                Born Again é o décimo primeiro álbum de estúdio da banda de heavy metal, Black Sabbath.
            </p>
            <br />

            <p>
                Após a saída de Ronnie James Dio, e Vinny Appice da banda, Tony Iommi e Geezer Butler viram-se novamente sem vocalista e baterista.
                O escolhido para substituir Dio foi Ian Gillan, do Deep Purple, um dos maiores vocalistas da época, embora David Coverdale, do
                Whitesnake, fosse a primeira opção. Gillan tinha acabado de sair de sua banda solo, para uma reunião do Deep Purple
                (que viria a acontecer só alguns anos depois) e estava livre para qualquer oferta. Bill Ward voltou e assumiu novamente a bateria.
            </p>
            <br />

            <p>
                O álbum foi lançado dia 7 de Agosto, com receios de que alguns fãs mais conservadores se recusassem a escutar o álbum, mas a critica e os
                números surpreenderam. O álbum ficou em 4° lugar nas paradas inglesas no primeiro mês após o lançamento. Canções como "Zero The Hero" e
                "Trashed" se tornaram clássicos e ganharam videoclipes. Born Again foi o único trabalho de Ian Gillan com o Black Sabbath, que deixou a
                banda em 1984 para gravar o álbum Perfect Strangers com o MK2 do Deep Purple.
            </p>
            <br />

            <p>
                Anos depois, os músicos do Black Sabbath não parecem gostar do álbum, em parte por causa da produção. Descobriu-se só após a mixagem
                final que um dos alto-falantes do amplificador da guitarra de Tony Iommi queimou durante as gravações e assim permaneceu, conferindo
                uma sonoridade única ao disco.
            </p>
            <br /><br />

            <p>Duração do album: 41 min. 08 seg.</p>
            <p>Gravadora(s): Vertigo (Reino Unido), Warner Brothers (Estados Unidos), Castle e Sanctuary.</p>
            <p>Produção: Robin Black e Black Sabbath. </p>
