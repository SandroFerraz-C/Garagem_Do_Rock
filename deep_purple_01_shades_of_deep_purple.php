            <h1>Shades of Deep Purple (1968)</h1>

            <p>"Shades of Deep Purple" é o álbum de estreia da banda de hard rock inglesa Deep Purple, lançado em Julho de 1968 pela Tetragrammaton
                Records, nos Estados Unidos, e em Setembro de 1968 pela Parlophone no Reino Unido.</p>

            <p>O grupo, originalmente designado por Roundabout, foi uma ideia do ex-baterista dos Searchers, Chris Curtis, que recrutou Jon Lord
                e Ritchie Blackmore antes de deixar o projecto. A formação final da banda ficou completa com o vocalista Rod Evans, o baixista
                Nick Simper e o baterista Ian Paice, em Março de 1968.</p>

            <p>Após dois meses de ensaios, Shades of Deep Purple foi gravado em apenas três dias em Maio de 1968, contendo quatro músicas
                originais e quatro covers, cuidadosamente trabalhados para incluir interlúdios clássicos e parecer mais psicadélico. Em termos
                de estilo, o álbum está próximo do rock psicadélico e do rock progressivo, dois géneros em crescimento na década de 1960.</p>

            <p>O álbum não foi bem recebido no Reino Unido, onde pouco vendeu, e nem entrou para as tabelas de vendas. Pelo contrário, nos Estados
                Unidos foi um sucesso, e o single Hush, uma música de rock muito ritmada escrita por Joe South, tornou-se popular na época, chegando ao 4.º
                lugar na tabela Billboard Hot 100. As boas vendas do álbum e a intensa passagem do single na rádio, contribuíram para a atenção que os
                Deep Purple iriam ter nas suas primeiras digressões pelos Estados Unidos e durante os anos 1970. As análises recentes ao álbum são
                positivas e consideram Shades of Deep Purple uma peça importante na história dos Deep Purple.</p>
