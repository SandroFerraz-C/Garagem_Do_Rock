            <p>
                - Chinese Democracy é o sexto álbum de estúdio do Guns N' Roses, lançado mundialmente em novembro de 2008.
            </p>

            <p>
                O álbum ficou conhecido como o "Disco eternamente adiado" e "O álbum mais caro do mundo ( 13 milhões de Dollares ) ", pois a banda começou a trabalhar nele em 1996 e cancelou todas as datas
                de lançamento previstas desde o ano até a distribuição. Este é o seu primeiro álbum de estúdio desde The Spaghetti Incident? de 1993,
                e o primeiro a apresentar apenas material original desde - Use Your Illusion II de 1991.
            </p>

            <p>
                O álbum começou a ser trabalhado logo após a saída de todos os membros originais da banda — exceto Axl Rose — nos anos 90, e seu
                anúncio ocorreu após o lançamento da canção "Oh My God" em 1999, na trilha sonora do filme - End of Days, porém apenas no começo de
                janeiro de 2006 é que começaram a ser divulgadas informações consistentes, quando Rose concedeu uma entrevista à revista Rolling Stone, na
                qual ele disse que o som de Chinese Democracy estava diferente do comum da banda, com alguns arranjos lembrando Queen e que tratava-se de
                um projeto muito complexo; uma carta aberta de Rose explicou que ele pretendia lançar o trabalho em março de 2007, mas somente em outubro
                de 2008 foi aberta uma contagem oficial na página virtual do grupo, que chegou ao fim no dia 23 de novembro de 2008, à meia-noite, quando
                o trabalho foi oficialmente lançado.
            </p>

            <p>
                A obra se tornou um sucesso comercial imediato, estreando no #3° lugar da tabela musical Billboard 200 com mais de 200 mil cópias
                vendidas na primeira semana e recebendo a certificação de platina pela Recording Industry Association of America (RIAA) em quatro
                meses, com mais de um milhão de edições comercializadas, além de também ter totalizado o mesmo valor em território europeu, com
                dezenas de autenticações de ouro e platina em vários países.
            </p>

            <p>
                Assim que foi lançado, - Chinese Democracy foi imediatamente proibido na China devido a seu título e a referência negativa ao Falun gong,
                uma prática espiritual popular na Ásia. O álbum foi banido das lojas e o Guns foi permanentemente proibido de tocar no país.
            </p>
