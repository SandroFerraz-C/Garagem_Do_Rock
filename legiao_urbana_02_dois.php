            <h1>Dois (1986)</h1>
            <br />

            <p>
                - Dois é o segundo álbum do Legião Urbana, lançado em julho de 1986.
            </p>
            <br />

            <p>
                O álbum ocupa a #21ª posição da lista dos 100 maiores discos da música brasileira pela Rolling Stone Brasil. Em setembro de 2012,
                foi eleito pelo público da rádio <a href="https://eldorado.estadao.com.br/" target="blank"> Eldorado FM</a>, do portal
                <a href="https://www.estadao.com.br" target="blank"> Estadao.com</a> e do <a href="https://cultura.estadao.com.br" target="blank">
                    Caderno C2 + Música</a> como o #3° melhor disco brasileiro da história. O álbum vendeu mais de 900 mil cópias no Brasil.
                "Tempo Perdido" fez um grande sucesso e se tornou num dos clássicos da Legião. "Eduardo e Mônica", "Índios" e "Quase sem Querer"
                também fizeram sucesso.
            </p>
            <br />

            <p>
                Na época do lançamento, a crítica de <a href="https://www.estadao.com.br" target="blank">O Estadão</a> elogiou o disco, afirmando:
                "Você morde e vê que ali tem carne. Sangrando. O gostinho de Smiths no lado A, com o letrista e vocalista Renato Russo enchendo a
                boca em Daniel na Cova dos Leões ou Tempo Perdido, na verdade apenas prepara o terreno para a paulada do lado B, como em
                Metrópole e Fábrica".
            </p>
            <br />

            <p>
                O sucesso do álbum anterior (que àquela altura já havia vendido 100 mil cópias) fez com que Renato Russo cogitasse fazer o segundo
                álbum como duplo, intitulado "Mitologia e Intuição". A gravadora, porém, não se entusiasmou com a ideia, e o álbum acabou sendo
                simples. Renato estava sofrendo a chamada "síndrome do segundo disco", como disse o guitarrista Dado Villa-Lobos. O sucesso de
                vendas e de crítica do - Legião Urbana fez com que ele quisesse superar a marca no segundo lançamento. A produção de - Dois ficou
                a cabo de Mayrton Bahia, que na época era delegado pela EMI-Odeon para produzir os artistas novos que chegavam à empresa.
                <br /><br />

                O disco acabou responsável pela maior quantidade de vendas da história da Legião Urbana, alavancado pelo sucesso de "Eduardo e Mônica", uma
                faixa que era considerada difícil, por não ter um refrão. O álbum vendeu mais de 1,8 milhão de cópias.
            </p>
            <br /><br />

