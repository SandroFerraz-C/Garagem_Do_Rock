                simples. Renato estava sofrendo a chamada "síndrome do segundo disco", como disse o guitarrista Dado Villa-Lobos. O sucesso de
                vendas e de crítica do - Legião Urbana fez com que ele quisesse superar a marca no segundo lançamento. A produção de - Dois ficou
                a cabo de Mayrton Bahia, que na época era delegado pela EMI-Odeon para produzir os artistas novos que chegavam à empresa.
            </p>

