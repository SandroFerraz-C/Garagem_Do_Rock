            <h1>The Book of Souls (2015)</h1>
            <br />

            <p>
                - The Book of Souls é o décimo sexto álbum do Iron Maiden. Lançado em 04 de setembro de 2015.
            </p>
            <br />

            <p>
                É o primeiro disco lançado pela Parlophone Records, gravadora com a qual a banda assinou após o fim da EMI Music em 2013, pela qual
                o Iron Maiden tinha lançado todos os seus álbuns até então. É, também, o primeiro registro da banda editado em disco duplo, sendo
                Kevin "Caveman" Shirley e Steve Harris os responsáveis pela produção. O álbum marca também a maior diferença de tempo entre dois
                lançamentos de álbuns de estúdio – cinco anos depois de - The Final Frontier.
            </p>
            <br />

            <p>
                - The Book Of Souls foi o quinto lançamento do Maiden a chegar ao primeiro lugar das paradas musicais inglesas, após - The Number
                of the Beast (1982), - Seventh Son of a Seventh Son (1988), - Fear of the Dark (1992) e - The Final Frontier (2010). Também ficou
                em primeiro lugar nas listas de vendas de outros 23 países, incluindo Brasil e Portugal.
            </p>
            <br />

            <p>
                O álbum foi um sucesso comercial, chegando ao #1º lugar de vendas em 24 países, além de outros 19 territórios que não publicam mais
                o número de vendas de varejo. Foi a quinta gravação da banda a atingir o topo das paradas musicais britânicas, com vendas acima
                de 60 mil unidades na primeira semana, superando a gravação anterior, - The Final Frontier, que havia vendido 44 mil unidades e
                chegou na mesma posição. Nos Estados Unidos, - The Book of Souls ficou em #4º lugar, sua melhor posição já registrada na Billboard
                200, repetindo a posição de - The Final Frontier, embora tenha vendido mais que o anterior, com vendas que ultrapassam 74 mil
                unidades. De acordo com a Billboard, isso marca a melhor semana de vendas do Iron Maiden em solo americano desde que o sistema
                Nielsen SoundScan começou a operar em 1991. Até janeiro de 2016, - The Book of Souls já havia vendido 148 mil cópias nos EUA.
            </p>
            <br /><br />

            <p>Duração do album: 92 min. 11 seg.</p>
            <p>Gravadora(s): Parlophone Records, Sanctuary Copyrights e BMG .</p>
            <p>Produção: Kevin Shirley e Steve Harris.</p>
