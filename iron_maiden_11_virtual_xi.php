            <p>
                - Virtual XI é o décimo primeiro álbum do Iron Maiden, lançado em 23 de março de 1998.
            </p>

            <p>
                Este álbum foi o segundo e último disco gravado com o vocalista Blaze Bayley e produzido por Nigel Green e também o foi último
                disco do grupo gravado com a formação em quinteto encerrando a formação com duas guitarras.
            </p>

            <p>
                A turnê promocional, intitulada Virtual XI World Tour, teve diversos shows cancelados nos EUA devido a problemas de saúde de
                Blaze Bayley ocasionados pela sua reação alérgica a alguns elementos dos cenários usados nos shows.
            </p>

            <p>
                O título Virtual XI foi escolhido por coincidir com dois fatores extra-musicais: o lançamento do jogo de computador da banda,
                Ed Hunter, e o fato da realização da Copa do Mundo de 1998 ocorrer em junho daquele ano. Steve Harris explicou: "Nós imaginamos que
                nossos fãs sejam muito parecidos conosco, com interesses muito similares, então pensamos, 'É o ano da Copa do Mundo de 98. Vamos
                envolver o futebol com o novo álbum'. E estávamos trabalhando num jogo de computador na mesma época, então também resolvemos
                trazer alguns elementos dele para o disco". Antes de lançar o álbum, a banda organizou uma turnê de divulgação na qual eles
                marcaram jogos de futebol pela Europa com "músicos convidados e jogadores profissionais da Inglaterra".
            </p>

            <p>
                Embora a maior parte das artes dos encartes tenham sido tiradas do jogo Ed Hunter, a capa foi criada por Melvyn Grant. De acordo
                com Grant, pediram-lhe que desenhasse algo relacionado a realidade virtual, mas depois foi proposta uma alteração adicionando um
                jogo de futebol, após a banda decidir relacionar o lançamento com a Copa do Mundo. Também foi o primeiro disco a contar com um
                logotipo alternativo da banda, com as extensões de algumas letras sendo removidas para deixá-lo mais "enquadrado". Essa variante
                seria usada em todos os futuros álbuns e singles do Iron Maiden até - The Book of Souls em 2015, onde o logo tradicional retornou.
            </p>
