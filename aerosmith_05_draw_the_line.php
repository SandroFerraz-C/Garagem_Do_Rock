                quando a banda começou a gravar seu próximo álbum, - Draw the Line, seu estilo de vida excessivo, combinado com turnês constantes e
                uso de drogas, começou a cobrar seu preço. "Draw the Line não foi um bom álbum porque não éramos mais uma unidade coesa", admitiu o
                guitarrista Joe Perry nas memórias da banda de Stephen Davis, - Walk This Way -. "Éramos viciados em drogas que se interessavam
