            <h1>Supermercados da Vida (1992)</h1>

            <p>
                - Supermercados da Vida é o oitavo álbum da banda Barão Vermelho, lançado em 1992 pela WEA.
            </p>

            <p>
                O disco marca a estréia do baixista Rodrigo Santos no lugar de Dadi. Rodrigo estava desempregado em decorrência da dissolução
                 da banda de Lobão, que havia optado por carreira solo. Então Guto entrou em contato com Rodrigo Santos e oficializou a vinda 
                 do novo baixista.
            </p>

            <p>
                Para produzir a capa do álbum foi chamado o artista plástico Antonio Peticov, entretanto seu trabalho não agradou o grupo que acabou
                por escolher uma foto em que pessoas saqueavam um mercado, no último ano do mandato de Fernando Collor de Melo.
            </p>
            <br />

            <p>Duração do album: 44 min. 19 seg.</p>
            <p>Gravadora(s): WEA Discos.</p>
            <p>Produção: Paulo Junqueiro e Ezequiel Neves.</p>
