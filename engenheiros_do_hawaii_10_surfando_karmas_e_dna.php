            <h1>Surfando Karmas & DNA (2002)</h1>
            <br />

            <p>
                Surfando Karmas & DNA é o décimo primeiro álbum dos Engenheiros do Hawaii, produzido e lançado em 2002.
            </p>
            <br />

            <p>
                Entre as principais canções do disco, estão a faixa-título "Surfando Karmas & DNA", "3ª do Plural" e "E-Stória", fruto de uma troca
                de e-mails entre o vocalista Humberto Gessinger e o ex-baterista Carlos Maltz, cofundador da banda.
            </p>
            <br />

            <ul>
                <li>
                    "3ª do Plural" foi escrita por Humberto Gessinger quando a banda ainda era um trio, com Augusto Licks e Carlos Maltz. Porém, o
                    resultado da canção nunca agradava aos três. Por isso, a canção foi engavetada até 2001, quando foi finalmente gravada.
                </li>
                <br />

                <li>
                    A explicação do nome do disco é dita por Gessinger no press release do mesmo: "...segundo o Aurélio, Carma é o conjunto de ações
                    dos homens e suas conseqüências ; DNA é substância química envolvida na transmissão de caracteres hereditários ; surf já é uma
                    questão muito mais complexa, difícil de definir, né?".
                </li>
                <br />

                <li>
                    O encarte do disco mostra alguns do processos de desenvolvimento do projeto da capa.
                </li>
                <br />

                <li>
                    A partir de uma troca de e-mails com Gessinger, Carlos Maltz, ex-baterista do grupo, voltou a colaborar com a banda, ainda que
                    indiretamente. O primeiro resultado da retomada da parceria foi a canção E-Stória.
                </li>
                <br />

                <li>
                    A banda optou por não se trancar em estúdio por um período fixo. Em vez disso, o grupo preferiu conciliar a gravação do disco
                    com a turnê de 10.001 Destinos: nas folgas da turnê, eles gravavam o disco. O processo durou de junho a novembro de 2001.
                </li>
                <br />

                <li>
                    Um trecho de "Esportes Radicais", que diz: "180, 360, 540 graus / girando, esquentando / só pra ver até quando o motor aguenta o
                    caos", faz com que a letra da canção seja considerada por Humberto como uma continuação ou complemento da letra de "Infinita
                    Highway", sucesso de 1987.
                </li>
            </ul>
            <br /><br />


