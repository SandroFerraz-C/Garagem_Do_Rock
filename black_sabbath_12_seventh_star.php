            <h1>Seventh Star (1986)</h1>

            <p>
               - Seventh Star é o décimo segundo álbum do Black Sabbath.
            </p>

            <p>
                Foi o primeiro lançamento do grupo sem o baixista Geezer Butler, que havia saído em 1984 após o fim da turnê - Born Again -. Originalmente,
                - Seventh Star foi composto e gravado para ser o primeiro disco solo de Tony Iommi, como evidenciado pela experimentações musicais
                apresentadas. Contudo, devido à pressão feita pela Warner Bros. Records e pelo manager da banda, Don Arden, o álbum acabou sendo publicado
                como Black Sabbath featuring Tony Iommi. Versões posteriores foram publicadas simplesmente como Black Sabbath.
            </p>

            <p>
                O lançamento veio em um período difícil da banda, já que eles haviam finalizado uma turnê conturbada, após um ano de inúmeras mudanças de
                integrantes. Tony Iommi, que era o guitarrista, compositor e único membro original que havia restado, chamou Geoff Nicholls, Eric Singer,
                Dave Spitz e Glenn Hughes para gravar o disco. Musicalmente, Seventh Star é mais eclético e segue um estilo de hard rock mais puro,
                afastando-se do heavy metal tradicional do grupo. Adicionalmente, foi o primeiro álbum do Black Sabbath como quinteto, já que Geoff Nicholls
                havia sido confirmado como membro oficial no ano anterior.
            </p>

            <p>
                Apesar dos problemas envolvidos em sua produção, - Seventh Star obteve considerável sucesso comercial, chegando ao #27º lugar nas paradas musicais
                do Reino Unido e #78º lugar na Billboard 200.
            </p>
            <br /><br />

            <p>Duração do album: 34 min. 46 seg.</p>
            <p>Gravadora(s): Vertigo / CASS / Castle / Sanctuary.</p>
            <p>Produção: Jeff Glixman. </p>
