                Renato, Dado e Bonfá passaram a contar com três músicos de apoio: Fred Nascimento (guitarra rítmica e violão), Bruno Araújo
                (baixo elétrico) e o produtor musical Mu Carvalho (teclados).
            </p>
            <br />

            <p>
                o clima entre os membros durante as gravações de - As Quatro Estações era amargo, não só devido ao então recente incidente no
                Estádio Mané Garrincha, mas também porque a siuação do baixista Renato Rocha ia se tornando cada vez mais insustentável. Após
                comprar um sítio em Mendes, ele passou a levar cerca de duas horas para chegar ao estúdio no Rio de Janeiro. Faltava a muitos
                ensaios e, quando vinha, demorava a se integrar com o grupo, muitas vezes pedindo para levar as fitas para casa de modo a criar
                seus arranjos lá. Seis meses após o início da criação do disco, Renato foi demitido em uma reunião na sede da gravadora EMI-Odeon.
                Apesar disso, os membros remanescentes decidiram que ele receberia todos os direitos sobre o novo disco.
            </p>
