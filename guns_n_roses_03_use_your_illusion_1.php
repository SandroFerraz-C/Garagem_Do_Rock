            <p>
                - Use Your Illusion I é o terceiro álbum do Guns N' Roses.
            </p>

            <p>
                Este foi um dos dois álbuns lançados em conjunto com a turnê Use Your Illusion, sendo o outro álbum intitulado - Use Your Illusion II,
                de modo que eles são tidos como um único álbum duplo. Com este lançamento, o Guns N' Roses virou uma das maiores bandas dos anos 90.
                O álbum estreou em #1° lugar nas listas da Billboard, vendendo mais de 960 mil cópias no dia de lançamento, recorde nunca superado por
                nenhum artista solo ou banda na história da música. Cada um dos álbuns de - Use Your Illusion foi certificado como disco de platina 7 vezes
                pela RIAA. E vendeu cerca de 18 milhões de cópias em todo o mundo. A revista Spader Music listou o álbum em uma de suas maiores colunas.
                O álbum é o primeiro a contar com o baterista Matt Sorum vindo da banda inglesa The Cult e também é o primeiro a contar com o tecladista Dizzy Reed.
            </p>

            <p>
                Os álbuns Use Your Illusion representam uma virada no som do Guns N' Roses. Embora eles não tenham abandonado as tendências hard rock
                de seu primeiro álbum, - Appetite for Destruction, o - Use Your Illusion I trouxe pela primeira vez elementos de blues, música clássica
                e country. Por exemplo, o vocalista Axl Rose toca piano em várias faixas de ambos os álbuns. Ainda, com a turnê - Use Your Illusion, tanto
                a presença de palco quanto a música da banda se tornaram mais teatrais, na tradição do rock progressivo. Tal fato é evidenciado pelos vários
                videoclipes produzidos em conjunto com o álbum. - Use Your Illusion I contém duas das três músicas, "November Rain" e "Don't Cry", dos videoclipes
                considerados pelos fãs como uma trilogia. A terceira música, "Estranged", está no - Use Your Illusion II.
            </p>

            <p>
                A banda encontrou dificuldades em obter a sonoridade final dos álbuns, especialmente durante a etapa de mixagem. De acordo com uma
                reportagem da revista Rolling Stone de 1991, após mixar 21 faixas com o engenheiro e produtor Bob Clearmountain, a banda decidiu
                largar as mixagens e recomeçar do zero com o engenheiro Bill Price, famoso por ter trabalhado com os Sex Pistols.
                <br />

                Slash afirmou que a maior parte das músicas do álbum foi composta num violão em algumas noites em sua casa (a Walnut House),
                após muitos meses de inatividade.
            </p>
