                <p>Em novembro de 1973, os guitarristas Malcolm Young e
                    Angus Young, formaram o AC/DC e recrutaram o baixista Larry Van Kriedt, o vocalista Dave Evans e Colin Burgess, ex- baterista do Masters Apprentices.
                    Logo os irmãos Young decidiram que Evans não era um vocalista adequado para o grupo; eles achavam que ele era mais um roqueiro glam como Gary Glitter. A banda gravou apenas um single com Evans, "Can I Sit Next To You Girl".
                </p>

                <p>
                    Em setembro
                    de 1974, Ronald Belford
                    Bon Scott, um experiente vocalista e amigo do produtor George Young, substituiu Dave Evans. A adição de Scott redefiniu a banda; assim como os
                    irmãos Young, Scott nasceu na Escócia antes de emigrar para a Austrália na infância e amava o rock and roll, especialmente Little Richard. Em uma entrevista de 2010 para Mojo's Sylvie Simmons, Angus Young lembrou que Scott "moldou o caráter
                    do AC/DC... Tudo se tornou mais realista e direto. Foi quando nos tornamos uma banda".
                </p>

                <p>- High Voltage foi originalmente lançado pela Albert Productions apenas na Austrália, e nunca foi relançado por outra gravadora neste formato. <a href="acdc_03_high_voltage_internacional.php" target="blank">A versão internacional de High Voltage</a>,
                    que foi lançada pela Atlantic Records em 1976, tem uma capa e lista de faixas diferentes, com apenas "She Got Balls" e "Little Lover" aparecendo no álbum. "Stick Around" (sobre a incapacidade de Scott de se segurar em uma amante por mais
