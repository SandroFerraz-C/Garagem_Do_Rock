                <h1><span lang="en-US">High Voltage</span> "Versão Australiana" (<time datetime="1975">1975</time>)</h1>

                <p>-<span lang="en-US">High Voltage</span> é o primeiro álbum da banda australiana de <span lang="en-US">Hard Rock</span>AC/DC, lançado em <time datetime="1975-02-17">17 de Fevereiro de 1975</time> apenas na Austrália.
                    O primeiro lançamento internacional em <time datetime="1975">1976</time> também seria chamado - <a href="acdc_03_high_voltage_internacional.php" target="blank"><span lang="en-US">High Voltage</span></a>, embora com uma
                    lista de faixas radicalmente diferente.</p>

                <p>Em novembro de <time datetime="1975">1973</time>, os guitarristas Malcolm Young e Angus Young, formaram o AC/DC e recrutaram o baixista Larry Van Kriedt, o vocalista Dave Evans e Colin Burgess, ex- baterista do Masters Apprentices.
                    Logo os irmãos Young decidiram que Evans não era um vocalista adequado para o grupo; eles achavam que ele era mais um roqueiro glam como Gary Glitter. A banda gravou apenas um single com Evans, "<span lang="en-US">Can I Sit Next To You Girl</span>".
                </p>

                <p>
                    Em <time datetime="1974-09">setembro de 1974</time>, Ronald Belford Bon Scott, um experiente vocalista e amigo do produtor George Young, substituiu Dave Evans. A adição de Scott redefiniu a banda; assim como os
                    irmãos Young, Scott nasceu na Escócia antes de emigrar para a Austrália na infância e amava o <span lang="en-US">Rock and Roll</span>, especialmente Little Richard. Em uma entrevista de <time datetime="2010">2010</time> para Mojo's Sylvie Simmons, Angus Young lembrou que Scott "moldou o caráter
                    do AC/DC... Tudo se tornou mais realista e direto. Foi quando nos tornamos uma banda".
                </p>

                <p>- <span lang="en-US">High Voltage</span> foi originalmente lançado pela Albert Productions apenas na Austrália, e nunca foi relançado por outra gravadora neste formato. <a href="acdc_03_high_voltage_internacional.php" target="blank">A versão internacional de <span lang="en-US">High Voltage</span></a>,
                    que foi lançada pela Atlantic Records em <time datetime="1976">1976</time>, tem uma capa e lista de faixas diferentes, com apenas "<span lang="en-US">She Got Balls</span>" e "<span lang="en-US">Little Lover</span>" aparecendo no álbum. "<span lang="en-US">Stick Around</span>"
                    (sobre a incapacidade de Scott de se segurar em uma amante por mais de uma noite) e "<span lang="en-US">Love Song</span>" foram relançados na - <span lang="en-US">Backtracksem</span> <time datetime="2009">2009</time>.
                </p>

                <p>O título e a arte foram sugestão de Chris Gilbey, da Albert Productions.
