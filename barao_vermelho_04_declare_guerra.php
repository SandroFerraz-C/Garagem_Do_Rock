            <h1>Declare Guerra (1986)</h1>

            <p>- Declare Guerra é o quarto álbum da banda Barão Vermelho, é também o primeiro álbum sem Cazuza e o último pela gravadora Som Livre.</p>

            <p>Em 1985, Cazuza, por conta de algumas brigas e de seu interesse em fazer um trabalho solo, sai da banda. Leva com ele algumas
                composições que fariam parte do seu primeiro trabalho solo, como "Exagerado" e "Só as Mães São Felizes". A banda estava pronta para
                começar a gravação do novo trabalho, mas por conta disso houve um atraso, se inicia uma fase ruim, para muitos era o fim, mas
                Frejat assume os vocais e ainda em 1985, surge a primeira música do Barão Vermelho sem o Cazuza, "Torre de Babel", gravada num
                programa infantil de TV.</p>

            <p>Finalmente em 1986 depois de um período longo de gravação, lançam o álbum - Declare Guerra, que contou com a participação de
                letristas como Renato Russo e Arnaldo Antunes, porém, ele não teve publicidade, vendendo muito pouco (em relação aos
                demais álbuns da banda). Ainda em 1986 a banda deixa a Som Livre e assina contrato com a WEA.</p><br />

