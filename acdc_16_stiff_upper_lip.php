            <h1>STIFF UPPER LIP (2000)</h1>

            <p> - Stiff Upper Lip é o 16° álbum do AC/DC, sendo lançado em 2000.</p>

            <p>A primeira coisa a se destacar em - Stiff Upper Lip é a opção acertada da tríade Young em fazer da mixagem um festival de guitarras que, não raramente, abafa o agudo quase monocórdico de Brian Johnson.
                <br /> A segunda coisa é a surpresa de, ao contrário de vários álbuns anteriores, as faixas irem evoluindo progressivamente.
            </p>

            <p>Os irmãos Young começaram a escrever canções para o que viria a ser - Stiff Upper Lip no verão de 1997 em Londres e na Holanda, com Malcolm na guitarra e Angus na bateria, e em fevereiro de 1998 as canções estavam concluídas. A banda planejava gravar
                um novo álbum com o canadense Bruce Fairbairn, que produziu o enorme sucesso - <a href="acdc_14_the_razor's_edge.php" target="blank">The Razors Edge</a> e AC/DC Live, mas Fairbairn morreu em maio de 1999. Os Youngs
                se voltaram para seu irmão mais velho George, que tinha produzido - <a href="acdc_13_blow_up_your_video.php" target="blank"> Blow Up Your Video</a>, de 1988, bem como os primeiros álbuns da banda com Harry Vanda e Mike Fraser, que
                co-produziu
                <a href="acdc_15_ballbreaker.php" target="blank">Ballbreaker</a> de 1995 (com Rick Rubin ) e para completar - Stiff Upper Lip.
            </p>

            <p>Se a faixa-título lembra bastante "Hard As A Rock", "Meltdown", a segunda, evoca os tempos de Bon Scott, com espírito bem rock’n roll. "House Of Jazz" tem marcação acentuada, bem pesada, uma das melhores do disco. "Hold Me Back" começa na linha de "Thunderstruck",
                assim como "Can’t Stand Still". E é na sétima faixa, "You Can’t Stop Rock’n Roll" que o álbum atinge seu ápice, um formidável trabalho de guitarras no mais puro estilo hard rock, como não se faz mais hoje em dia. Ouça-a o mais alto
                que puder. "All Screwed Up" te leva a pensar que está escutando "Electric", o cd mais rocker do The Cult, pela semelhança de timbre e pegada.</p>

            <p>Este álbum foi lançada no Estados Unidos em 17 de abril de 2007, na série Remasters. Isso foi feito no Reino Unido em 2005. O álbum está atualmente certificado em platina nos E.U.A com vendas superiores a 1.000.000 cópias.</p>
