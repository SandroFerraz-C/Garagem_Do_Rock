            <h1>Gessinger, Licks & Maltz (1992)</h1>

            <p>-  Gessinger, Licks & Maltz é o sexto álbum dos Engenheiros do Hawaii, produzido e lançado em 1992.</p>

            <p>Foi o derradeiro disco de músicas inéditas gravado pela formação clássica da banda, formação essa que, inclusive, dá título ao mesmo.</p>

            <p>O disco marca uma estética baseada no rock progressivo de bandas como Emerson, Lake & Palmer e Pink Floyd. Os principais sucessos
                do disco são: "Ninguém = Ninguém", "Pose" (Anos 90),"Até Quando Você Vai Ficar?", "Parabólica" (composição de Humberto Gessinger e
                Augusto Licks, dedicada a Clara, filha de Humberto, nascida no início de 1992) e "A Conquista Do Espaço".</p>

            <p>A capa do disco mostra as letras GLM (iniciais dos sobrenomes dos integrantes da banda) na forma de uma engrenagem, que lembra o
                símbolo da banda Emerson, Lake & Palmer, que pode ser vista no disco Brain Salad Surgery. Foi também o último disco auto-produzido
                pela banda, assim como os anteriores O Papa é Pop (1990) e Várias Variáveis (1991).</p>

            <p>Lulu Santos foi convidado por Humberto Gessinger para produzir o álbum, mas Humberto levou a demo com a capa do disco já pronta com
                o trabalho muito próximo de ser finalizado. Por causa disso, Lulu rejeitou o convite.</p>
            <br />


            <p>Duração do álbum: 51 min. 45 seg.</p>
            <p>Gravadora(s): BMG.</p>
            <p>Produção: Engenheiros do Hawaii.</p>
