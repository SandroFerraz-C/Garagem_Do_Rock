            <h1>Mob Rules (1981)</h1>

            <br />
            <p>
                Mob Rules é o décimo álbum de estúdio da banda de heavy metal Black Sabbath.
            </p>
            <br />

            <p>
                Infelizmente, durante a turnê do álbum Heaven and Hell o Black Sabbath perdeu outro membro que sempre esteve na banda, o baterista
                Bill Ward, que teve de se afastar por motivos de saúde (provavelmente por causa do álcool) e por motivos pessoais (perda de seus pais).
                A saída de Ozzy foi de importante influencia na referida decisão de Ward. O escolhido para substituir Ward na bateria foi Vinny Appice, e
                seu primeiro trabalho com a banda foi a canção "The Mob Rules" que também foi escolhida para ser o nome do novo álbum, e seu primeiro single.
            </p>
            <br />

            <p>
                Depois do sucesso de Heaven and Hell, em 1980, o Black Sabbath provou que não apenas podia seguir em frente sem seu co-fundador
                Ozzy Osbourne, mas que podia ir adiante conquistando uma nova legião de fãs do heavy metal. Mob Rules ficou em décimo segundo nas
                paradas britânicas e em vigésimo nono nas americanas.
            </p>
            <br />

            <p>
                O Black Sabbath nesse ponto estava com o empresário Sandy Pearlman, o mesmo da banda Blue Öyster Cult, o que resultou em uma turnê
                conjunta, que levou o nome de Black and Blue Joint Tour, nome de um VHS que seria lançado posteriormente. Dio e Appice fizeram sua
                última performance com a banda no dia 31 de Agosto de 1982.
