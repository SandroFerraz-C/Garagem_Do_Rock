            <h1>Led Zeppelin II (1969)</h1>
            <br />

            <p>
                - Led Zeppelin II é o segundo álbum do Led Zeppelin, lançado em 22 de outubro de 1969 pela Atlantic Records.
            </p>
            <br />

            <p>
                As sessões de produção e gravação ocorreram em diversos locais do Reino Unido e Estados Unidos, entre janeiro e agosto de 1969.
                A produção foi inteiramente creditada ao guitarrista e compositor Jimmy Page. Foi o primeiro álbum do Led Zeppellin a utilizar as
                técnicas do engenheiro e produtor de som Eddie Kramer.
            </p>
            <br />

            <p>
                Após o seu lançamento, - Led Zeppelin II teve um bom desempenho nas vendas, foi o primeiro álbum da banda a chegar à primeira
                posição nas paradas do Reino Unido e nos Estados Unidos. Em 1970, o diretor de arte David Juniper, autor da capa do álbum, foi
                indicado para o Grammy Award na categoria de Melhor Capa. Em 15 de novembro de 1999, o disco recebeu doze certificações de platina
                pela (RIAA) por vendas superiores a 12 milhões de cópias. Desde seu lançamento, o disco tem sido reconhecido por escritores e
                críticos musicais como um dos maiores e mais influentes álbuns de rock já lançados.
            </p>
            <br />

            <p>
                Cada canção foi gravada, mixada e produzida separadamente, em vários estúdios no Reino Unido e América do Norte, alguns dos quais
                eram tecnicamente pobres. Um estúdio em Vancouver, foi creditado como "uma cabana", tinha um equipamento de oito canais que nem
                mesmo possuía conexões apropriadas para fones de ouvido. As sessões de gravação do álbum ocorreram em 12 estudios diferentes.
                Sobre o processo de composição e gravação, Robert Plant, vocalista do grupo, mais tarde afirmou: "Foi realmente uma loucura.
                Compúnhamos em quartos de hotel, gravávamos a faixa de ritmo em Londres, adicionávamos os vocais em Nova Iorque, a gaita em
                Vancouver e, por fim, fazíamos a mixagem em Nova Iorque".
            </p>
            <br />

            <p>
                A produção foi inteiramente creditada a Jimmy Page, embora este tenha sido o primeiro álbum da banda a utilizar as habilidades e
                técnicas de gravação do engenheiro Eddie Kramer, cujo trabalho anterior com Jimi Hendrix havia impressionado os membros da banda,
                especialmente Page. O especialista em Led Zeppelin, Dave Lewis, escreveu sobre a produção do disco, afirmando "que o álbum tornou-se
                um triunfo, particularmente por uma qualidade de produção que ainda hoje soa fresca, que não deve pouco a grande comunhão de Page
                e Kramer na sala de controle". Esta parceria fica clara na seção central da faixa "Whole Lotta Love". Kramer mais tarde comentou:
                "O famoso trecho de Whole Lotta Love, onde acontece de tudo, é o resultado que Jimmy e eu obtivemos ao experimentar absolutamente
                todos os botões do pequeno console de mixagem".
            </p>
            <br /><br />

