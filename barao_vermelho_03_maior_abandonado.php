            <h1>Maior Abandonado (1984)</h1>

            <p>
                - Maior Abandonado é o terceiro álbum da banda de rock brasileiro Barão Vermelho, lançado em outubro de 1984.
            <p>

            <p>
                É considerado o melhor álbum da banda, tanto pela crítica especializada como pelo público. Foi o último álbum com a formação
                 original, com Cazuza nos vocais, Roberto Frejat na guitarra, Dé Palmeira no baixo, Maurício Barros nos teclados e Guto Goffi
                  na bateria.
                </p>

            <p>
                Após "Pro Dia Nascer Feliz" virar um hit no Brasil, a banda é convidada para compor e gravar o tema do filme Bete Balanço. "Bete Balanço"
                torna-se a música de maior sucesso da banda impulsionando o filme, que vira sucesso de bilheteria. A música também impulsiona as vendagens
                do terceiro disco do Barão Maior Abandonado lançado em outubro de 1984, que conquista disco de ouro. Outros sucessos do álbum são: "Maior
                Abandonado" e "Por Que a Gente é Assim?".
            </p>

            <p>
                Ainda em 1984, é lançado o single "Eu Queria Ter Uma Bomba" (que faz parte da trilha sonora da telenovela A Gata Comeu).
            </p>

            <p>
                A banda se apresentou no Rock in Rio em janeiro de 1985 e foi uma das poucas bandas brasileiras a não ser vaiada. Após este
                álbum, em julho de 1985, durante os ensaios do próximo álbum, Cazuza deixou o Barão Vermelho para a seguir carreira solo.

            </p>
            <br />

            <p>Duração do album: 35 min. 09 seg.</p>
            <p>Gravadora(s): Opus Columbia.</p>
            <p>Produção: Ezequiel Neves e Barão Vermelho. </p>
