            <h1>O Descobrimento do Brasil (1993)</h1>
            <br />

            <p>
                - O Descobrimento do Brasil é o sexto álbum da Legião Urbana, lançado em 1993 pela gravadora EMI-Odeon.
            </p>
            <br />

            <p>
                No Brasil, foram vendidos mais 950 mil cópias, tornando-o o sétimo álbum mais vendido da banda. Foi premiado com Disco de Platina
                Triplo pela ABPD.
            </p>
            <br />

            <p>
                O álbum foi criado num momento de tensão entre a banda e a gravadora. Na época, a empresa havia vendido sua fábrica de discos
                e agora usava a Fonobras, da concorrente PolyGram. Por coincidência, o produtor Mayrton Bahia trabalhava lá e soube, por meio
                de um pedido que chegou ao local, que a EMI-Odeon tinha a intenção de lançar uma coletânea da banda à revelia da mesma.
                Furiosos, os integrantes picharam as paredes da diretoria da empresa, incluindo a frase (em inglês) "Vocês nos tratam como lixo!
                Nunca mais vocês vão fazer isso!", escrita pelo vocalista Renato Russo.
                <br /><br />

                Por não saberem as consequências que o ato traria para eles, os músicos suspenderam os ensaios até que a gravadora sinalizasse algo.
                No fim, ela acabou cancelando a coletânea. Esse foi um dos motivos que levou João Augusto a proceder com cautela ao iniciar seu
                relacionamento com a banda.
            </p>
            <br />

            <p>
                O disco recebeu o nome de "O Descobrimento do Brasil" porque veio em uma época em que Renato se recuperava das drogas e os demais
                integrantes se mostravam otimistas com o futuro. Além disso, conforme Renato disse ao <a href="https://www.jb.com.br"> Jornal do Brasil</a>,
                "A gente acredita no Brasil. Existem muita coisas legais. Ficam querendo que a gente seja ladrão, que seja do jeito que eles são. Nós
                não somos, não".
                <br /><br />

                A banda tinha como intenção fazer uma obra diferente do lançamento anterior, - V, que trazia, segundo eles, "aquelas músicas
                progressivas, de carga melancólica". A ideia do novo disco, para Renato, era que fosse uma coletânea de singles pop curtos.
            </p>
            <br />

            <p>
                A capa de -O Descobrimento do Brasil tinha como objetivo transmitir o momento de leveza e otimismo pelo qual a banda passava na
                época de seu lançamento. Ela traz os três integrantes em um campo florido montado especialmente para o momento, cada um vestindo
                uma roupa diferente: Renato aparece como um cavaleiro medieval, Dado como um caçador com um bandolim e Marcelo como um camponês.
                A foto é de Flávio Colker. No dia em que a imagem foi registrada, Dado teve seu carro e seu bandolim levados por um assaltante
                em frente ao estúdio, forçando a banda a substituir o instrumento na foto.
            </p>
            <br /><br />

            <p>Duração do album: 51 min. 20 seg.</p>
            <p>Gravadora(s): EMI-Odeon.</p>
            <p>Produção: Mayrton Bahia e Legião Urbana.</p>
