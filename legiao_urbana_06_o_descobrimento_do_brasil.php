            </p>

            <p>
                A capa de -O Descobrimento do Brasil tinha como objetivo transmitir o momento de leveza e otimismo pelo qual a banda passava na
                época de seu lançamento. Ela traz os três integrantes em um campo florido montado especialmente para o momento, cada um vestindo
                uma roupa diferente: Renato aparece como um cavaleiro medieval, Dado como um caçador com um bandolim e Marcelo como um camponês.
                A foto é de Flávio Colker. No dia em que a imagem foi registrada, Dado teve seu carro e seu bandolim levados por um assaltante
