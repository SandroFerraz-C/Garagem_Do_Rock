            <h1>Powerslave (1984)</h1>
            <br />

            <p>
                - Powerslave é o quinto álbum do Iron Maiden, lançado em 3 de setembro de 1984.
            </p>
            <br />

            <p>
                Tendo Bruce Dickinson no vocal, Adrian Smith e Dave Murray nas guitarras, Steve Harris no baixo e Nicko McBrain na bateria,
                - Powerslave, assim como o - The Number of the Beast, é um dos expoentes do Iron Maiden. Marca uma época em que a formação estava
                muito entrosada e amiga, pois em anos anteriores a batalha de egos entre Steve Harris e Bruce Dickinson deixava sempre uma
                incerteza sobre a continuação de Bruce na banda.
            </p>
            <br />

            <p>
                Gravado nas Bahamas e mixado nos Estados Unidos, o álbum tem sua abertura com "Aces High", uma canção sobre as lutas aéreas na
                Segunda Guerra Mundial. Logo depois tem "2 Minutes to Midnight", escrita por Adrian Smith com letras de Bruce, tratando da constante
                ameaça de uma guerra nuclear que pairava na época. Logo após, uma canção instrumental ao estilo egípcio do álbum, chamado "Losfer Words
                (Big 'Orra)". Depois as canções "Flash of the Blade", "The Duellists" e "Back in the Village". Segue "Powerslave", composta por Bruce
                Dickinson e que trata de temas egípcios e como as pessoas podem ficar "escravas do poder". Por fim, a "saga" "Rime of the Ancient Mariner",
                escrita por Steve Harris, sobre uma longa canção de treze minutos sobre um homem amaldiçoado por um Albatroz; Foi escrita a partir do poema
                homônimo do poeta romântico inglês do século XVIII, Samuel Taylor Coleridge.
            </p>
            <br />

            <p>
                Em 2017, foi eleito o 38º melhor álbum de metal de todos os tempos pela revista Rolling Stone.
            </p>
