            <h1>História do AC/DC</h1>

            <p>AC/DC é uma banda australiana de rock formada em Sydney, Austrália em 1973, pelos irmãos escoceses Malcolm e Angus Young.
                O estilo musical da banda é normalmente classificado como hard rock e até mesmo blues rock. Mas seus membros sempre classificaram a sua
                música simplesmente como "rock and roll".</p>

            <p>O AC/DC passou por várias mudanças de alinhamento antes de lançarem o seu primeiro álbum, High Voltage, em 1975.
                A formação manteve-se estável até o baixista Cliff Williams substituir Mark Evans em 1977. Em 1979,
                a banda gravou o seu bem-sucedido álbum Highway to Hell. O vocalista e co-compositor Bon Scott faleceu em 19 de fevereiro de 1980,
                após consumir na noite anterior uma grande quantidade de álcool, e o corpo foi encontrado no banco de trás do carro de Alistair Kinnear.
                O grupo considerou por algum tempo a separação, mas rapidamente o ex-vocalista dos Geordie, Brian Johnson,
                foi selecionado para o lugar de Scott. Mais tarde nesse ano, a banda lançou o seu álbum mais vendido, Back in Black.</p>

            <p> O álbum seguinte da banda, For Those About to Rock (We Salute You), foi também bem sucedido e tornou-se o primeiro álbum da banda a
                atingir o 1.º lugar nos Estados Unidos. O AC/DC caiu em popularidade pouco após a saída do baterista Phil Rudd em 1983.
                As fracas vendas continuaram até ao lançamento de The Razor's Edge em 1990. Phil Rudd regressou em 1994 e contribuiu para o álbum de 1995
                da banda, Ballbreaker. Stiff Upper Lip foi lançado em 2000, sendo bem recebido pela crítica.
                Planos para um novo álbum foram anunciados em 2004, sendo cumpridos em 2008, com o lançamento do álbum Black Ice no dia
                20 de outubro de 2008. Em 2016, Brian Johnson foi afastado da banda por risco de surdez e Axl Rose completou a turnê do AC/DC.</p>

            <p>A banda já vendeu mais de 200 milhões de cópias em todo o mundo, incluindo 71 milhões somente nos Estados Unidos.
                Back in Black já vendeu cerca de 50 milhões de cópias mundialmente, das quais 22 nos Estados Unidos,
                fazendo dele o 2.º álbum mais vendido de todos os tempos e o 5.º mais vendido nos Estados Unidos.
                AC/DC ficou em quarto na lista da VH1 os "100 Maiores Artistas de Hard Rock" e
                foram considerados pela MTV a 7.ª "Maior Banda de Heavy Metal de Todos os Tempos" e em 2004, a banda ficou em 72.º na lista dos
                "100 Maiores Artistas de Todos os Tempos" feita pela revista Rolling Stone.</p>

             
