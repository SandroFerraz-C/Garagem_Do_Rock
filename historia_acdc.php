            <h1>História do AC/DC</h1>

            <p>
                AC/DC é uma banda australiana de <span lang="en-us">Rock</span> formada em Sydney, Austrália em <time datetime="1973">1973</time>, pelos irmãos escoceses
                Malcolm e Angus Young. O estilo musical da banda é normalmente classificado como <span lang="en-us">hard rock</span> e até
                mesmo <span lang="en-us">blues rock</span>. Mas seus membros sempre classificaram a sua música simplesmente como
                "<span lang="en-us">rock and roll</span>".
            </p>

            <p>
                O AC/DC passou por várias mudanças de alinhamento antes de lançarem o seu primeiro álbum,
                - <a href="acdc_01_high_voltage.php" target="blanck"><span lang="en-us">High Voltage</span></a>, em <time datetime="1975">1975</time>.
                A formação manteve-se estável até o baixista Cliff Williams substituir Mark Evans em <time datetime="1977">1977</time>.
                Em <time datetime="1977">1977</time>,a banda gravou o seu bem-sucedido álbum - <a href="acdc_07_highway_to_hell.php" target="blanck"><span lang="en-us">Highway to Hell</span></a>.
            </p>

            <p>O vocalista e co-compositor Bon Scott faleceu em <time datetime="1980-02-19">19 de fevereiro de 1980</time>, após
                consumir na noite anterior uma grande quantidade de álcool, e o corpo foi encontrado no banco de trás do carro de Alistair Kinnear.
                O grupo considerou por algum tempo a separação, mas rapidamente o ex-vocalista dos Geordie, Brian Johnson,
                foi selecionado para o lugar de Scott. Mais tarde nesse ano, a banda lançou o seu álbum mais vendido,
                - <a href="acdc_08_black_in_black.php" target="blanck"><span lang="en-us">Back in Black</span></a>.
            </p>

            <p>
                O álbum seguinte da banda, - <a href="acdc_09_for_those_about_to_rock_(we_salute_you).php" target="blanck">
                    <span lang="en-us">For Those About to Rock (We Salute You)</span></a>,
                foi também bem sucedido e tornou-se o primeiro álbum da banda a atingir o #1.º lugar nos Estados Unidos.
            </p>

            <p>
                O AC/DC caiu em popularidade pouco após a saída do baterista Phil Rudd em <time datetime="1983">1983</time>.
                As fracas vendas continuaram até ao lançamento de - <a href="acdc_14_the_razor's_edge.php" target="blanck"><span lang="en-us">The Razor's Edge</span></a>
                em <time datetime="1990">1990</time>.
                Phil Rudd regressou em <time datetime="1994">1994</time> e contribuiu para o álbum de <time datetime="1995">1995</time> da banda,
                - <a href="acdc_15_ballbreaker.php" target="blanck"><span lang="en-us">Ballbreaker</span></a>.
                - <a href="acdc_16_stiff_upper_lip.php" target="blanck"><span lang="en-us">Stiff Upper Lip</span></a> foi lançado em <time datetime="2000">2000</time>, sendo bem recebido pela crítica.
            </p>

            <p>
                Planos para um novo álbum foram anunciados em <time datetime="2004">2004</time>, sendo cumpridos em <time datetime="2008">2008</time>,
                com o lançamento do álbum - <a href="acdc_17_black_ice.php" target="blanck"><span lang="en-us">Black Ice</span></a> no dia <time datetime="2008-10-29">20 de outubro de 2008</time>. Em <time datetime="2016">2016,
                    Brian Johnson foi afastado da banda por risco de surdez e Axl Rose do <a href="guns_n_roses.php" target="blank">Guns N' Roses</a> completou a turnê do AC/DC.
            </p>

            <p>
                A banda já vendeu mais de 200 milhões de cópias em todo o mundo; <a href="acdc_08_back_to_black.php" target="blanck"><span lang="en-us">Back in Black</span></a>
                já vendeu cerca de 50 milhões de cópias mundialmente, fazendo dele o #2.º álbum mais vendido de todos os tempos e o #5º mais vendido.
            </p>

            <p>
                AC/DC ficou em quarto na lista da VH1 "Os 100 Maiores Artistas de Hard Rock" e foram considerados pela MTV a #7.ª
                "Maior Banda de <span lang="en-us">Heavy Metal</span> de Todos os Tempos" e em <time datetime="2004">2004</time>, a banda ficou em #72.º na lista dos
                "100 Maiores Artistas de Todos os Tempos" feita pela revista <cite>Rolling Stone</cite>.
            </p><br />

