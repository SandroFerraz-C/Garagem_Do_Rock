            <h1>Simples de Coração (1995)</h1>
            <br />

            <p>
                Simples de Coração é o setimo álbum dos Engenheiros do Hawaii, produzido e lançado em 1995.
            </p>
            <br />

            <p>
                Foi o primeiro álbum da banda a ser produzido já para o formato CD e também o último a ser lançado em fita cassete.
            </p>
            <br />

            <p>
                Foi primeiro - e até agora o único - álbum de estúdio dos Engenheiros cuja formação que o gravou foi um quinteto: Humberto Gessinger
                (voz e baixo), Carlos Maltz (bateria), Ricardo Horn (guitarra), o então ex-RPM Fernando Deluqui (guitarra) e Paolo Casarin (teclados).
                Seus principais sucessos foram as canções "A Perigo" e "A Promessa" (ambas ganharam videoclipes). Em 1996, os três novos integrantes e o
                fundador Carlos Maltz sairiam da banda e, com isso, Gessinger daria uma breve pausa nos Engenheiros para trabalhar com o Humberto Gessinger
                Trio, sua segunda banda.
            </p>
            <br />

            <p>
                Foi o primeiro álbum da banda gravado fora do Brasil (em Los Angeles, com produção de Greg Ladanyi). Devido a isso, há participações
                estrangeiras, como o percussionista cubano Luis Conte (que já trabalhou com Roger Waters e Phil Collins), que assina a percussão de
                "A Promessa".
            </p>
            <br />

            <p>
                Este disco também foi gravado em inglês, porém arquivada pela BMG e nunca foi lançada oficialmente, mas uma versão com qualidade
                baixa vazou na internet.
            </p>
            <br /><br />


            <p>Duração do album: 45 min. 00 seg.</p>
            <p>Gravadora(s): BMG.</p>
            <p>Produção: Greg Ladanyi.</p>

