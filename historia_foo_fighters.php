            <h1>História do Foo Fighters</h1>

            <p>
                Foo Fighters é uma banda de rock dos Estados Unidos formada pelo ex-Nirvana Dave Grohl em 1994. Seu nome é uma referência ao
                termo "foo fighter", usado por aviadores na Segunda Guerra Mundial para descrever fenômenos aéreos misteriosos, considerados OVNIs.
                A banda atingiu sucesso internacional, lançando vários hits incluindo "This Is a Call", "Everlong", "Learn to Fly", "All My Life",
                "Times Like These", "Best of You", "Walk", "My Hero" e "The Pretender". Quatro de seus álbuns, - There Is Nothing Left to Lose,
                - One by One, - Echoes, - Silence, - Patience & Grace e - Wasting Light ganharam o Grammy por melhores álbuns de rock.
            </p>

            <p>
                Dave Grohl passou quatro anos como baterista da banda grunge Nirvana. Nesse período desenvolveu uma série de composições não
                divulgadas, uma forma de preservar a interação do grupo. Em contrapartida, Dave gravou algumas demos em estúdio, sendo que algumas
                canções foram compiladas no álbum Pocketwatch, lançado com o pseudônimo "Late!", em 1992.
            </p>

            <p>
                Após a morte de Kurt Cobain em Abril de 1994, Grohl considerou abandonar a música, mas consequentemente resolveu gravar como
                experiência suas composições. Em Outubro do mesmo ano, Grohl entrou no Robert Lang's Studio em Seattle com o amigo e produtor
                musical Barrett Jones. No espaço de uma semana, Grohl gravou as faixas cantando e tocando todos os instrumentos, com exceção de
                uma guitarra em "X-Static" por Greg Dulli do grupo americano Afghan Whigs. Para manter o anonimato, Grohl escolheu o nome
                "Foo Fighters" para criar a ilusão de que era uma banda em vez de "aquele cara do Nirvana".
            </p>

            <p>
                Grohl passou as fitas das gravações para amigos, e atraiu a atenção de gravadoras. Um contrato foi assinado com a Capitol Records por
                Grohl conhecer o presidente Gary Gersh desde que ele era da gravadora do Nirvana. A fita se tornou o primeiro álbum do Foo Fighters,
                também intitulado Foo Fighters.
            </p>

            <p>
                Para divulgar o trabalho e tocar as músicas ao vivo, Grohl decidiu montar uma banda completa. Grohl chamou o guitarrista Pat Smear,
                que tocava no - The Germs e era membro da banda de turnê do Nirvana, e após tomar conhecimento sobre o fim da banda conterrânea de
                rock alternativo, - Sunny Day Real Estate, convocou o baixista Nate Mendel e o baterista William Goldsmith. A banda realizou sua
                primeira turnê ainda em 1995 abrindo concertos para - Mike Watt. O Foo Fighters tocaria cerca de 190 vezes, ao longo de 1995 e 1996.
            </p>

            <p>
                Em 23 de novembro de 2015, o Foo Fighters lançou um novo EP, intitulado "Saint Cecilia". Este EP homenageia as vitímas dos Atentados
                de 13 de novembro, na cidade de Paris, principalmente na casa de shows Bataclan, onde tocava a banda Eagles of Death Metal, com a
                qual Dave Grohl, vocalista da banda tem relação. Tal EP pode ser baixado de graça.
                <br />

                Junto com esse álbum, foi lançada uma carta, na qual Dave Grohl fala muito sobre um possível hiato ou fim da banda. Grohl anunciou
                que a banda passaria a maior parte de 2017 gravando seu nono álbum de estúdio. Em 1 de junho de 2017 a banda lança seu novo single Run.
                Em 23 de agosto é lançado o segundo single de Concrete and Gold, The Sky Is a Neighborhood.
            </p><br />

