            <p>
                Machine Head é o sexto álbum de estúdio do Deep Purple, gravado em dezembro de 1971 em Montreux, Suíça e lançado em 25 de março de
                1972 pela Purple Records .
            </p>
            <br />

            <p>
                Após sessões de gravação intermitentes entre os shows de álbuns anteriores, o Deep Purple queria dedicar um tempo dedicado para
                gravar um álbum longe do ambiente de estúdio típico que parecia mais próximo de seus shows ao vivo. Eles contrataram o
                Rolling Stones Mobile Studio para gravar, e o block reservou o Montreux Casino como local, mas durante um show de Frank Zappa
                imediatamente antes das sessões, ele foi totalmente destruído. A banda conseguiu reservar o Grand Hotel, fechado para o inverno,
                e transformou o hotel em uma sala ao vivo adequada para gravação. Esses eventos, principalmente o incêndio do cassino, serviram
                de inspiração para a música " Smoke on the Water ".
            </p>
            <br />

            <p>
                Machine Head é citado como uma grande influência no desenvolvimento inicial da música heavy metal. É o álbum de maior sucesso
                comercial do Deep Purple, liderando as paradas em vários países. O álbum alcançou o número um no Reino Unido e permaneceu no top 40
                por 20 semanas, enquanto alcançou o número sete nos Estados Unidos em agosto de 1973, permanecendo na Billboard 200 por 118 semanas.
            </p>
