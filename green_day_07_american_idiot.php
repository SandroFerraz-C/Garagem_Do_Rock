            <h1>American Idiot (2004)</h1>
            <br />

            <p>
                "American Idiot" é o sétimo álbum de estúdio do Green Day. O seu lançamento ocorreu em 20 de setembro de 2004 através da Reprise Records.
            </p>
            <br />

            <p>
                Com produção de Rob Cavallo, o disco foi gravado entre abril de 2003 e março de 2004 em estúdios localizados no estado da Califórnia. O grupo
                conquistou popularidade na década de 1990, lançando diversos álbuns bem-sucedidos. Após as vendas desapontadoras do sexto trabalho do trio,
                - Warning, de 2000, a banda entrou em um recesso antes de gravar sua próxima obra, que viria a se chamar - Cigarettes and Valentines.
                O processo de gravação foi por água abaixo quando as fitas mestre do disco foram roubadas. Ao invés de regravar o material, o conjunto tomou
                a decisão de começar do zero.
            </p>
            <br />

            <p>
                - American Idiot marcou a retomada da carreira do Green Day após um período de declínio na sua popularidade. Registrou entrada nas
                paradas de 27 países, chegando ao topo em dezenove deles, e acabou por vender dezesseis milhões de cópias mundialmente. Cinco singles
                foram lançados a partir do disco: "American Idiot", "Boulevard of Broken Dreams", "Holiday", "Wake Me Up When September Ends" e
                "Jesus of Suburbia". O álbum foi aclamado pela crítica e ganhou o Grammy de Melhor Álbum de Rock em 2005. Seu sucesso inspirou um
                musical da Broadway e uma adaptação para o cinema, ainda a ser lançada. O disco tem sido elogiado mesmo anos após seu lançamento,
                tendo sido incluído em diversas listas que compilaram os melhores trabalhos do ano e da década.
            </p>
