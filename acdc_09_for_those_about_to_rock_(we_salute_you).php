            <h1><span lang="en-US">FOR THOSE ABOUT TO ROCK (WE SALUTE YOU)</span> (<time datetime="1981">1981</time>)</h1>

            <p>- For <span lang="en-US">Those About To Rock (We Salute You)</span> é o décimo álbum da banda australiana de <span lang="en-US">Hard Rock</span> AC/DC, contendo 9 faixas. Foi lançado em <time datetime="1981">1981</time>, um ano após o bem-sucedido álbum - <a href="acdc_08_back_in_black.php" target="blank">Back In Black</a>.</p>

            <p>
            <blockquote>O nome do álbum foi inspirado num livro que Angus Young leu, entitulado <span lang="en-US"><cite>For Those About To Die, We Salute You</cite></span>, sobre os gladiadores romanos. As palavras finais dos gladiadores para o imperador, antes dos combates, era <q><span lang="lat">Ave Caesar morituri te salutant</span></q>
                (PT.: “César os que vão morrer te saúdam”; EN.: ”<span lang="en-US">Caesar, we who are about to die, salute you</span>”).</blockquote>
            O álbum foi relançado em 2003 como parte da série AC/DC <span lang="en-US">Remasters</span>.</p>

            <p>O álbum conta com clássicos da banda como "<span lang="en-US">Night of the Long Knives</span>", "<span lang="en-US">Evil Walks"</span>, "<span lang="en-US">C.O.D."</span> , "<span lang="en-US">Spellbound</span>", "<span lang="en-US">Let's Get It Up</span>" , "<span lang="en-US">I Put The Finger On You</span>"
                e a tão famosa faixa título "<span lang="en-US">For Those About to Rock (We Salute You)</span>", faixa que desde então vem sendo o encerramento de todos os shows da banda, que destaca-se por usar canhões de verdade que disparam durante a música. O nome desta faixa acabou também sendo o lema definitivo da banda.</p>

            <p>- <span lang="en-US">For These About to Rock</span> se tornou o primeiro álbum do AC/DC a atingir o #1° lugar nos EUA na parada da <span lang="en-US">Billboard</span> e permaneceu no topo por três semanas. Até o momento, nos Estados Unidos, atingiu quatro milhões de vendas. No Reino Unido, os dois singles
                do álbum, "<span lang="en-US">Let's Get It Up</span>" e "<span lang="en-US">For They About to Rock (We Salute You)</span>", chegaram aos #13º e #15º lugares, respectivamente. O álbum vendeu cerca de sete milhões de cópias em todo o mundo, tornando-se um dos álbuns AC/DC mais vendidos em
                todo o mundo. No entanto, isso marca uma grande queda nas vendas nos Estados Unidos, vendendo apenas 4 milhões como sequência do 25 milhões de - <a href="acdc_08_black_in_black.html" target="blank"><span lang="en-US">Back in Black</span></a>.</p>
