            <h1>FOR THOSE ABOUT TO ROCK (WE SALUTE YOU) (1981)</h1>

            <p>- For Those About To Rock (We Salute You) é o décimo álbum da banda australiana de hard rock AC/DC, contendo 9 faixas. Foi lançado em 1981, um ano após o bem-sucedido álbum Back In Black.</p>

            <p>O nome do álbum foi inspirado num livro que Angus Young leu, entitulado For Those About To Die, We Salute You, sobre os gladiadores romanos. As palavras finais dos gladiadores para o imperador, antes dos combates, era “Ave Caesar morituri te salutant”
                (pt: “César os que vão morrer te saúdam”, en:”Caesar, we who are about to die, salute you”). O álbum foi relançado em 2003 como parte da série AC/DC Remasters.</p>

            <p>O álbum conta com clássicos da banda como Night of the Long Knives, Evil Walks, C.O.D. , Spellbound , Let's Get It Up , "I Put The Finger On You" e a tão famosa faixa título For Those About to Rock (We Salute You), faixa que desde então vem
                sendo o encerramento de todos os shows da banda, que destaca-se por usar canhões de verdade que disparam durante a música. O nome desta faixa acabou também sendo o lema definitivo da banda.</p>

            <p>- For These About to Rock se tornou o primeiro álbum do AC/DC a atingir o #1° lugar nos EUA na parada da Billboard e permaneceu no topo por três semanas. Até o momento, nos Estados Unidos, atingiu quatro milhões de vendas. No Reino Unido, os dois singles
                do álbum, "Let's Get It Up" e "For They About to Rock (We Salute You)", chegaram aos #13º e #15º lugares, respectivamente. O álbum vendeu cerca de sete milhões de cópias em todo o mundo, tornando-se um dos álbuns AC/DC mais vendidos em
                todo o mundo. No entanto, isso marca uma grande queda nas vendas nos Estados Unidos, vendendo apenas 4 milhões como sequência do 25 milhões de - <a href="acdc_08_black_in_black.html" target="blank">Back in Black</a>.</p>
