            <h1>HIGHWAY TO HELL (1976)</h1>

            <p>- Highway to Hell é o sexto álbum de estúdio, sendo o última da fase de Bon Scott, que morreu no dia 19 de Fevereiro de 1980.</p>

            <p>Foi o primeiro álbum do AC/DC a não ser produzidor por Harry Vanda e George Young. Uma pré-produção do álbum começou em Janeiro de 1979 com músicas demos no Albert Studios em Sydney na Austrália, onde encontraram o desejado produtor, Eddie Kramer. Kramer
                foi despedido antes da faixa de single estar completa e então Robert John “Mutt” Lange foi contratado para substituí-lo. A mundaça proveu ser positiva e o álbum é considerado uns dos melhores da banda até hoje.</p>

            <p>Se tornou o primeiro álbum do AC/DC a chegar no top #100, eventualmente chegando em #17º, e isso impulsionou o AC/DC a chegar ao topo dos ícones do Hard Rock. Com “Highway To Hell” atingindo #17º lugar no mercado americano, o álbum foi o primeiro sucesso
                sólido que o AC/DC conquistou nos EUA.</p>

            <p>Talvez a canção mais famosa do álbum seja "Night Prowler", principalmente por sua associação com o serial killer Richard Ramírez. Em junho de 1985, um caso de assassinato altamente divulgado começou, envolvendo Ramírez, que foi responsável por assassinatos
                brutais em Los Angeles. Apelidado de "Night Stalker", Ramírez era fã do AC/DC, principalmente de "Night Prowler". A polícia também alegou que Ramirez estava vestindo uma camisa do AC/DC e deixou um chapéu da banda em uma das cenas do crime.
                Durante o julgamento, Ramírez costumava murmurar "Salve Satan" e exibir o pentagrama esculpido em sua palma. Isso trouxe publicidade extremamente ruim para a banda, cujos shows e álbuns enfrentaram protestos em Los Angeles. Em Behind the
                Music, do VH1, a banda afirmou que Ramírez deu à música uma conotação assassina, mas na verdade é sobre um menino que entra furtivamente no quarto da namorada à noite enquanto os pais dela estão dormindo.</p>

            <p>As palavras finais ditas por Bon Scott no álbum foram “Shazbot, na-nu na-nu” frases usadas no então popular sitcom americano, “Mork and Mindy”, pelo personagem Mork (um visitante extraterrestre interpretado por Robin Williams). A frase é falada no final
                da música “Night Prowler” e também fecha o álbum.</p>
