            <h1>Crush (2000)</h1>

            <p>
                - Crush é o sétimo álbum da banda Bon Jovi, lançado em 2000. Possui um dos maiores hits da banda,
                a música "It's My Life".
            </p>

            <p>
                O álbum marca o maior intervalo de tempo entre os álbuns de estúdio da banda, com cinco anos entre o lançamento de
                - <a href="bon_jovi_06_these_days.php" target="blank">These Days</a> (1995) e este álbum. Depois que o plano inicial de se juntar
                ao produtor Bruce Fairbairn fracassou por causa de sua morte um ano antes, Bon Jovi e Sambora contrataram Luke Ebbin para
                atualizar seu som com um punhado de loops e arranjos impressionantes.
            </p>

            <p>
                Apesar da longa pausa, o álbum teve tanto sucesso quanto seus lançamentos anteriores e ajudou a apresentar a banda a uma nova geração de
                fãs. O sucesso do álbum foi em grande parte devido ao single "It's My Life", que foi nomeado para um Grammy de Melhor Performance de
                Rock por um Duo ou Grupo com Vocal, enquanto o próprio álbum foi nomeado para Melhor Álbum de Rock.
            </p>

            <p>
                - Crush estreou no #9° kugar na Billboard 200 na edição de 1° de julho de 2000 com 115.000 cópias vendidas em contraste com seu último album,
                - <a href="bon_jovi_06_these_days.php" target="blank">These Days</a> (1995), que estreou com 73.000 unidades, permaneceu no #9° lugar por
                uma semana antes de cair para o #29° lugar e passou 51 semanas no gráfico da Billboard 200.
            </p>

            <p>
                O álbum foi certificado 2X platina pela RIAA (EUA), vendeu 2.100.000 cópias nos Estados Unidos de acordo com a Nielsen SoundScan.
                - Crush estreou como nº #1 no Reino Unidoem 10 de junho de 2000 e permaneceu no topo das paradas por uma semana antes de cair para
                a quarta posição, permanecendo na parada por trinta e nove semanas.
            </p>

            <p>Foi certificado platina pelo BPI em 1 de setembro de 2000 pela venda de 300.000 Discos. O álbum também liderou o Top 100 de álbuns europeus
                por sete semanas, passou 15 semanas no Top #10 e recebeu a certificação de platina dupla pelo IFPI Europe. O álbum foi n° #6 na parada
                de álbuns de fim de ano da Europa de 2000 e n° #7 na parada de álbuns de fim de ano em todo o mundo. O primeiro single também com um
                videoclipe, "It's My Life" foi o single nº #3 mais vendido em todo o mundo em 2000 e liderou a parada de singles europeus por 4 semanas.
            </p>

            <p>
                - Crush foi bem recebido pela maioria dos críticos. Foi o primeiro álbum do Bon Jovi a ser nomeado para um Grammy; Em uma crítica ao
                AllMusic, Steve Huey expressou a opinião de que Crush era um "disco de rock convencional solidamente elaborado e que é muito mais do que
                a maioria poderia esperar." A revista Rolling Stone deu ao álbum 3 de 5 estrelas e descreveu: "se os roqueiros de Jersey não amadureceram muito,
                pouco importa. - Crush - é o clássico Bon Jovi. "
            </p>
