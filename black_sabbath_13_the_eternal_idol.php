            <h1>The Eternal Idol (1987)</h1>

            <p>
                - The Eternal Idol é o décimo terceiro álbum do Black Sabbath. É o primeiro álbum gravado pelo vocalista Tony Martin no Black Sabbath.
            </p>

            <p>
                Ao término da turnê do álbum - <a href="black_sabbath_12_seventh_star.php" target="blank">Seventh Star</a>, o Black Sabbath
                começou a gravar o álbum - The Eternal Idol juntamente com o vocalista Ray Gillen, substituto de Glenn Hughes, o qual havia
                abandonado o grupo na última turnê devido aos seus problemas com as drogas. Nessa época, Dave Spitz e Eric Singer deixam a banda.
                E então o Black Sabbath ficava sem baixista e baterista durante algum tempo. Com esse desfalque, Ray Gillen afirma que a banda não
                iria mais progredir, e assim decide sair da banda. Como a banda precisava de um vocalista, o escolhido foi Tony Martin. O baterista
                Bev Bevan também foi convidado para voltar a banda, porém quem realmente gravou o álbum foi Bob Daisley e Singer.
            </p>

            <p>
                A banda deveria apresentar-se na Grécia e na África do Sul em um festival em Sun City, com isso Spitz volta ao grupo. Com a banda
                já em direção à África do Sul, Bevan se recusa a tocar, e assim foi substituído. Logo após ao final do show na África, as gravações
                de - The Eternal Idol foram concluídas. Porém o Black Sabbath ficava novamente sem baterista e baixista para uma turnê posterior.
                Para a turnê foram escolhidos Jo Burt e Terry Chimes (ex-The Clash).
            </p>
            <br />

