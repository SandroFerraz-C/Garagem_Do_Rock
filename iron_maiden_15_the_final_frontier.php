            <h1>The Final Frontier (2010)</h1>

            <p>
                - The Final Frontier é o décimo quinto álbum do Iron Maiden. Lançado em 16 de agosto de 2010.
            </p>

            <p>
                O disco recebeu inúmeras críticas favoráveis e chegou ao topo das paradas musicais em 28 países. É o quarto lançamento da banda a
                chegar a #1° nas paradas inglesas, após - The Number of the Beast (1982), - Seventh Son of a Seventh Son (1988) e - Fear of the Dark (1992).
                Além disso, - The Final Frontier chegou a #4ª posição nos Estados Unidos, sendo a maior posição atingida pela banda na Billboard 200.
                Para completar, a banda ganhou o prêmio Grammy na categoria Melhor Performance de Metal pela música "El Dorado", que foi lançada como single
                em 8 de junho de 2010.
            </p>

            <p>
                Em entrevista concedida à Billboard em 1 de julho de 2010, o guitarrista Dave Murray comentou que o álbum mistura "canções de rock
                rápidas e diretas com bons grooves e ainda conta com algumas faixas mais extensas e complexas", referindo-se particularmente à
                música "When the Wild Wind Blows", terceira música mais longa da banda até a data, depois de "Rime of the Ancient Mariner" (Powerslave)
                e "Sign of the Cross" - The X Factor. Janick Gers foi entrevistado pela Pittsburgh Post-Gazette em 14 de julho, e discutiu o som do
                álbum num geral: "Nós estamos levando isso a extremos. A canção que nós liberamos ["El Dorado"] não é indicativo do restante do álbum.
                Há tantas sensações e formas distintas de tocar no disco. Tomamos algumas atitudes diferentes e levamos vocês a lugares diferentes. Há
                várias músicas temáticas e algumas muito variadas". Falando ao Classic Rock em junho de 2010, Bruce Dickinson afirmou: "Esse é provavelmente
                o maior distanciamento do nosso som tradicional. Isso vem acontecendo crescentemente desde - Brave New World. Isso é ótimo, depois de
                fazer tudo isso por tanto tempo, ainda estarmos inovando. Poderíamos ficar cansados de tudo isso, mas obviamente não estamos".
            </p>
