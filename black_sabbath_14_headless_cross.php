            <h1>Headless Cross (1989)</h1>

            <p>- Headless Cross é o décimo quarto álbum de estúdio da banda inglesa de heavy metal Black Sabbath.</p>

            <p>Este álbum foi o primeiro com o baterista Cozy Powell e o segundo com o vocalista Tony Martin. O guitarrista Brian May participa do
                disco com um solo na faixa “When Death Calls”.</p>

            <p>- Headless Cross foi elogiado pela crítica e pelos fãs, sendo considerado o melhor álbum do Sabbath em anos. Com altas vendas
                (recebeu certificação de ouro em solo inglês), fizeram uma bem-sucedida tour na Inglaterra e Europa. Em 2005, o album foi classificado
                na posição 403 no livro The 500 Greatest Rock & Metal Albums of All Time da revista Rock Hard.</p>

            <p>Conceitualmente, as letras têm, de forma predominante, elementos satanista e ocultistas, sendo assim provavelmente o único álbum da história
                da banda em que todos os ideais de composição tem essa inspiração, ao invés de somente algumas canções específicas. A canção "Call of the
                Wild" seria primeiramente intitulada "Hero", mas quando Ozzy Osbourne usou esse título no seu álbum No Rest for the Wicked, Tony Iommi optou
                por mudá-lo. "Devil & Daughter" tinha originalmente o nome de "Devil's Daughter", mas foi mudado pela mesma razão."Call of the Wild" e
                "Devil & Daughter" são as únicas canções que não terminam com o som desvanecendo e com o improvisamento vocal de Tony Martin ("Nightwing",
                porém, só tem o desvanecimento da canção, sem o improvisamento vocal de Martin). De acordo com Martin, os vocais em "Nightwing" são na
                verdade vocais-guia, porque Iommi achou que soavam melhor do que as gravações posteriores.</p>
