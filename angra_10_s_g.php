            <h1>Secret Garden (2014)</h1>


            <p>
                Secret Garden é o décimo álbum de estúdio da banda brasileira de heavy metal Angra lançado em 17 de dezembro de 2014 no Japão e em 16 de janeiro de 2015 no Brasil e na Europa.
            </p>
            <br />

            <p>
                O álbum foi produzido e gravado na Suécia por Jens Bogren, com pré-produção de Roy Z, sendo o primeiro trabalho da banda com o novo
                baterista, Bruno Valverde, e também o primeiro trabalho de estúdio com o vocalista Fabio Lione. O álbum também conta com algumas
                partes cantadas pelo guitarrista Rafael Bittencourt e tem participações especiais da cantora holandesa Simone Simons (Epica) e da
                também cantora Doro Pesch, da Alemanha.
            </p>
            <br />

            <p>
                Sobre o álbum, o guitarrista Kiko Loureiro comentou em junho de 2014:
                <br /><br />

                "As mudanças musicais sempre acontecem porque nós mudamos como pessoas também. Então não apenas as mudanças de formação, então você pode
                esperar algumas mudanças, é claro, com o Fabio, uma voz diferente. Nós tentamos buscar novos caminhos para fazer música, então com certeza
                vocês ouvirão uma coisa nova e diferente, mas o núcleo e a essência do Angra estarão sempre lá de qualquer forma."
            </p>
            <br />

            <p>
                Segundo Rafael Bittencourt em entrevista ao programa Metrópolis da TV Cultura em 9 de junho de 2015, o álbum mostra a mistura
                religiosa que compõe a banda unindo em um trabalho músicas bem diferentes umas das outras. A história de um cientista que perde a
                sua amada, e suas reflexões após isto é o tema que amarra o álbum.
            </p>
            <br /><br />


            <p>Duração do album: 54 min. 01 seg.</p>
            <p>Gravadora(s): JVC/Victor Entertainment (Japão), Universal Music (Brasil) e Edel Music (União Europeia, Estados Unidos, Canadá e Rússia).</p>
            <p>Produção: Jens Bogren. </p>
