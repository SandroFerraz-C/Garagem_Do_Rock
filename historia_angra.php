            <h1>História do Angra</h1>

            <p>
                A banda Angra foi formada por Rafael Bittencourt e Andre Matos com a proposta de fundir a agressividade do heavy metal, os ritmos étnicos
                brasileiros e a sofisticação da música erudita. O nome significa "deusa do fogo e da beleza" na mitologia tupiniquim, além de significar
                uma pequena enseada ou baía usada como porto natural (como em Angra dos Reis). Além disso, também foi escolhido por se parecer com o
                adjetivo angry, que, em inglês, significa "raivoso".
            </p>

            <p>
                O quinteto ensaiou praticamente por um ano para lançar sua primeira demo tape, intitulada - Reaching Horizons em 1992. Ainda
                desconhecidos do grande público, o Angra assinou com a JVC e viajou para a Alemanha para gravar seu primeiro álbum de
                estúdio. Antes das gravações iniciarem, Marco Antunes deixa o grupo, ficando a cargo de Alex Holzwarth tocar a bateria no
                disco.
            </p>

            <p>
                No fim de 1993 foi lançado o disco de estreia, - <a href="angra_01_angels_cry.php" target="blank">Angels Cry</a>,
                que contava com a participação de Kai Hansen, Dirk Schlachter, Thomas Nack (Gamma Ray) e Sascha Paeth (Heavens Gate).
                O álbum apresentava uma mistura de heavy metal e música clássica, sonoridade que marcou o estilo da banda.
            </p>

            <p> Após o lançamento a
                banda ganhou fama no Japão, onde - Angels Cry chegou à terceira posição na parada internacional, tendo vendido 106 mil cópias, ganhando
                seu primeiro Disco de Ouro. Leitores de revistas japonesas e sul-americanas elegeram o Angra "Melhor Banda Nova" de 1993, sendo que,
                na Rock Brigade, o grupo e o álbum - Angels Cry venceu diversas categorias da votação dos leitores: "Melhor Álbum", "Melhor Vocalista",
                "Melhor Capa", "Melhor Tecladista" e "Melhor Música" "Carry On".
            </p>

            <p> O videoclipe de "Time" foi executado muitas vezes em emissoras de
                TV do Brasil e do Japão, enquanto que o de "Carry On" foi indicado para o MTV Video Music Awards.
            </p>

            <p>
                Após diversos desentendimentos com Pirani, Andre Matos, Ricardo Confessori e Luis Mariutti saíram da banda em agosto de 2000
                e em março de 2001, uma nova formação era anunciada com Aquiles Priester (bateria), Edu Falaschi (vocal) e Felipe Andreoli (baixo).
                Edu Falaschi era líder da banda Symbols. Felipe Andreoli, por sua vez, era baixista do grupo Karma, e ainda faz parte desta formação.
                Já Aquiles Priester, fundador do grupo Hangar, que sugeriu para Kiko e Rafael convidarem o tecladista Fábio Laguna (Hangar) para tocar nos
                shows.
            </p>

            <p>
                Assim, o Angra voltou às atividades no ano de 2001 com o lançamento mundial do disco - <a href="angra_05_rebirth.php" target="blank">Rebirth</a>
                no mês de outubro. O nome do álbum, que significa renascimento em português, remete à nova fase vivida pela banda a partir
                do primeiro semestre daquele ano e foi gravado no Brasil e na Alemanha pelo produtor Dennis Ward.
            </p><br />

