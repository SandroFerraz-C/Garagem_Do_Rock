<p>
De fato, o trítono (assim como a segunda menor formada por mi e fá) é um intervalo incômodo de cantar e tem potencial para desafinar, então é 
mesmo bem provável que ele não tenha sido recomendado por especialistas ao longo da história da música – tudo se resume a um problema técnico, 
sem participação do tinhoso. Pode tocar Black Sabbath à vontade: você não vai para o inferno!! </p>
    
