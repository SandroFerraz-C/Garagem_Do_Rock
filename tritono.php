    <h1>TRÍTONO, O SOM DO DIABO</h1>
    <img src="img/tritono2.jpg">

    <p>
        Você já ouviu essa história: o músico Robert Johnson vendeu sua alma para o demônio. Em troca, ganhou talento e sucesso. A negociação ocorreu
        em uma encruzilhada no interior do Mississipi – até hoje, garantem crentes e céticos, cruzamentos são um ótimo lugar para fazer comércio com o
        coisa ruim.
    </p>
    <br />

    <p>
        O mito de Johnson nasceu, na verdade, em outra encruzilhada, da cultura americana com a cultura alemã: é o mito de Fausto. Na mesma época em
        que o músico negro – pobre e filho ilegítimo – fundava um dos dos gêneros mais importantes da música popular dos EUA, o autor alemão Thomas
        Mann começava a idealizar sua obra-prima: o romance Doutor Fausto, história de um compositor que (adivinha só!) vende a alma para o tinhoso
        em troca da máxima realização artística.
    </p>
    <br />

    <p>
        Ambos são versões, uma erudita e uma popular, da história de um alquimista que viveu na Alemanha no século 15. Dá para passar mais um tempão
        falando de todas as encarnações do mito fáustico – como o 100% brasileiro Grande Sertão: Veredas, de Guimarães Rosa – mas não será tão legal
        quanto explicar o demônio na música: será que há mesmo algo de demoníaco no blues (ou em qualquer outro tipo de música)?
    </p>
    <br />

    <p>
        Corre por aí a história de que um determinado intervalo musical, chamado trítono, é o próprio som do inferno, tão dissonante que não podia
        (por um decreto assinado pelo Papa em pessoa!) aparecer nas partituras de música sacra durante a Idade Média.
    </p>
    <br />

    <p>
        Um intervalo musical é, em bom português, uma combinação de duas notas diferentes. Você pode testar vários deles mesmo se não souber tocar
        nenhum instrumento: vá a um piano de estação de metrô, escolha duas teclas aleatórias (não muito distantes entre si) e toque ambas ao mesmo
        tempo. Algumas dessas combinações vão soar super bem. Outras, nem tanto.
    </p>
    <br />

    <p>
        O trítono é o intervalo que você ouve na abertura da música Black Sabbath – que está no álbum Black Sabbath. Quem
        achar o metal em questão heavy-Metal demais, também pode ouvir o trítono no comecinho de Purple Haze, de Jimi Hendrix; Ou na abertura dos Simpsons
        (ouça as notas mais graves do piano no segundo 0:16). O bichinho aparece até em Garota de Ipanema – mas aí ele dá as caras em uma substituição
        de acordes que não é tão óbvia, então vamos passar batido.
    </p>
    <br />

    <p>
        São todas músicas muito diferentes entre si – e com exceção da do Black Sabbath, nenhuma soa particularmente demoníaca. Isso acontece porque,
        na música, não existem intervalos do céu e do inferno, só intervalos mais consonantes e intervalos mais dissonantes. Os primeiros são
        simpáticos e bonitinhos. Os segundos são tensos, criam desequilíbrio. E
        ambos são úteis em qualquer música.
    </p>
    <br />

    <p>
        A seguir dois perfeitos e clássicos exemplos de músicas onde o trítono se faz presente. A música Black Sabbath da banda Black Sabbath
        (o mais famoso trítono do Heavy Metal) e o 1º movimento da 5ª sinfonia de Beethoven.
    </p>
