            <h1>Killing Machine (1978)</h1>
            <br />

            <p>
                - Killing Machine é o quinto álbum de estúdio do Judas Priest, lançado em novembro de 1978.
            </p>
            <br />

            <p>
                O título original trouxe várias discussões com a Columbia Records, distribuidora do álbum nos Estados Unidos, que segundo eles,
                o nome poderia ter "implicações assassinas" e era agressivo demais para ser colocado à venda. Além disso, anunciaram que várias
                lojas de departamento, incluído a Kmart, lhes disseram que se não mudassem o título, não o colocariam em sua prateleiras.
                <br />
                Por esta razão e para evitar problemas com grandes compradores no mercado estadunidense, o selo lhe sugeriu mudar o nome do
                disco, que passou a se chamar - Hell Bent for Leather e foi finalmente colocado à venda, não só nos Estados Unidos mas também
                no Canadá e outros mercados menores. Esta edição contou como faixa exclusiva uma versão de "The Green Manalishi (With The Two
                Pronged Crown), original de Fleetwood Mac que havia sido escrita em 1970 pelo guitarrista Peter Green algumas semanas antes
                de sair da banda.
            </p>
            <br />

            <p>
                Foi lançado em 09 de outubro de 1978 no Reino Unido através da CBS Records, alcançando o #32° lugar na lista UK Albums Chart em
                no mesmo ano de lançamento. Enquanto na América do Norte foi lançando em 1979 pela Columbia Records, como mencionado, sob o título
                - Hell Bent For Leather, e alcançou 0 #128° lugar na na Billboard 200. Além disso, em 1989 foi certificado com dico de ouro pela RIAA
                após vender mais de 500 mil cópias neste último país.
            </p>
            <br />

            <p>
                Para dar ênfase a nova imagem da banda ( Roupas de couro e tachas ), o artista Roslav Szaybo colocou um capacete de motocicleta de
                couro sobre a cabeça de um manequim. Ele também adicionou óculos quebrado e coberto de sangue. Segundo ele, isso dava clara
                referências a aparência da banda, e quando a Columbia decidiu mudar o título para - Hell Bent For Leather, a imagem parecia se
                encaixar mais. Além disso, retomou o logotipo que ele criou para a capa da - Stained Class, adicionando uma ponto sublinhado a
                mais sob a palavra Judas e uma ponto a menos para a palavra Priest. Estas mudanças são as que definiu o logotipo característico
                e é o que mais tem sido usada desde então.
            </p>
