            <h1>The Long Run (1979)</h1>
            <br />

            <p>
                The Long Run é o sexto álbum da banda Eagles.
            </p>
            <br />

            <p>
                Este foi o primeiro álbum dos Eagles a apresentar Timothy B. Schmit, que substituiu o membro fundador Randy Meisner, e o último
                álbum de estúdio completo a apresentar Don Felder antes de seu término da banda em 2001.
            </p>
            <br />

            <p>
                Este foi o último álbum de estúdio da banda com a gravadora Asylum Records. Também acabou sendo seu último álbum de estúdio antes
                dos Eagles se separaram em 1980, até Long Road Out of Eden, em 2007, quando a banda se reformou em 1994.
            </p>
            <br />

            <p>
                Três singles foram lançados do álbum, "Heartache Tonight", "The Long Run" e "I Can't Tell You Why". "Heartache Tonight" alcançou
                o primeiro lugar na parada de singles e ganhou um Grammy. O álbum foi certificado 7X Platinum pela RIAA e vendeu mais de oito
                milhões de cópias nos Estados Unidos.
            </p>
            <br />

            <p>
                Revendo o álbum retrospectivamente no AllMusic, o crítico William Ruhlmann escreveu que o álbum foi uma "grande decepção, embora
                tenha vendido vários milhões de cópias e lançado três singles de sucesso", acrescentando que o álbum "supostamente foi planejado como
                um álbum duplo antes de ser truncado para um único disco. Se esses fossem os guardiões, como seriam os rejeitados?".
            </p>
