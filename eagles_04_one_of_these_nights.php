            <h1>One of These Nights (1975)</h1>

            <p>- One of These Nights é o quarto álbum da banda Eagles.</p>

            <p>O álbum se tornaria o primeiro álbum dos Eagles na parada de álbuns da Billboard em julho daquele ano, e rendeu três singles no
                Top 10, "One of These Nights", "Lyin 'Eyes" e "Take It to the Limit". Sua música título é o segundo single número #1 do grupo na
                Billboard Hot 100. O álbum vendeu quatro milhões de cópias e foi indicado para o Grammy Álbum do Ano. Um single do álbum,
                "Lyin 'Eyes", também foi indicado para Gravação do Ano, Melhor Performance Pop de Duo ou Grupo com Vocais.</p>

            <p>- One of These Nights é o último álbum dos Eagles com o guitarrista Bernie Leadon, que deixou a banda após a turnê One of These Nights
                e foi substituído por Joe Walsh. A sétima faixa, "Visions", é a única música dos Eagles em que o guitarrista Don Felder cantou os
                vocais principais, apesar de seu desejo de escrever e cantar mais músicas. O álbum foi o avanço comercial da banda, transformando-os em
                superstars internacionais. Eles fizeram uma turnê mundial para promover o álbum.</p>

            <p>Os Eagles começaram a trabalhar em seu quarto álbum no final de 1974. Glenn Frey e Don Henley escreveram quatro das nove canções
                sozinhos e também colaboraram com outros membros da banda em três outras canções. Muitas das canções foram escritas enquanto Frey e
                Henley compartilhavam uma casa em Beverly Hills , incluindo "One of These Nights", "Lyin 'Eyes", "Take It to the Limit" e
                "After the Thrill Is Gone". Em uma entrevista com Cameron Crowe, Henley brincou que era o "período do country-rock satânico" porque
                "era uma época sombria, tanto política quanto musicalmente" na América, referindo-se à turbulência em Washington e à música disco
                começando a decolar. Ele acrescentou: "Nós pensamos:" Bem, como podemos escrever algo com esse sabor, com esse tipo de batida, e
                ainda ter as guitarras perigosas? ” Queríamos capturar o espírito da época.</p>
