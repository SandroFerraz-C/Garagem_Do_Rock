            <h1>Just Push Play (2001)</h1>

            <p>- Just Push Play é o décimo terceiro álbum do Aerosmith, lançado no dia 5 de março de 2001 pela Columbia Records.</p>

            <p> álbum foi produzido pela banda em conjunto com Marti Frederiksen e Mark Hudson. O primeiro single lançado, "Jaded", tornou-se Top
                10 nos Estados Unidos, levando o álbum ao disco de platina.</p>

            <p>Em 2010, o guitarrista Joe Perry revelou seu desgosto pelo álbum: "Acho que não fazemos um álbum decente há anos. - Just Push Play é
                o que menos gosto. Quando gravamos, nunca houve um ponto em que todos os cinco membros estivessem na sala ao mesmo tempo e a maior
                força do Aerosmith é tocar juntos. Foi uma experiência de aprendizado para mim: me mostrou como não fazer um disco do Aerosmith".</p>

            <p>- A critica não gostou muito do álbum, a ponto de considerá-lo uma prova de que o Aerosmith deveria parar de fazer novas músicas.
                Chris Willman da Entertainment Weekly chamou o álbum de "Bom, mas não ótimo". NME disse que a maior parte do álbum não é nova,
                mas o álbum foi o primeiro álbum do Aerosmith a apresentar rap metal com canções como "Just Push Play" e "Outta Your Head".
                David Fricke da Rolling Stone disse que Just Push Play foi o mais próximo que o Aerosmith chegou de um "ótimo álbum" desde Rocks,
                apesar dos "pontos fracos" das baladas poderosas do álbum. Robert Christgau escolheu o primeiro single do álbum, "Jaded", como
                um corte de escolha.</p>
