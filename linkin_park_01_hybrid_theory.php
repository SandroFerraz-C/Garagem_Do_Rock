            <h1>Hybrid Theory (2000)</h1>
            <br />

            <p>
                - Hybrid Theory é o álbum de estreia da banda americana de rock Linkin Park.
            </p>
            <br />

            <p>
                O álbum é o mais vendido do grupo até o momento, com cerca de mais de 30 milhões de cópias comercializadas pelo mundo, sendo disco
                de diamante nos Estados Unidos (vendendo mais de 12 milhões de cópias somente neste país). É o álbum de estreia mais vendido desde
                <a href="guns_n_roses_01_appetite_for_destruction.php" target="blank"> - Appetite for DestructionS</a> (1987) do
                <a href="guns_n_roses.php" target="blank"> Guns N' Roses</a>, o álbum de rock mais vendido do século XXI e um dos álbuns mais
                vendidos de todos os tempos. Também está na lista dos 200 álbuns definitivos no Rock and Roll Hall of Fame.
            </p>
            <br />

            <p>
                Entre 1998 e 1999, o título do álbum era originalmente o nome da banda (anteriormente chamada Xero). No começo de 2000, por causa
                de uma marca registrada com os produtores galeses de música eletrônica Hybrid, eles mudaram o nome para "Linkin Park". O vocalista
                Chester Bennington passava dirigindo perto de um parque chamado Lincoln Park todos os dias após gravar, e sugeriu o nome.
                - Hybrid Theory foi mantido como nome do álbum.
            </p>
            <br />

            <p>
                Linkin Park gravou este álbum sem seu baixista original, Dave Farrell, que teve outros compromissos na época. As partes do baixo
                foram, na maioria das músicas, gravadas pelo guitarrista Brad Delson, mas a banda também contratou dois baixistas para as
                sessões. Ian Hornbeck tocou baixo em três músicas, mais notavelmente em "Papercut", enquanto Scott Koziol toca baixo na qual
                se tornou o primeiro single americano da banda, "One Step Closer".
            </p>
            <br />

            <p>
                Com o - Hybrid Theory sendo o primeiro álbum do Linkin Park, Mike Shinoda, que havia trabalhado como designer gráfico antes de
                se tornar um músico profissional, afirmou que a banda havia analisado os livros para se inspirar em se apresentar pela primeira vez. O
                resultado foi um soldado alado que Shinoda se ilustrou. De acordo com Chester Bennington, a ideia do soldado com asas de libélula era
                descrever a mistura de elementos musicais pesados e suaves com o uso dos olhares cansados do soldado e os fracos toques das asas.
                O estilo de arte foi amplamente influenciado por grafite de estêncil, incluindo obras iniciais de Banksy. A capa também apresenta letras
                reviradas das músicas do álbum no fundo, embora a letra de "One Step Closer" seja a mais proeminente. Uma versão diferente do soldado
                pode ser vista em alguns dos singles do álbum.
            </p>
            <br /><br />

            <p>Duração do album: 37 min. 45 seg.</p>
