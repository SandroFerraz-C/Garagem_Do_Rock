                    <br />
                    <p>- Em outubro de 1979 a banda faz um "showcase" (show de demonstração para chamar a atenção das gravadoras), porém,
                        como nenhuma se interessou, em novembro a banda criou um selo próprio, chamado "Rock Hard Records", e transformam a
                        fita demo gravadas por 200 libras num disco independente. Os caras pegam as fitinhas K7 que tinham
                        (já que o Spaceward Studios tinha apagado as fitas master) e copiam três músicas para o EPzinho ("Prowler" de um lado e
                        "Iron Maiden" e "Invasion" do outro), intitulando-o "The Soundhouse Tapes" em homenagem ao pub. Foram prensadas apenas cinco mil
                        cópias do disco, vendidas quase que imediatamente nos shows e via correio. Aí então a EMI se interessa, e a banda assina com a
                        super poderosa gravadora em 28 de dezembro, quase um mês após o lançamento do EP, que como nunca mais foi reeditado, é hoje em
                        dia o item mais cobiçado (e pirateado) da banda.</p>
