            <h1>Hotel California (1976)</h1>
            <br />

            <p>
                Hotel California é o quinto álbum da banda Eagles.
            </p>
            <br />

            <p>
                O álbum foi gravado por Bill Szymczyk nos estúdios Criteria e Record Plant entre março e outubro de 1976, e então lançado no Asylum
                em dezembro. Foi o primeiro álbum com o guitarrista Joe Walsh, que substituiu o membro fundador Bernie Leadon, e é o último álbum com
                o baixista Randy Meisner. A capa é uma fotografia do Beverly Hills Hotel, de David Alexander.
            </p>
            <br />

            <p>
                O Hotel California liderou a parada de LPs e fitas da Billboard nos EUA. No 20º Grammy Awards, os Eagles ganharam um Grammy por
                "Hotel California", e por " New Kid in Town ". O álbum foi indicado para Álbum do Ano, mas perdeu para Fleetwood Mac 's Rumors.
                Três singles foram lançados do álbum, com dois no topo da Billboard Hot 100 , "New Kid in Town" e "Hotel California", enquanto " Life in the Fast Lane " alcançou a 11ª posição.
            </p>
            <br />

            <p>
                Uma edição especial do 40º aniversário do Hotel California foi lançada em novembro de 2017. Hotel California é um dos álbuns mais
                vendidos de todos os tempos. Foi certificado como 26 × Platina nos Estados Unidos e vendeu mais de 32 milhões de cópias em todo o
                mundo, tornando-o o álbum mais vendido da banda depois de They Greatest Hits (1971–1975). Foi classificado como um dos melhores
                álbuns de todos os tempos. Em 2003 e 2012, foi classificada como número #37 na Rolling Stone's lista de 'Os 500 Maiores Álbuns de
                Todos os Tempos'.
            </p>
