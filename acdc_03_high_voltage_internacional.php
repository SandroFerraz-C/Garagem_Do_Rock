            <p>Originalmente lançado na Austrália em <time datetime="1976-04-30">30 de abril de 1976</time> pela Atlantic Records e nos EUA. em <time datetime="1976-03-14">14 de maio de 1976</time> pela ATCO Records , esta edição de - <span lang="en-US">High Voltage</span>
                provou ser popular, vendendo três milhões de unidades apenas nos EUA.
                No entanto, inicialmente o álbum foi criticado por alguns críticos após seu lançamento, incluindo uma crítica da revista <a href="https://www.rollingstone.com/music/music-album-reviews/high-voltage-186715" target="blank"><span lang="en-US">Rolling Stone</span></a>, que
