            <p>Em dezembro de <time datetime="1975">1975</time>, o chefe da Atlantic Records no Reino Unido, Phil Carson, assinou com o AC/DC um acordo mundial. Os dois primeiros álbuns do grupo, <a href="acdc_01_high_voltage.php" target="blank"><span lang="en-US">High Voltage</span></a>
                e <a href="acdc_02_tnt.php" target="blank">TNT</a>, foram sucessos em sua terra natal Austrália - o single "<span lang="en-US">It's a Long Way to the Top (If You Wanna Rock 'n' Roll)</span>" alcançou a posição #5 - E agora planos foram feitos para a banda fazer uma turnê pela Inglaterra em
                <time datetime="1976">1976</time>. O grupo já havia gravado seu próximo <span lang="en-US">single</span> <a href="https://open.spotify.com/track/5UKeFkVeUfvP6S43I8s9mt?si=ywkKWJcUTR2nE65mXYAWfQ" target="blank"><span lang="en-US">Jailbreak</span></a> e já havia começado a gravar seu terceiro LP
                <a href="acdc_04_dirty_deeds_done_dirt_cheap.php" target="blank"><span lang="en-US">Dirty Deeds Done Dirt Cheap</span></a> quando, em <time datetime="1976-04">abril 1976</time>, eles voaram para a sua primeira turnê britânica. O lançamento internacional incluiu apenas duas faixas do lançamento australiano de
                - <span lang="en-US">High Voltage</span> - "<span lang="en-US">She Got Balls</span>" e "<span lang="en-US">Little Lover</span>" - com o resto das músicas retiradas do álbum - <a href="acdc_02_tnt.php" target="blank">TNT</a>. A banda chegou ao Reino Unido em meio ao movimento <span lang="en-US">Punk</span>,
                liderada por bandas como <span lang="en-US">Sex Pistols</span> e <span lang="en-US">Damned</span>. AC/DC nunca foi realmente parte do movimento <span lang="en-US">Punk</span>, mas foi erroneamente identificada como tal por alguns observadores.
            </p>
