            <h1>High Voltage "Versão Internacional" (1979)</h1>

            <p> - High Voltage foi o primeiro lançamento internacional do AC/DC.
                <br /> Ele contém faixas de seus dois primeiros álbuns anteriores lançados apenas na Austrália: <a href="acdc_01_high_voltage.php" target="blank">High Voltage</a> e <a href="acdc_02_tnt.php" target="blank">TNT</a> (ambos de 1975).
            </p>

            <p>Originalmente lançado na Austrália em 30 de abril de 1976 pela Atlantic Records e nos EUA. em 14 de maio de 1976 pela ATCO Records , esta edição de - High Voltage provou ser popular, vendendo três milhões de unidades apenas nos EUA. No entanto, inicialmente
                o álbum foi criticado por alguns críticos após seu lançamento, incluindo uma crítica da revista <a href="https://www.rollingstone.com/music/music-album-reviews/high-voltage-186715" target="blank">Rolling Stone</a>, que
                o chamou de "Ponto baixo de todos os tempos" para o gênero hard rock. Foi relançado em 2003 como parte da série AC/DC Remasters.</p>

            <p>Em dezembro de 1975, o chefe da Atlantic Records no Reino Unido, Phil Carson, assinou com o AC/DC um acordo mundial. Os dois primeiros álbuns do grupo, <a href="acdc_01_high_voltage.php" target="blank">High Voltage</a> e <a href="acdc_02_tnt.php" target="blank">TNT</a>, foram sucessos em sua terra natal Austrália - o single "It's a Long Way to the Top (If You Wanna Rock 'n' Roll)" alcançou a posição #5 - E agora planos foram feitos para a banda fazer uma turnê pela Inglaterra em
                1976. O grupo já havia gravado seu próximo single
                <a href="https://open.spotify.com/track/5UKeFkVeUfvP6S43I8s9mt?si=ywkKWJcUTR2nE65mXYAWfQ" target="blank">Jailbreak</a> e já havia começado a gravar seu terceiro LP <a href="acdc_04_dirty_deeds_done_dirt_cheap.php" target="blank">Dirty Deeds Done Dirt Cheap</a> quando, em abril 1976, eles voaram para a sua primeira turnê britânica. O lançamento internacional incluiu apenas duas faixas do lançamento australiano de - High Voltage - "She Got Balls" e "Little Lover" - com o resto das músicas retiradas
                do álbum - TNT. A banda chegou ao Reino Unido em meio ao movimento punk, liderada por bandas como Sex Pistols e Damned. AC/DC nunca foi realmente parte do movimento punk, mas foi erroneamente identificada como tal por alguns observadores.
            </p>
