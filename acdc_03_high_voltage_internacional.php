            <p> - <span lang="en-US">High Voltage</span> foi o primeiro lançamento internacional do AC/DC.
                <br /> Ele contém faixas de seus dois primeiros álbuns anteriores lançados apenas na Austrália: <a href="acdc_01_high_voltage.php" target="blank">High Voltage</a> e <a href="acdc_02_tnt.php" target="blank">TNT</a> (ambos de <time datetime="1975">1975</time>).
