            <p>
                Depois do fim do grupo Wicked Lester, Paul Stanley e Gene Simmons decidiram criar uma nova banda para ser um supergrupo. Para isso,
                colocaram anúncios em jornais e revistas para achar novos músicos. O baterista Peter Criss respondeu a um destes anúncios para
                fazer o teste pra banda. Como o rapaz estava bem vestido, eles lhe perguntaram se ele iria fantasiado de mulher a um show, e com
                uma resposta afirmativa o aceitaram na banda, mas ainda era preciso um guitarrista solo na banda, já que Stanley não sabia solar.
                No dia do teste, apareceu um rapaz que chegou cortando fila, vestido de forma estranha, com um tênis de cada cor e calça rasgada.
                Simmons pensou que se tratava de um mendigo, à primeira vista, mas estava segurando uma guitarra. Alertaram-no que ele não podia
                cortar fila, então foi esperar sua vez. Quando o chamaram para mostrar suas habilidades, impressionou a todos e assegurou um lugar
                na banda. Seu nome era Paul Daniel Frehley, mas acabou virando Ace Frehley pois já havia um Paul na banda.
            </p>

            <p>
                Faltava o nome para a banda e queriam um nome simples, fácil de ser lembrando, e acabaram se inspirando num concurso de beijo
                de Nova Iorque, daí veio KISS. Eles esconderam por anos a fio a verdadeira identidade. Para definir o figurino da banda,
                mesclaram elementos de super-heróis em quadrinhos com personagens do teatro japonês. Usando botas com saltos enormes,
                tornaram-se então: "The Starchild" (Stanley), "The Demon" (Simmons), "Space Man" (Frehley) e "The Catman" (Criss). Frehley
                que gostava de artes, pintou o primeiro logo da banda.
            </p>

            <p>
                Os primeiros concertos, com os membros já maquiados e trajados a seu modo, ocorreram no "New York Coventry Club", e os cachês
                corresponderam a 35 dólares por noite. Em 1973, ainda tocando composições próprias pelas noites de Nova Iorque, foram
                descobertos por Bill Aucoin e no mesmo ano assinaram contrato com a recém-inaugurada Casablanca Records.
            </p>

            <p>
                O Kiss usou as maquiagens entre 1973 e 1983 e somente voltou a usar na Reunion Tour em 1996. Após a primeira saída de Peter Criss
                e Ace Frehley, na década de 1980, os novos membros (Eric Carr e Vinnie Vincent) usaram suas próprias maquiagens. Porém, quando a
                banda retornou com a Reunion Tour, Peter Criss e Ace Frehley voltaram com suas maquiagens originais e, mesmo após nova saída de
                ambos, os novos membros Eric Singer e Tommy Thayer deram continuidade as maquiagens da formação original.
            </p>

