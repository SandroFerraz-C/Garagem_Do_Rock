            <h1>Led Zeppelin III (1970)</h1>
            <br />

            <p>
                - Led Zeppelin III é o terceiro álbum do Led Zeppelin. Foi gravado entre janeiro e agosto de 1970 e lançado em 5 de outubro pela
                Atlantic Records.
            </p>
            <br />

            <p>
                Foi composto em grande parte em uma casa remota no País de Gales conhecido como Bron-Yr-Aur, este trabalho representa um
                amadurecimento da música da banda para uma maior ênfase na música folk e sons acústicos. Isso surpreendeu os fãs e críticos, e
                logo após seu lançamento o álbum recebeu críticas bastante indiferentes.
            </p>
            <br />

            <p>
                Embora não seja um dos maiores vendedores do catálogos do Led Zeppelin, Led Zeppelin III hoje é geralmente elogiado e reconhecido
                por ter representando um importante marco em sua história. Apesar de músicas acústicas serem os destaques em seus antecessores,
                é este o álbum que é amplamente reconhecido por mostrar que o Led Zeppelin era mais do que apenas uma banda de rock convencional,
                e que eles poderiam se ramificar em territórios musicais mais vastos.
            </p>
            <br />

            <p>
                Muitas das canções no álbum foram concebidas em meados de 1970, Bron-Yr-Aur, uma casa de campo do século 18 em Gwynedd, País de
                Gales, numa colina com vista para o Vale do Dyfi, três quilômetros ao norte da cidade Machynlleth mercado. Lá, Jimmy Page e Robert
                Plant passaram algum tempo depois de uma turnê exaustiva da América do Norte para tocar e compor novas músicas. O local não tinha
                eletricidade, o que incentivou uma mudança de direção musical da banda para uma ênfase em arranjos acústicos de Page.
            </p>
            <br />

            <p>
                - Led Zeppelin III foi um dos álbuns mais aguardados de 1970, e encomendas antecipadas nos Estados Unidos sozinhas chegaram perto
                da marca de um milhão. Seu lançamento foi construído por um anúncio de uma página inteira retirada pela revista Melody Maker no
                final de Setembro, que disse simplesmente: "Obrigado por nos fazer a banda número um do mundo".
            </p>
            <br /><br />

            <p>Duração do album: 43 min. 04 seg.</p>
            <p>Gravadora(s): Atlantic Records.</p>
            <p>Produção: Jimmy Page.</p>
