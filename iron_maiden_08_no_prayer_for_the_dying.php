            <h1>No Prayer for the Dying (1990)</h1>
            <br />

            <p>
                - No Prayer for the Dying é o oitavo álbum do Iron Maiden, lançado em 1990.
            </p>
            <br />

            <p>
                Depois de dois álbuns tendo como característica principal o uso de teclados e sintetizadores ( - Somewhere in Time e - Seventh Son
                of a Seventh Son), a intenção neste disco era voltar aos primórdios da banda. No entanto, o resultado acabou não agradando os fãs e a
                mudança na sonoridade foi um dos fatores motivadores da saída do guitarrista Adrian Smith, que foi substituído por Janick Gers.
            </p>
            <br />

            <p>
                Teve como principal hit "Bring Your Daughter... to the Slaughter", composta por Bruce Dickinson, que ficou em primeiro lugar nas
                paradas britânicas, um feito inédito de um single para o Iron Maiden. A música causou muita polêmica e grupos religiosos reforçavam
                a acusação de que o Iron Maiden pertencia a cultos satânicos, fazendo com que a igreja católica do Chile proibisse a banda de se apresentar
                nesse país.
            </p>
            <br />

            <p>
                Existem duas versões da capa. A versão original de 1990 tem Eddie saindo de seu túmulo e agarrando um coveiro ( Que seria o
                empresário da banda, Rod Smallwood ) pelo pescoço. No entanto, Smallwood não gostou da figura e pediu ao artista Derek Riggs para
                removê-lo da capa para o relançamento de 1998. Além disso, uma inscrição foi adicionada à placa na tumba, que Riggs havia
                inicialmente deixado em branco para permitir que a banda adicionasse suas próprias palavras, e lê "Depois da luz do dia, a
                noite da dor, que não está morta, que pode surgir novamente." O LP de fotos mostra Eddie disparando uma arma feita de quatro
                metralhadoras (uma referência à faixa de abertura do álbum, "Tailgunner"). Possui a capa original no lado dois.
            </p>
