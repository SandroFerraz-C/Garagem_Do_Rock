            <h1>Sabotage (1975)</h1>
            <br />

            <p>
                Sabotage é o sexto álbum de estúdio da banda inglesa de heavy metal Black Sabbath.
            </p>
            <br />

            <p>
                É um álbum bem variado, misturando o som original da banda ainda mais ao rock progressivo, uma das músicas em que a influência
                progressiva aparece claramente é "Megalomania", o álbum também passa por alguns toques de pop rock em "Am I Going Insane (Radio)",
                e até mesmo cantos gregorianos, em "Supertzar".
            </p>
            <br />

            <p>
                Apesar de ser variado, o álbum ainda mostra o lado pesado da banda em músicas como "Hole in the Sky" e "Symptom of the Universe".
            </p>
            <br />

            <p>
                O título Sabotage foi escolhido pois na época a banda estava sendo processada pela sua antiga administração e os membros se sentiram
                como se estivessem sendo "sabotados o tempo todo e sendo socados de todos os lados", segundo Iommi. Ele credita esses problemas
                judiciais ao som raivoso e pesado do álbum
            </p>
            <br />

            <p>
                Sabotage foi lançado em 28 de julho de 1975 e atingiu a posição n° 7 nas paradas britânicas e n°28 nas americanas. Foi premiado com
                Disco de Prata (60 mil unidades vendidas) no Reino Unido pela BPI, a 1° de dezembro de 1975. Nos Estados Unidos, recebeu o Disco de Ouro
                em 16 de junho de 1997, mas foi o primeiro álbum do grupo a não alcançar a marca de platina no país.[12] Pela segunda vez, um álbum do
                Black Sabbath inicialmente recebeu resenhas favoráveis, tendo a Rolling Stone afirmado: "Sabotage não só é o melhor álbum do Black Sabbath
                desde Paranoid, mas pode também ser o melhor de todos seus discos".
