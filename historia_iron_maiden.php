                Pioneiros da nova onda do heavy metal britânico, o Iron Maiden alcançou o sucesso inicial no início dos anos 1980. Após várias
                mudanças na formação, a banda lançou uma série de álbuns de platina e ouro no Reino Unido e nos EUA, incluindo - The Number of the Beast,
                de 1982, - Piece of Mind de 1983, - Powerslave de 1984 , lançamento ao vivo do - Live After Death de 1985, o - Somewhere In Time de 1986 e
                o - Seventh Son of a Seventh Son de 1988. Desde o retorno do vocalista
