            <p>
                Na véspera de Ano Novo de 1978, o Iron Maiden gravou uma demo, composta por quatro canções, no Spaceward Studios em Cambridge.
                Na esperança de que a gravação os ajudasse a garantir mais shows, a banda apresentou uma cópia para Neal Kay, então gerenciando
                um clube de heavy metal chamado "Bandwagon Heavy Metal Soundhouse", localizado em Kingsbury Circle, no noroeste de Londres.
                Ao ouvir a fita, Kay começou a tocar a demo regularmente no Bandwagon, e uma das canções, "Prowler", finalmente foi para o #1° lugar
                nas paradas Soundhouse, que eram publicadas semanalmente na revista Sounds. Uma cópia também foi adquirida por Rod Smallwood, que
                logo se tornou o empresário da banda, e com o aumento da popularidade do Iron Maiden, eles lançaram a demo em sua própria gravadora
                como - The Soundhouse Tapes, em homenagem ao clube. Apresentando apenas três faixas todas as cinco mil cópias foram vendidas em
                semanas.
            </p>

            <p>
                Em dezembro de 1979, a banda garantiu um grande contrato com a gravadora EMI, e pediu ao amigo de infância de Dave Murray,
                Adrian Smith do - Urchin, para se juntar ao grupo como seu segundo guitarrista. Devido ao seu compromisso com o - Urchin, Smith
                recusou e Dennis Stratton foi contratado em seu lugar. Pouco depois, Doug Sampson saiu devido a problemas de saúde e foi substituído
                pelo ex- baterista do Samson Clive Burr por sugestão da Stratton em 1979.
            </p>

            <p>
                O Iron Maiden lançou seu 1° álbum de estudio "Iron Maiden" em 1980, que estreou na #4ª posição na UK Albums Chart. Além da
                faixa-título (uma versão ao vivo da qual seria um dos primeiros videoclipes exibidos na MTV ), o álbum inclui outros favoritos
                iniciais, como " Running Free ", "Transylvania", "Phantom of the Opera "e" Sanctuary "- que não estava no lançamento original no
                Reino Unido, mas apareceu na versão americana e remasterizações subsequentes. A banda embarcou em uma turnê pelo Reino Unido,
                antes de abrir para o - Kiss e - Judas Priest. O Iron Maiden também apareceu, com grande aclamação, no Reading Festival de 1980.
                Eles ficaram em #2° lugar no ranking no sábado, com OVNI como atração principal. Após a turnê do Kiss, Dennis Stratton foi demitido
                da banda como resultado de diferenças criativas e pessoais, e foi substituído por Adrian Smith em outubro de 1980.
            </p>

            <p>
                O cantor Bruce Dickinson tem um estilo vocal operístico, inspirado por Arthur Brown, Peter Hammill, Ian Anderson e Ian Gillan, e é
                frequentemente considerado um dos melhores vocalistas de heavy metal de todos os tempos. Embora Nicko McBrain tenha recebido apenas um
                crédito de escrita, no álbum - Dance of Death, Harris frequentemente depende dele enquanto desenvolve canções. Adrian Smith comentou:
                "Steve adora tocar com ele. [Eles] costumavam trabalhar por horas examinando esses padrões de baixo e bateria".
            </p>

            <p>
                Ao longo de sua carreira, o estilo da banda permaneceu praticamente inalterado, apesar da adição de sintetizadores de guitarra em
                - Somewhere in Time de 1986, teclados em - Seventh Son of a Seventh Son. Nos últimos anos, no entanto, a banda começou a usar elementos
                mais progressivos em suas canções, que Steve Harris descreve como não progressivos "no sentido moderno no estilo dos anos 70".
                De acordo com Harris, - Seventh Son of a Seventh Son foi o primeiro álbum da banda que foi "mais progressivo", enquanto eles
                só voltariam a esse estilo a partir do - The X Factor de 1995, que ele afirma ser "como uma extensão de - Seventh Son ..., no
                sentido do elemento progressivo."
                <br />
                O desenvolvimento contrasta com o material anterior da banda, que o AllMusic afirma ter sido "claramente derivado de elementos
                do punk rock", embora Harris negue firmemente.
            </p>
