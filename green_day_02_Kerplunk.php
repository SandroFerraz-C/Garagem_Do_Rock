                shows ao vivo de sucesso, as grandes gravadoras perceberam a popularidade fenomenal de - Kerplunk!. Como resultado, muitas gravadoras
                se aproximaram da banda. O - Green Day percebeu que havia superado sua capacidade de distribuição recorde com a Lookout! e finalmente
                assinou com a Reprise Records. Com a Reprise, o - Green Day gravou e lançou seu próximo álbum, - Dookie (1994). - Kerplunk inclui
                oficialmente apenas 12 faixas, mas as versões lançadas em CD e a fita cassete também incluem as 4 faixas do EP - Sweet Children.
