            <h1>Sin After Sin (1977)</h1>
            <br />

            <p>
                - Sin After Sin é o terceiro álbum de estúdio da banda Inglesa de heavy metal Judas Priest, lançado em 8 de abril 1977 pela
                Columbia Records; Porém, pode ser considerado como o primeiro também, já que o grupo perdeu os direitos sobre os 2 outros álbuns
                ( - Rocka Rolla e - Sad Wings of Destiny)
            </p>
            <br />

            <p>
                Depois de lançar seus dois primeiros álbuns pelo pequeno selo Gull, o Judas Priest ficou insatisfeito com o que considerou uma falta
                de apoio financeiro de sua gravadora. Seu álbum anterior, - Sad Wings of Destiny, chamou a atenção da CBS Records, e com a ajuda do novo
                empresário David Hemmings, a banda assinou com a CBS e recebeu um orçamento de £ 60.000 para o álbum seguinte, que deveria ter como título
                a frase ( Sin After Sin ) "pecado após pecado" da letra da música "Genocide" do álbum - Sad Wings. A mudança para a CBS exigiu a quebra do
                contrato com Gull, e uma vez que a poeira legal assentou, a banda perdeu os direitos daqueles dois primeiros álbuns e todas as gravações
                relacionadas a Gull.
            </p>
            <br />

            <p>
                As sessões de Ensaios para o - Sin After Sin aconteceram no Pinewood Studios em Londres, com a gravação começando em janeiro de
                1977 no The Who's Rampart Studios em Londres. O baixista do Deep Purple Roger Glover foi contratado para produzir o álbum com Mark
                Dodson atuando como engenheiro. As experiências da banda no estúdio enquanto estava com Gull foram menos do que satisfatórias,
                particularmente durante a mixagem de - Rocka Rolla, e eles estavam inicialmente muito interessados ​​em produzir - Sin After Sin
                sozinhos. A CBS, no entanto, insistiu em um produtor experiente. O baixista do Deep Purple, Roger Glover, foi sugerido e a banda
                concordou, mas depois de uma sessão a banda demitiu Glover, informando-o de que continuariam por conta própria. Após algumas
                semanas lutando com gravações insatisfatórias, a banda chamou Glover de volta e as sessões começaram de novo, com apenas seis dias
                restantes. Foi também durante este período que a banda se separou do baterista Alan Moore, sentindo que sua técnica era muito
                limitada para seu som em evolução. O baterista da sessão Simon Phillips foi trazido para terminar as sessões.
            </p>
            <br />

            <p>
                - Sin After Sin foi o lançamento de Judas Priest com maior sucesso comercial até o momento, alcançando a posição #23 no UK Albums
                Chart. Esse sucesso foi difícil de duplicar nos EUA, onde - Sin After Sin falhou nas paradas. Em casa, eles também enfrentaram
                uma recepção um tanto hostil ou foram completamente ignorados por uma imprensa musical que na época estava fortemente focada no
                novo gênero de punk rock que varreu a Grã-Bretanha no final dos anos 1970. Embora levasse vários anos, - Sin After Sin é o
                primeiro de onze álbuns consecutivos do Judas Priest a ser certificado ouro ou superior pela RIAA.
            </p>
            <br /><br />

