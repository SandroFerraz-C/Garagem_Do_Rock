            <h1>Heaven and Hell (1980)</h1>
            <br />

            <p>
                Heaven And Hell é o nono álbum de estúdio da banda de heavy metal Black Sabbath
            </p>
            <br />

            <p>
                É o primeiro a contar com o vocalista Ronnie James Dio, que entrou na banda após a demissão de Ozzy Osbourne. Heaven
                And Hell é considerado um dos melhores álbuns da banda e um dos melhores discos de heavy metal dos anos 1980.
                Vendeu mais de 1 milhão de cópias nos Estados Unidos.
                <br />
            </p>

            <p>
                O baixista Geezer Butler também deixou a banda em 1979. Neste mesmo momento, o vocalista Ronnie James Dio foi
                escolhido como novo frontman do Black Sabbath. Anteriormente, Dio fazia parte da banda de Ritchie Blackmore, o
                Rainbow.
            </p>
            <br />

            <p>
                Muitas canções famosas da fase pré-Dio foram interpretadas por ele durante a turnê, sendo "Paranoid",
                "War Pigs", "Children of the Grave", "Iron Man" e "N.I.B." alguns exemplos. A turnê mundial da banda,
                tinha 20 datas marcadas no Reino Unido, sendo 4 delas no "Hammersmith Odeon", provando que a banda não
                tinha perdido nada de sua popularidade. Em um show em Milwaukee, Geezer levou uma garrafada na cabeça, e
                a banda teve que se retirar após tocar apenas duas músicas.
                <br />
                Infelizmente o baterista Bill Ward, que foi co-fundador da banda, teve que se retirar no meio da turnê por motivos pessoais e de saúde, e o grupo novamente teve que fazer uma substituição. O escolhido foi o baterista Vinny Appice, ex-Derringer, que continuou na banda até meados de 1982. Outra curiosidade sobre o álbum é que na música "A Hard Road", a banda inteira canta junto no final, acontecimento único na carreira do Sabbath.
            </p>
            <br />

            <p>
                O álbum rendeu uma turnê de certo sucesso, que foi filmada e lançada no DVD Never Say Die. Ela terminou, no entanto, quando o maior
                show da turnê teve que ser cancelado depois de Ozzy não aparecer para tocar, pois estava dormindo bêbado num quarto do hotel em que
                estavam hospedados.
                <br />
                Outro fator que contribuiu para isso foi que Tony Iommi, fazia longas sessões de improviso nos shows, que duravam aproximadamente 1 hora,
                e isso irritava muito Ozzy, que mais tarde declarou: "Eu sempre vi o Black Sabbath como uma banda de Rock! Iommi queria dar outro segmento à
                banda, o Sabbath não é uma banda de Jazz, e sim de Rock!".
            </p>
            <br />

            <p>
                Após o fim dessa turnê, Ozzy foi demitido da banda devido ao abuso de drogas e álcool cada vez maior e por causa de desavenças com outros
                membros, em particular com Iommi. Para substituí-lo foi chamado Ronnie James Dio, que tinha acabado de sair do Rainbow. Com o novo
                vocalista, a banda gravou um de seus álbuns mais pesados desde os meados da década de 1970, Heaven and Hell.
            </p>
