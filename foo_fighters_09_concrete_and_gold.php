            <h1>Concrete and Gold (2017)</h1>

            <p>
                - Concrete and Gold é o nono álbum do Foo Fighters, lançado em 15 de setembro de 2017, através de Roswell e RCA Records.
            </p>

            <p>
                É o primeiro álbum da banda a ser produzido ao lado de Greg Kurstin. Descrito pela banda como um álbum onde "os extremos do hard rock
                e as sensibilidades do pop colidem", - Concrete and Gold preocupa-se com o futuro dos Estados Unidos do ponto de vista do vocalista e
                compositor principal Dave Grohl, com a atmosfera aquecida das eleições de 2016 e a presidência de Donald Trump citado como grandes
                influências por Grohl. As justaposições servem como um motivo comum na composição lírica e musical do álbum, com Grohl ainda descrevendo
                o tema geral do álbum como "esperança e desespero".
            </p>

            <p>
                A composição e gravação de Concrete and Gold começaram no final de 2016, depois que Grohl encerrou um hiato auto-imposto de seis meses
                da música enquanto se recuperava de uma lesão sofrida na turnê mundial de - Sonic Highways. Trabalhando a partir de um conjunto de doze
                ou treze idéias para canções concebidas por Grohl, a banda contou com a ajuda de Kurstin, um produtor de música pop, que nunca havia
                trabalhado em um disco de rock pesado antes. O estúdio em que a banda escolheu para gravar - Concrete and Gold,foi o EastWest Studios em
                Hollywood, Califórnia, que já promoveu colaborações com vários outros artistas que também trabalhavam no estúdio na época, incluindo
                Shawn Stockman do Boyz II Men, Justin Timberlake e Paul McCartney. É o primeiro álbum de estúdio do Foo Fighters a apresentar o tecladista
                Rami Jaffee em turnês de longa duração como membro permanente.
            </p>

            <p>
                - Concrete and Gold foi recebido positivamente pelos críticos musicais, que elogiaram a sensação mais expansiva do álbum, tanto
                musicalmente quanto liricamente. Críticas modestas foram direcionadas à percepção da falta de desvio musical dos álbuns anteriores da
                banda. O álbum se tornou o segundo da banda a estrear em #1° lugar na Billboard 200, vendendo 127.000 unidades do álbum e vendendo
                120.000 cópias em sua primeira semana apenas nos Estados Unidos. O álbum também estreou em #1° lugar em doze outras paradas de álbuns
                nacionais, como a Tabela de Álbuns Oficial do Reino Unido e a Tabela de Álbuns ARIA da Austrália. Singles do álbum também tiveram
                sucesso; "Executar" e "The Sky Is a Neighbourhood" alcançou o #1° lugar na parada de músicas de rock da Billboard Mainstream. Uma
                turnê como atração principal para promover o álbum decorreu ao longo do segundo semestre de 2017.
            </p>

            <p>
                As sessões iniciais de composição envolveram apenas Grohl, que continuou isolado da banda, embora inicialmente tenha lutado,
                sentindo-se "fora de prática" e "atrofiado criativamente" devido ao seu intervalo mais longo do que o normal da música. Grohl alugou
                um Airbnb em Ojai, Califórnia, para que ele pudesse se concentrar em longos períodos de escrita, com Grohl contando "Eu trouxe uma
                caixa de vinho e sentei lá de cueca com um microfone por cerca de cinco dias, apenas escrevendo." Depois que doze ou treze idéias
                brutas foram mapeadas, ele as conduziu pela banda, que compartilhou a crença de Grohl de que ele estava no caminho certo com o material.
                Feliz com seu trabalho, mas sentindo que o material ainda precisava de mais desenvolvimento, Grohl começou a pensar em entrar em contato com
                um produtor musical, quando fez contato com o produtor Greg Kurstin.
            </p>
