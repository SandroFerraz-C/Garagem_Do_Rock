            <h1><span lang="en-US">FLICK OF THE SWITCH</span> (<time datetime="1983">1983</time>)</h1>

            <p>- <span lang="en-US">Flick of the Switch</span> é o décimo álbum de estúdio da banda AC/DC, lançado em <time datetime="1983-08-15">15 de agosto de 1983</time>.</p>

            <p>O álbum foi considerado uma decepção comercial por não corresponder aos números de vendas dos dois lançamentos anteriores da banda, - <a href="acdc_08_black_in_black.php" target="blank"><span lang="en-US">Back in Black</span></a> de <time datetime="1980">1980</time> e
                - <a href="acdc_09_for_those_about_to_rock_(we_salute_you).php" target="blank"><span lang="en-US">These About to Rock</span></a> de <time datetime="1981">1981</time>, e seu lançamento representou o início do declínio comercial da banda. Apesar disso, as opiniões sobre
                - <span lang="en-US">Flick of the Switch</span> têm sido bastante positivas nos anos desde seu lançamento, e o álbum tem sido freqüentemente mencionado como um dos melhores lançamentos de <time datetime="1983">1983</time>. Terceiro álbum do AC/DC com o vocalista Brian Johnson,
                o álbum também é o último a apresentar o baterista Phil Rudd antes de seu retorno em - <a href="acdc_15_ballbreaker.php" target="blank"><span lang="en-US">Ballbreaker</span></a> de (<time datetime="1995">1995</time>). O álbum foi relançado em <time datetime="2003">2003</time>
                como parte da série AC/DC <span lang="en-US">Remasters</span>.
            </p>

            <p>O nascimento do álbum foi problemático; Depois de ter problemas com Malcolm, bem como drogas e álcool, o baterista Phil Rudd foi demitido no meio das sessões de gravação do álbum, embora ele tivesse completado suas partes de bateria.
            <blockquote>De acordo com as memórias da banda de <cite>Murray Engleheart - AC/DC: <span lang="en-US">Maximum Rock & Roll</span></cite> -, Rudd vinha lutando há algum tempo; O gerente de turnê Ian Jeffrey lembra de ter recebido um telefonema de Rudd exausto às quatro da manhã, quando a banda estava
                tocando em Nebraska na turnê - <span lang="en-US">Back in Black</span> - e encontrando o baterista em seu quarto de hotel em um estado de agitação desorientada. Por fim, Rudd desatou a chorar e implorou a Jeffery: <q>Não conte a Malcolm</q>. <cite>Jeffery</cite> também revela
                que Malcolm socou o baterista depois que ele apareceu duas horas atrasado para o show da banda em <span lang="en-US">Long Island's Nassau Coliseum</span> e foi incapaz de tocar a última música do "bis". <q>Foi uma coisa absolutamente estúpida terminar, mas já
                    vinha fermentando há muito, muito tempo</q>, lembra Jeffery. <q>Ele se drogou e ficou exausto</q>, explicou Malcolm mais tarde ao <a href="http://www.knac.com" target="blank">KNAC.com</a> em agosto de <time datetime="2000">2000</time>.</blockquote>
            O ex- baterista do - <span lang="en-US">Procol Harum BJ Wilson</span> foi contratado para ajudar a completar a gravação se necessário, mas suas contribuições não foram usadas.</p>

            <p>Gravado logo após sua turnê européia de <time datetime="1982">1982</time> no início de <time datetime="1983">1983</time>, o álbum foi originalmente lançado com pouca promoção nos Estados Unidos em <time datetime="1983-08-15">15 de agosto de 1983</time>. O álbum alcançou o #4º lugar no Reino Unido e o #15º nos EUA, e foi certificado como platina.
            <blockquote>Em sua crítica original da <a href="https://www.rollingstone.com/music/music-album-reviews/flick-of-the-switch-200578" target="blank">Rolling Stone</a>, <cite>David Fricke</cite> observou: <q>Produzido pela banda, - <span lang="en-US">Flick of the
                        Switch</span> não é exatamente a explosão monstruosa de - <a href="acdc_08_black_in_black.php" target="blank"><span lang="en-US">Back in Black</span></a> dos anos de <time datetime="1980">1980</time>, e a reformulação dos Youngs de riffs antigos para novos sucessos também às vezes oscila no autoplágio.</q></blockquote>
            <blockquote>O jornalista canadense <cite>Martin Popoff</cite> considerou o álbum como: <q>O pico furioso e ofuscante da era Brian Johnson</q>, onde o AC/DC recapturou <q>a borda crua perdida durante os anos de Mutt Lange ... mesmo que as canções estivessem começando
                    a reviver glórias passadas</q>.</blockquote>
            <blockquote><cite>Steve Huey</cite> do <span lang="en-US">AllMusic</span> era da mesma opinião, escrevendo que <q>Como talvez indicado pelo título original idiota do disco, o genérico <span lang="en-US">I Like to Rock</span>, parecia estar ficando sem ideias em um ritmo alarmante,
                    e suas vendas de discos começaram a refletir esse fato.</q></blockquote>
            <blockquote><cite>Malcolm Young</cite> disse mais tarde sobre o LP: <q>Foi lançado bem rápido. Eu não diria que é um ótimo álbum.</q></blockquote>
            </p>
