            <h1>FLICK OF THE SWITCH (1983)</h1>

            <p>- Flick of the Switch é o décimo álbum de estúdio da banda AC/DC, lançado em 15 de agosto de 1983.</p>

            <p>O álbum foi considerado uma decepção comercial por não corresponder aos números de vendas dos dois lançamentos anteriores da banda, - <a href="acdc_08_black_in_black.html" target="blank">Back in Black</a> de 1980 e - <a href="acdc_09_for_those_about_to_rock_(we_salute_you).html" target="blank">For These About to Rock</a> de 1981, e seu lançamento representou o início do declínio comercial da banda. Apesar disso, as opiniões sobre - Flick of the Switch têm sido bastante positivas nos anos desde seu lançamento,
                e o álbum tem sido freqüentemente mencionado como um dos melhores lançamentos de 1983. Terceiro álbum do AC/DC com o vocalista Brian Johnson, o álbum também é o último a apresentar o baterista Phil Rudd antes de seu retorno em -
                <a href="acdc_15_ballbreaker.html" target="blank">Ballbreaker</a> de (1995). O álbum foi relançado em 2003 como parte da série AC/DC Remasters.
            </p>

            <p>O nascimento do álbum foi problemático; depois de ter problemas com Malcolm, bem como drogas e álcool, o baterista Phil Rudd foi demitido no meio das sessões de gravação do álbum, embora ele tivesse completado suas partes de bateria. De acordo com as
                memórias da banda de Murray Engleheart - AC/DC: Maximum Rock & Roll -, Rudd vinha lutando há algum tempo; O gerente de turnê Ian Jeffrey lembra de ter recebido um telefonema de Rudd exausto às quatro da manhã, quando a banda estava
                tocando em Nebraska na turnê - Back in Black - e encontrando o baterista em seu quarto de hotel em um estado de agitação desorientada. Por fim, Rudd desatou a chorar e implorou a Jeffery: "Não conte a Malcolm". Jeffery também revela
                que Malcolm socou o baterista depois que ele apareceu duas horas atrasado para o show da banda em Long Island's Nassau Coliseum e foi incapaz de tocar a última música do "bis". “Foi uma coisa absolutamente estúpida terminar, mas já
                vinha fermentando há muito, muito tempo”, lembra Jeffery. "Ele se drogou e ficou exausto", explicou Malcolm mais tarde ao <a href="http://www.knac.com" target="blank">KNAC.com</a> em agosto de 2000. O ex- baterista
                do - Procol Harum BJ Wilson foi contratado para ajudar a completar a gravação se necessário, mas suas contribuições não foram usadas.</p>

            <p>Gravado logo após sua turnê européia de 1982 no início de 1983, o álbum foi originalmente lançado com pouca promoção nos Estados Unidos em 15 de agosto de 1983. O álbum alcançou o #4º lugar no Reino Unido e o #15º nos EUA, e foi certificado como platina.
                Em sua crítica original da <a href="https://www.rollingstone.com/music/music-album-reviews/flick-of-the-switch-200578" target="blank">Rolling Stone</a>, David Fricke observou: "Produzido pela banda, Flick of the
                Switch não é exatamente a explosão monstruosa de - <a href="acdc_08_black_in_black.html" target="blank">Back in Black</a> dos anos 80, e a reformulação dos Youngs de riffs antigos para novos sucessos também às vezes oscila no autoplágio."
                O jornalista canadense Martin Popoff considerou o álbum como: "O pico furioso e ofuscante da era Brian Johnson", onde o AC/DC recapturou "a borda crua perdida durante os anos de Mutt Lange ... mesmo que as canções estivessem começando
                a reviver glórias passadas". Steve Huey do AllMusic era da mesma opinião, escrevendo que "como talvez indicado pelo título original idiota do disco, o genérico I Like to Rock, parecia estar ficando sem ideias em um ritmo alarmante,
                e suas vendas de discos começaram a refletir esse fato". Malcolm Young disse mais tarde sobre o LP: "Foi lançado bem rápido. Eu não diria que é um ótimo álbum".</p>
