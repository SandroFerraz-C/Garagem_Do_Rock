                versão anterior, estavam o temor de que uma capa muito ousada afastasse as lojas e o fato de que Jon não teria gostado de uma borda cor-de-rosa
                em volta da referida foto. No Japão, a maior parte dos lançamentos inclui o primeiro projeto gráfico.
