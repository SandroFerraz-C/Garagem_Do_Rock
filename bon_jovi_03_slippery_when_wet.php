                Com a ajuda de um técnico vocal, Jon Bon Jovi e os outros integrantes da banda continuaram a turnê. Daí em diante, Jon teve que cantar em
                tons ligeiramente mais baixos desde então.
            </p>

            <p>
                O nome do disco passou por várias alterações, sendo inclusive cogitada a possibilidade de se chamar - Wanted Dead or Alive -, com os integrantes
                da banda vestidos de cowboys - ideia aproveitada mais tarde no single da canção homônima. A decisão por - Slippery When Wet, segundo Jon,
                se deu após uma visita a um strip club em Vancouver, durante as folgas da gravação, onde as mulheres se ensaboavam e ficavam escorregadias
                quando estavam molhadas.
            </p>

            <p>
                O projeto gráfico da capa, inicialmente, mostrava uma mulher com grandes seios, vestindo uma camiseta amarela molhada com o nome do álbum.
