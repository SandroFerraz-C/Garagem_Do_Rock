                no estúdio Little Mountain Sound Studios, em Vancouver, no Canadá.
            </p>

            <p>
                Jon inicialmente não queria incluir "Livin' on a Prayer" no álbum, acreditando que a canção não era boa o suficiente. Entretanto, convencido
