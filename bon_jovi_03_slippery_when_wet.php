                na versão final que entrou para o disco. A canção logo se tornou um dos maiores sucessos do grupo.
            </p>

            <p>
                O álbum vendeu 28 milhões de cópias mundialmente, sendo considerado pela crítica especializada um dos grandes feitos do mundo do rock.
