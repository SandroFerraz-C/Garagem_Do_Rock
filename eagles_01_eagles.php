            <h1>Eagles (1972)</h1>

            <p>- Eagles é o primeiro álbum da banda Eagles.</p>

            <p>O álbum foi gravado no Olympic Studios de Londres com o produtor Glyn Johns e lançado em 1972. O álbum foi um sucesso imediato
                para a jovem banda, alcançando o 22º lugar nas paradas e chegando ao disco de platina. Três singles foram lançados do álbum, cada
                um alcançando o Top 40: " Take It Easy " (número 12), " Witchy Woman " (número 9) e " Peaceful Easy Feeling " (número 22). A banda,
                a partir desse álbum, teve um papel importante na popularização do som country rock .</p>

            <p>O álbum foi classificado como número #368 na lista dos #500 melhores álbuns de todos os tempos da revista Rolling Stone
                publicada em 2012. O single "Take It Easy" faz parte do Hall da Fama do Rock and Roll "500 músicas que moldaram o rock".</p>

            <p>Em 1971, a banda havia acabado de ser formada e assinada por David Geffen, que então os enviou para Aspen, Colorado, para se
                desenvolverem como uma banda. Para seu primeiro álbum, Glenn Frey queria que Glyn Johns fosse o produtor, pois eles gostavam de
                uma série de álbuns de rock produzidos por Johns, incluindo álbuns dos Rolling Stones, The Who e Led Zeppelin. Johns foi convidado
                pela Geffen para ver a banda se apresentar em um clube chamado Tulagi em Boulder, Colorado, em dezembro de 1971. Johns, no entanto,
                não ficou impressionado com a performance ao vivo da banda, pensando que a banda estava confusa e sem coesão - Frey queria que fosse uma
                banda de rock & roll, enquanto Bernie Leadon queria uma sensação country - então Johns se recusou a produzir o álbum. Johns foi persuadido
                por Geffen a ter uma segunda audição em um cenário de ensaio em Los Angeles, mas Johns não mudou sua opinião sobre a banda até que todos os
                quatro começaram a cantar harmonias com violão em uma balada escrita por Meisner, "Pegue o Diabo". Johns ficou impressionado com a harmonia
                deles cantando, e mais tarde disse: "Lá estava, o som. Combinação extraordinária de vozes, som de harmonia maravilhoso, simplesmente
                deslumbrante." Nos álbuns que ele produziu para os Eagles, Johns enfatizou a mistura vocal da banda, e ele foi creditado por moldar a banda
                em "uma banda de country-rock com aquelas harmonias agudas".</p>
