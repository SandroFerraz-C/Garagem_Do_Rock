            <h1>Sonic Highways (2014)</h1>
            <br />

            <p>
                "Sonic Highways" é o oitavo álbum do Foo Fighters, lançado em 10 de novembro, 2014, pela Roswell e RCA Records
            </p>
            <br />

            <p>
                Semelhante ao álbum anterior, - Wasting Light (2011), - Sonic Highways também foi produzido pela banda com Butch Vig. Ao escrever as
                oito canções do álbum, o cantor e guitarrista Dave Grohl viajou para oito cidades nos Estados Unidos para conduzir entrevistas com
                músicos, engenheiros de gravação, produtores musicais e outros indivíduos discutindo a história musical de cada cidade, que ele
                usou como inspiração para as letras das músicas. A banda e Butch Vig então viajaram para um local de gravação diferente em cada cidade
                para gravar as músicas. Cada faixa apresenta contribuições de um ou mais músicos ligados à história musical daquela cidade. O processo
                foi filmado para uma série de televisão associada, - Foo Fighters: Sonic Highways, que foi transmitida pela HBO nos meses que cercaram o
                lançamento do álbum.
            </p>
            <br />

            <p>
                Em 6 de setembro de 2013, o guitarrista Chris Shiflett postou uma foto em sua conta do Instagram que indicava que 13 músicas estavam
                sendo gravadas para o novo álbum. O tecladista Rami Jaffee gravou partes para três canções, uma das quais intitulada "In the Way".
                Butch Vig, que produziu o álbum anterior da banda, - Wasting Light, confirmou que produziu - Sonic Highways também. Em 30 de julho de 2014,
                Vig revelou que a banda havia terminado de gravar e mixar o novo álbum e que estava programado para ser lançado um mês após a série de TV
                - Sonic Highways. Em um comunicado à imprensa em agosto de 2014, Grohl falou sobre o álbum, dizendo: "Este álbum é instantaneamente
                reconhecível como um álbum do Foo Fighters, mas há algo mais profundo e musical nele. Acho que essas cidades e essas pessoas nos influenciaram
                a nos expandirmos e explorar novos territórios, sem perder nosso 'som'".
            </p>
            <br />

            <p>
                A capa do álbum criada por Stephan Martiniere tem uma paisagem urbana com marcas de cada cidade utilizada para a produção - de Seattle
                a Space Needle, o sinal de Hollywood em Los Angeles - e um motivo recorrente do número oito, se referindo as 8 cidades que se foi pecorrido e
                o oitavo álbum da banda, e o edifício "Forever" em forma de infinito (∞). As impressões em vinil do álbum são embaladas em nove capas
                diferentes, representando cada uma das oito cidades e o edifício "Forever". A maioria dos varejistas não garante qual cobertura o comprador
                receberá, e a arte do álbum é "selecionada aleatoriamente" para pedidos no site oficial da banda.
            </p>
            <br />

            <p>
                No Metacritic, que atribui uma classificação normalizada de 100 às críticas dos críticos convencionais, o álbum recebeu uma pontuação
                média de #68, com base em 31 críticas que indicam "críticas geralmente favoráveis". Stephen Thomas Erlewine do AllMusic diz que o álbum
                "celebra não a fúria enrolada do rock underground que explodiu no mainstream, como fez Wasting Light dos anos 90, mas sim o rock clássico
                que une os EUA de costa a costa". Philip Cosores da Consequence of Sound afirmou que "o álbum funciona mais como um bônus, algo que pode
                aumentar a diversão da série ou simplesmente informar ainda mais a experiência".
            </p>
