            <h1>The House of Blue Light (1989)</h1>

            <p>- The House of Blue Light é o estúdio décimo segundo álbum de The British rocha banda Deep Purple , lançado em 1987.</p>

            <p>A criação do álbum foi um processo excessivamente longo e difícil, que o cantor Ian Gillan comparou à gravação de Who Do We
                Think We Are em Roma. Gillan comentou sobre como as relações tensas dentro da banda comprometeram o álbum: "I look back at House Of Blue Light",
                há algumas músicas boas nesse álbum, mas há algo faltando no álbum geral. Não consigo sentir o espírito desta banda. Posso ver ou
                ouvir cinco profissionais dando o seu melhor, mas é como um time de futebol, não está funcionando. É como onze superestrelas que
                estão jogando no mesmo campo, mas não estão conectadas pelo coração ou pelo espírito" Guitarrista Ritchie Blackmoredisse que muito
                disso foi regravado, e confessou: "Eu acho que toquei como uma merda nele, e não acho que ninguém realmente tenha entendido isso."
                O organista Jon Lord acrescentou: " House of Blue Light foi um álbum estranho e difícil de montar. Cometemos o grande erro de tentar tornar
                nossa música atual. Descobrimos que as pessoas não queriam que fizéssemos isso."</p>

            <p>Apesar das preocupações da banda, House of Blue Light vendeu bem. Alcançou a #10 posição nas paradas do Reino Unido, a #34 na
                Billboard 200 nos Estados Unidos e alcançou o top 10 em seis outros países.</p>

            <p>O álbum foi um sucesso comercial, alcançando a posição #5 nas paradas do Reino Unido e #12 na Billboard 200 nos
                Estados Unidos. Perfect Strangers foi apenas o segundo álbum de estúdio do Deep Purple a receber disco de platina
                nos Estados Unidos, depois de Machine Head, de 1972. A turnê foi tão bem sucedida que a banda teve que reservar
                muitas datas adicionais para a turnê nos Estados Unidos, pois os ingressos esgotaram muito rapidamente. Sua turnê
                pelos Estados Unidos em 1985 superou todos os artistas naquele ano, exceto Bruce Springsteen .</p>
