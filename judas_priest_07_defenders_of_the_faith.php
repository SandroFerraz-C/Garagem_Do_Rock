            <h1>Defenders of the Faith (1984)</h1>
            <br />

            <p>
                - Defenders of the Faith é o nono álbum do Judas Priest, lançado em 1984 pela Columbia Records.
            </p>
            <br />

            <p>
                Este álbum é considerado um clássico do estilo heavy metal com sons pesados, rápidos e harmônicos.
                <br />
                Foram gravados videoclipes para as músicas "Freewheel Burning" e Love Bites"
            </p>
            <br />

            <p>
                Apesar do fato de que o processo de gravação em si foi um sucesso, o guitarrista K. K. Downing sofreu um grave acidente que quase
                lhe custou a vida. Quando eles estavam ainda na cidade espanhola de Ibiza, ele e o engenheiro de som, Mark Dodson, se dirigiram a
                uma boate e quando saíram do estabelecimento, Downing foi atropelado por um táxi. Segundo testemunhas, antes dele chegar a uma
                esquina o táxi lhe atropelou de frente jogando-lhe a vários metros de distância. De acordo com Glenn Tipton, o guitarrista foi
                levado ao hospital consciente, mas com fraturas graves que lhe custaram várias semanas de descanso antes de voltar a gravar.
            </p>
            <br />

            <p>
                Sua capa foi projetada pela banda e o desenho na sua parte prática foi trabalho de Doug Johnson, o mesmo artista que criou o
                pássaro de metal chamado The Hellion em - Screaming for Vengeance. O personagem que aparece sobre esta capa é chamado de
                Metallian, uma criatura semelhante a um tigre, com chifres de carneiro e corpo com o formato de um tanque, criado pelos
                membros do banda. De acordo com Downing, precisavam de uma capa chamativa, refletindo o título do disco e, portanto, criado
                o Metallian como um defensor do Heavy metal.
                <br />

                Como em - Screaming for Vengeance, na sua contracapa está escrita um pequeno trecho com a ideia de contar uma breve história
                desta criatura: "Emerge das trevas, onde o inferno não tem piedade e os ecos dos gritos de vingança estarão para sempre.
                Apenas aqueles que mantém a sua fé irão escapar da ira de Metallian... mestre de todo Metal."
            </p>
            <br /><br />

            <p>Duração do album: 38 min. 42 seg.</p>
            <p>Gravadora(s): Columbia Records.</p>
            <p>Produção: Tom Allom.</p>
