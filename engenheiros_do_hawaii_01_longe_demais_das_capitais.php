            <h1>Longe Demais das Capitais (1986)</h1>
            <br />

            <p>
                "Longe Demais das Capitais" é o álbum de estreia da banda de rock brasileira, do Rio Grande do Sul, Engenheiros do Hawaii, lançado em
                1986 pela gravadora RCA (posteriormente BMG e Sony BMG, atualmente Sony Music).
            </p>
            <br />

            <p>
                Revelada pela coletânea Rock Grande do Sul com as canções Sopa de Letrinhas (até agora, a única parceria entre Humberto Gessinger e
                Marcelo Pitz) e Segurança, a banda gaúcha lançou em fins de 1986 seu primeiro LP, puxado pelos sucessos "Toda Forma de Poder"
                (incluída na trilha sonora da novela Hipertensão, da Rede Globo), "Segurança" (incluída na trilha sonora da telenovela Corpo Santo,
                da Rede Manchete) e "Sopa de Letrinhas". O disco se destaca pela visão crítica das letras do vocalista Humberto Gessinger, que versam
                sobre insegurança, falta de perspectiva e existencialismo, com toques políticos.
            </p>
            <br />

            <p>
                O disco foi recentemente relançado em vinil pela Sony Music na série Meu Primeiro Disco, que traz os relançamentos dos discos de
                estréia de vários artistas da gravadora, em vinil e também em CD, com versões demo.
            </p>
            <br /><br />

            <p>Duração do album: 36 min. 38 seg.</p>
            <p>Gravadora(s): BMG e RCA.</p>
            <p>Produção: Reinaldo Barriga.</p>

