            <h1>Get Your Wings (1974)</h1>

            <p>- Get Your Wings, é o segundo álbum de estúdio da banda de rock estadunidense Aerosmith, lançado em 1 de março, 1974.
                O álbum é o primeiro a ter a participação na produção de Jack Douglas, que passaria a produzir os próximos quatro álbuns da banda.</p>

            <p>Em sua autobiografia, Tyler escreve que "Seasons of Wither" estava "germinando na minha cabeça há muito tempo, mas as outras faixas
                mais sinistras, como "Lord of the Thighs", vieram da área decadente onde gravamos o álbum. "Lord of the Thighs" era sobre um cafetão
                e a vida selvagem nas ruas". Tyler toca piano em "Lord of the Thighs", cuja batida de abertura é semelhante à que Kramer faria um ano
                depois em "Walk This Way". Ele afirmou que o título era um trocadilho com o famoso romance de William Golding, - O Senhor das Moscas -,
                e "os críticos nos odiavam por isso. Não devíamos ser inteligentes o suficiente para usar referências literárias".</p>

            <p>O título do álbum foi a primeira aparição do logo alado da banda, que passaria a ser modificada e usada em muitos de seus álbuns posteriores.
                O disco também é considerado uma espécie de "laboratório" para seu sucessor, "Toys In The Attic", lançado no ano seguinte.</p>
