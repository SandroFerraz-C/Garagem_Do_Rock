            <h1>THE RAZOR’S EDGE (1990)</h1>

            <p>- The Razor’s Edge foi lançado a 21 de Setembro de 1990. O álbum foi o primeiro e único álbum de estúdio a incluir o baterista Chris Slade, e possui canções como “Thunderstruck” e “Moneytalks,” que se encontram entre as mais reconhecidas do grupo.</p>

            <p>O álbum chegou ao #2º lugar da norte-americana Billboard 200 e #4º no Reino Unido, dando aos AC/DC um sucesso e popularidade similar aos tempos de glóriado início dos anos 80. O álbum foi certificado com 5X platina referente à venda de mais de 5 milhões
                de cópias nos EUA, e foi relançado em 2003 como parte da série Remasters dos AC/DC.</p>

            <p>A banda gravou - Blow Up Your Video com seus produtores originais, Harry Vanda e George Young. Foi um sucesso comercial e vendeu mais cópias do que os dois lançamentos anteriores Juntos. A turnê mundial - Blow Up Your Video - começou em fevereiro de 1988
                em Perth, Austrália. Naquele mês de abril, na véspera da etapa norte-americana da turnê, Malcolm Young anunciou que estava tirando uma folga da turnê, principalmente para começar a se recuperar do alcoolismo. Outro membro da família
                Young, seu sobrinho, Stevie Young, temporariamente tomou o lugar de Malcolm na guitarra base. Após a turnê, o baterista Simon Wright deixou o grupo para trabalhar no próximo álbum da banda - Dio -, = Lock Up the Wolves , e foi substituído
                pelo veterano de bateria do ex-Firm e Manfred Mann's Earth Band Chris Slade, que Malcolm se lembrava de ter tocado com Manfred Mann durante uma apresentação em Sydney anos antes com Deep Purple.</p>
