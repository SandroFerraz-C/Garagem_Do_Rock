            <h1>Wasting Light (2011)</h1>

            <p>
                - Wasting Light é o sétimo álbum do Foo Fighters, lançado em 12 de abril de 2011, pela Roswell e RCA Records.
            </p>

            <p>
                Querendo capturar a essência de seu trabalho anterior e evitar a artificialidade da gravação digital, o Foo Fighters gravou na
                garagem de Dave Grohl em Encino, Califórnia, usando apenas equipamentos analógicos. As sessões foram produzidos pela banda ao
                lado de Butch Vig, com quem Grohl tinha trabalhado no Nirvana. Como o equipamento antigo não permitia que muitos erros fossem
                corrigidos na pós-produção, a banda passou três semanas ensaiando as músicas e Vig teve que reaprender técnicas de edição ultrapassadas.
                A banda buscou um som mais pesado e cru em contraste com a experimentação de seus álbuns anteriores. A maioria das letras foi escrita
                enquanto Grohl refletia sobre sua vida e possível futuro. Os músicos convidados incluem Bob Mold, Krist Novoselic, Jessy Greene, Rami Jaffee
                e Fee Waybill. Pat Smear tocou como membro oficial da banda pela primeira vez desde - The Color and the Shape (1997); ele apareceu em seu
                álbum anterior, - Echoes, Silence, Patience & Grace (2007), apenas como músico de sessão.
            </p>

            <p>
                As sessões de gravação foram documentadas no site da banda e no Twitter. A promoção incluiu o documentário -Back and Forth e uma
                turnê mundial que incluiu apresentações nas garagens dos fãs. - Wasting Light foi precedida pelo sucesso de "Rope ", que se tornou
                a 2° música da banda a estrear no número #1 na Billboard's rock. O single seguinte, "Walk", também obteve sucesso. Wasting Light
                estreou em 1° lugar em onze países, e recebeu críticas positivas da maioria dos críticos musicais, que elogiaram a produção e
                composição. Em 2012, - Wasting Light ganhou quatro prêmios Grammy, incluindo Melhor Álbum de Rock.
            </p>

            <p>
                Grohl queria criar um álbum que "definiria" o Foo Fighters, como ele sentia que - Back in Black definia AC / DC ou - Black Album
                definia Metallica: "Pode não ser o melhor álbum deles, mas é aquele que as pessoas identificam a banda com o a maioria ...
                Você pega todas as coisas que as pessoas consideram características da sua banda e apenas as amplifica e faz um álbum simples
                com isso." Ele contratou Vig para o projeto porque achava que Vig era especialista em "aparar toda a gordura e dar sentido a tudo".
            </p>

            <p>
                Em vez de gravar o álbum em um estúdio moderno, Grohl decidiu gravar em sua garagem mesmo. Grohl disse: "Há poesia em ser uma banda
                que pode esgotar estádios, mas também faz um disco em uma garagem. Por que ir para o estúdio mais caro com o maior produtor e usar
                o melhor equipamento de última geração? Onde está o rock'n'roll nisso? " Grohl sentiu que era uma maneira de fazer um álbum inovador,
                com "som primordial", subverter as expectativas e "fazer discos do jeito que costumávamos fazer discos".
            </p>
