            <p>
                - Temple of Shadows é o setimo álbum da banda brasileira de power metal Angra.
            </p>
           

            <p>
                O álbum é conceitual, pois conta a história fictícia de um cavaleiro conhecido como "The Shadow Hunter" (O Caçador da Sombra) que se
                une ao exército convocado pelo Papa para participar da Primeira Cruzada (1096-1099);
                <br />
                Durante sua saga, ele passa por conflitos que o fazem refletir sobre a guerra santa da qual participa e os ideais da Igreja Católica,
                colocando sua devoção a prova.
            </p>
           

            <p>
                O álbum foi bem recebido pela crítica especializada, o Akihisa Ozawa da Burn! deu 91 pontos pelo álbum, algo raro na revista, o álbum
                ganhou 3 notas 10 no Free Paper da loja Sumiya, Edson Rocha (Redator do site Novo Metal) e chegou a comentar:
                <br /><br />

                "Um álbum que está há mais de dois meses no meu player não pode ser ruim. Já fiz diversos testes para avaliar a qualidade de - Temple of Shadows;
                como ouvir de mau humor, deixar ele por uma semana na gaveta e voltar a escutá-lo, ouvir no carro, no discman, com dor de cabeça e etc,
                ou seja, dos mais diversos modos e humores e não consegui encontrar falhas nem músicas que estivessem lá somente para preencher espaço."
