            <h1>Powerage (1978)</h1>

            <p>- Powerage é o quinto álbum do AC/DC, lançado em 25 de Maio de 1978, e também é o primeiro álbum que não contém uma faixa homônima.</p>

            <p>Foi o primeiro álbum do AC / DC a apresentar Cliff Williams no baixo, e também foi o primeiro álbum do AC/DC a não ter uma faixa-título (Como "Powerage" além do álbum - <a href="acdc_01_high_voltage.php" target="blank">High Voltage</a> exclusivo da Austrália). - Powerage foi relançado em 2003 como parte da série AC/DC Remasters.</p>

            <p>Embora as vendas iniciais tenham sido um tanto decepcionantes, - Powerage ultrapassou seu antecessor, <a href="acdc_05_let_there_be_rock.php" target="blank">Let There Be Rock</a>, alcançando a posição #133 na parada de álbuns pop da Billboard
                nos Estados Unidos, eventualmente obtendo o certificado de platina. Eddie Van Halen e o guitarrista dos Rolling Stones, Keith Richards, afirmaram que o - Powerage continua sendo seu disco favorito do AC/DC. O álbum manteve-se um favorito
                de Malcolm Young, que foi citado em - AC/DC: Maximum Rock & Rolldizendo, "Eu sei que muitas pessoas o respeitam, muitos fãs de rock and roll de verdade, do AC/DC, os caras do rock and roll puro. Acho que é o álbum mais subestimado de todos."</p>

            <p>O álbum conta com faixas históricas que vieram a se tornar um dos maiores clássicos de todos os tempos. Guitarristas como
                <a href="https://pt.wikipedia.org/wiki/Slash" target="blank">Slash</a>,
                <a href="https://pt.wikipedia.org/wiki/Keith_Richards" target="blank">Keith Richards</a>,
                <a href="https://pt.wikipedia.org/wiki/Eddie_Van_Halen" target="blank">Eddie Van Halen</a>,
                <a href="https://pt.wikipedia.org/wiki/Gene_Simmons" target="blank">Gene Simmons</a>,
                <a href="https://pt.wikipedia.org/wiki/Ron_Wood" target="blank">Ron Wood</a>, afirmam sua paixão pelo AC/DC, tendo como em especial o álbum - Powerage.
                <br /> É o primeiro trabalho com o baixista
                <a href="https://pt.wikipedia.org/wiki/Cliff_Williams" target="blank">Cliff Williams</a>, que entrou substituindo
                <a href="https://pt.wikipedia.org/wiki/Mark_Evans" target="blank">Mark Evans</a>.
                <br /> Anos depois, Evans afirmou que parte do álbum contém linhas de baixo tocadas por ele.
            </p>
