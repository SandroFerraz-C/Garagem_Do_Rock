            <h1>Minutes to Midnight (2007)</h1>

            <p>
                - Minutes to Midnight é o terceiro álbum do Linkin Park.
            </p>

            <p>
                lançado em 14 de maio de 2007 pela Warner Bros. Records, o álbum foi produzido por Mike Shinoda e Rick Rubin, e é o primeiro álbum
                que não foi produzido por Don Gilmore, que havia produzido os dois álbuns anteriores da banda. - Minutes to Midnight foi o álbum
                seguinte de - Meteora (2003) e apresenta uma mudança na direção musical do grupo. Para a banda, o álbum marca o início do desvio de
                seu som característico do nu metal. É também o primeiro álbum completo da banda a levar um selo
                <a href="https://pt.wikipedia.org/wiki/Parental_Advisory" target="blank"> Parental Advisory</a>.
            </p>

            <p>
                O álbum estreou em #1° na Billboard 200 dos EUA e em #15° em outros países, incluindo o Reino Unido e Canadá. Nos Estados Unidos,
                o álbum teve as maiores vendas na primeira semana de 2007 na época, com 623.000 álbuns vendidos, passando a ser certificado como
                platina quádrupla nos Estados Unidos. Também foi certificado com platina dupla na Nova Zelândia, Itália, Irlanda e Austrália e
                platina certificada no Canadá, França, Suíça e no Reino Unido. Apesar de seu sucesso comercial, - Minutes to Midnight recebeu
                críticas mistas dos críticos. A revista Rolling Stone o nomeou como o #25° melhor álbum de 2007. NO total ele vendeu mais de quatro
                milhões de cópias nos Estados Unidos e 20 milhões de cópias em todo o mundo. Foi classificada como #154° na Billboard's Hot 200
                álbuns da década.
            </p>

            <p>
                Em uma entrevista, o vocalista Chester Bennington explicou que o álbum é "uma mistura de padrões de punk, rock clássico e
                hip-hop" e que "Rick trouxe uma sensação mais despojada de rock clássico e hip-hop".
                <br />
                Em outra entrevista, Bennington afirmou: “Desta vez, Mike Shinoda está cantando muito mais. Pode parecer que ele não está no
                álbum, mas ele está fazendo muitas harmonias. Ele também canta algumas músicas sozinho. estamos nos apresentando de uma maneira
                diferente.
            </p>

            <p>
                - Minutes to Midnight foi adiado várias vezes antes de seu lançamento. Primeiro foi programado para ser lançado no verão de 2006,
                mas foi posteriormente adiado para o outono de 2006, e novamente até o início de 2007. A data de lançamento do álbum foi finalmente
                definida para 14 de maio de 2007. Existem lançamentos não - Parental Advisory do álbum padrão e da edição especial, onde as canções
                "Given Up", "Bleed It Out" e "Hands Held High" não contém palavrões. Na Malásia, a versão limpa do álbum está disponível na capa
                Digipak, enquanto a edição explícita disponível para o Tour Edition, que possui capa de papelão branco padrão. Nos Estados Unidos,
                o álbum teve as maiores vendas da primeira semana de 2007 na época, com 625 mil cópias vendidas. No Canadá, o álbum vendeu mais de
                50.000 cópias em sua primeira semana e estreou em número um no Canadian Albums Chart. Em todo o mundo, o álbum vendeu mais de 3,3
                milhões de cópias nas suas primeiras quatro semanas de lançamento.
            </p>
            <br />

