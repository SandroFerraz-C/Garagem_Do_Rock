                aclamado pela crítica, que elogiou principalmente as letras variadas e as melodias potentes. Foi também um grande sucesso comercial,
                se tornando o disco de estreia mais vendido da história da música e é hoje o #11º álbum mais vendido nos Estados Unidos, bem como um
