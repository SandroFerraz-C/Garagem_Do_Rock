            <h1>Appetite for Destruction (1987)</h1>
            <br />

            <p>
                "Appetite for Destruction" é o álbum de estreia do grupo norte-americano de hard rock Guns N' Roses.
            </p>
            <br />

            <p>
                Lançado em 21 de julho de 1987 pela Geffen Records, não obteve muito sucesso de início, mas começou a vender assim que a banda iniciou
                turnês pela América do Norte como abertura para grandes nomes do rock como The Cult, Alice Cooper e Aerosmith, além de seus próprios
                shows por festivais e casas de show.
            </p>
            <br />

            <p>
                Apesar de todas as faixas creditarem todos os cinco membros originais como compositores algumas das faixas tiveram as letras criadas
                individualmente por cada um em diferentes épocas e as melodias só foram produzidas quando a banda já estava junta em estúdio. O
                estilo do álbum, como um todo, se resume ao popular hard rock dos anos 80 com influências de AC/DC e Rolling Stones, com guitarras em
                evidência em canções repletas de solos acompanhadas por vocais altos e distorcidos, sendo que outros instrumentos e técnicas como
                sintetizadores e piano só foram usados em outros lançamentos da banda.
            </p>
            <br />

            <p>
                Appetite for Destruction é tido como um dos mais importantes lançamentos da história do rock por trazer canções que se tornaram
                clássicas e são populares até hoje como "Welcome to the Jungle", "Paradise City" e "Sweet Child o' Mine" dentre outras. O álbum foi
                aclamado pela crítica, que elogiou principalmente as letras variadas e as melodias potentes. Foi também um grande sucesso comercial,
                se tornando o disco de estreia mais vendido da história da música e é hoje o #11º álbum mais vendido nos Estados Unidos, bem como um
                dos mais vendidos mundialmente, com cerca de 40 milhões de cópias comercializadas desde seu lançamento. O álbum também aparece
                no topo da lista dos "10 melhores álbuns de rock de todos os tempos" da revista Kerrang! Este álbum está na lista dos #200 álbuns
                definitivos no Rock and Roll Hall of Fame.
            </p>
