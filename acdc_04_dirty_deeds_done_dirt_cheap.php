            <h1>Dirty Deeds Done Dirt Cheap (1976)</h1>

            <p> - Dirty Deeds Done Dirt Cheap é o quarto álbum do AC/DC, lançado a 20 de Setembro de 1976. </p>

            <p>Esse álbum foi lançado primeiramente na Austrália em 1976. O álbum não foi lançado no EUA até 1981, mais de um ano após a morte de Bon Scott. O disco marcou (juntamente com T.N.T.) a conquista de milhares de fãs, junto da primeira turnê mundial
                da banda. Foi o Quarto álbum gravado pelo AC/DC. </p>

            <p>A faixa-título se tornaria uma das canções mais famosas da banda. Seu narrador convida as pessoas com problemas a ligar para ele no 36-24-36, um número de telefone real na Austrália na década de 1960, ou visitá-lo em sua casa, momento em que ele irá realizar
                diversos atos desagradáveis ​​para resolver esses problemas. As situações em que ele oferece assistência incluem aquelas envolvendo diretores obscenos de escolas de segundo grau e outras pessoas importantes que são adúlteras ou que constantemente
                criticam seus parceiros. O termo "Dirty Deeds Done Dirt Cheap" é uma homenagem ao desenho animado Beany e Cecil , que Angus assistia quando era criança. Um dos personagens do desenho se chamava Dishonest John, que dizia: "Ações sujas feitas
                de maneira barata. Tarifas especiais para domingos e feriados." No DVD Live on Donington , Malcolm e Angus explicaram que o conceito do álbum era basear-se como um cenário de mistério.</p>

            <p>- Dirty Deeds Done Dirt Cheap foi certificado 6x de platina na Austrália e nos EUA, vendendo pelo menos seis milhões de cópias, tornando-se o terceiro álbum mais vendido do AC/DC nos EUA depois de - <a href="acdc_07_highway_to_hell.php" target="blank">Highway to Hell</a> (7x platina) e <a href="acdc_08_black_in_black.php" target="blank">Black in Black</a> (22x platina). Allmusic dá ao álbum cinco de cinco estrelas e proclama: "Ele capturou a malevolência fervilhante de Bon
                Scott ... encorajado pelos riffs maníacos de Angus e Malcolm Young" e que havia um "senso real de perigo neste álbum." Greg Kot da Rolling Stone dá ao álbum apenas três de cinco estrelas, mas afirma: "As guitarras dos irmãos Angus e Malcolm
                Young latem um para o outro, Phil Rudd balança a batida mesmo enquanto pulveriza seu bumbo, e Scott traz o atrevimento e lamentação. O assunto é rebelião de rock padrão; Scott faz uma pausa apenas uma vez para contemplar brevemente as
                consequências de sua perseguição noturna em 'Ride On'. "</p>
