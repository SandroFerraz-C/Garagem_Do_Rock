            <h1> Vol. 4 (1971)</h1>


            <p>
                - Vol. 4 é o quarto álbum do Black Sabbath.
            </p>


            <p>
                O nome original do álbum seria - Snowblind -, que faz referência ao uso de cocaína, mas a gravadora não os deixou manter o título,
                porque naqueles dias a cocaína era um problema sério e eles não queriam causar polêmica nem problemas juridicos.
            </p>

            <p>
                - Vol. 4 viu o Black Sabbath começar a experimentar o som pesado pelo qual se tornaram conhecidos. Em junho de 2013, Mojo declarou:
                "Se a bebida e a droga ajudaram a alimentar os álbuns anteriores do Sabbath, o - Vol. 4 é a cocaína deles ... Apesar de seus vícios
                crescentes, musicalmente o - Vol. 4 é outra saída ambiciosa. O lado pesado da banda permanece intacto nos gostos de "Tomorrow's Dream",
                "Cornucopia" e "Supernaut", mas a introdução de guitarra em "St. Vitus Dance" possui uma qualidade aromatizada como o
                <a href="led_zeppelin.php" target="blank">Led Zeppelin</a>.
                "Depois de ficar acordado a noite toda e assistir o nascer do sol em Laguna Beach, Iommi compôs "Laguna Sunrise", que é um evocativo neoclássico instrumenta.
                No estúdio, uma orquestra acompanhou o violão de Iommi, embora eles se recusassem a tocar até que suas partes fossem devidamente escritas.
                A mesma orquestra tocou em "Snowblind".
            </p>

            <p>
                Então o nome "Snowblind" foi esquecido e seu quarto álbum se tornou conhecido só como - Vol. 4; Em 2017, foi eleito o #14º
                melhor álbum de metal de todos os tempos pela revista <a href="https://www.rollingstone.com/music/music-album-reviews/black-sabbath-vol-4-91397/" target="blank">Rolling Stone</a>.
