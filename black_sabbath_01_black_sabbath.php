            <p>- Black Sabbath é o álbum de estreia lançado pela banda inglesa do mesmo nome.</p>

            <p>O álbum é amplamente considerado como o primeiro álbum dogênero heavy metal. Além disso, a faixa de abertura,
                autointitulada "Black Sabbath", foi referida como a primeiramúsica do doom metal;
                <br />
                Após o lançamento, o álbum alcançou a oitava posição no UK Albums Charts e a posição #23 na Billboard 200 dos EUA.
                Foi incluído no livro de Robert Dimery, <a href="https://www.discogs.com/pt_BR/lists/1001-albums-you-must-hear-before-you-die/18222" target="blank">1001 Álbuns que você deve ouvir antes de morrer</a>.
            </p>

            <p>Ainda que não tenha recebido muita atenção na época de seu lançamento, - Black Sabbath tem sido, desde então, considerado como um dos
                discos mais importantes para o desenvolvimento do heavy metal.</p>

            <p>De acordo com o guitarrista e fundador do - Black Sabbath, Tony Iommi, o álbum de estréia do grupo foi gravado em um único dia em
                16 de outubro de 1969; A sessão durou doze horas. Iommi disse: "Nós apenas entramos no estúdio e fizemos isso em um dia, tocamos
                nosso set ao vivo e pronto. Na verdade, pensamos que um dia inteiro era muito tempo."
                <br /><br />
                Além dos sinos, efeitos sonoros de trovão e chuva adicionados ao início da faixa de abertura, e os solos de guitarra de duas faixas
                em "NIB" e "Sleeping Village", não houve virtualmente nenhum overdubs adicionado ao álbum. Iommi relembra a gravação ao vivo:
                “Nós pensamos, 'Temos dois dias para fazer isso e um dos dias é a mixagem.' Então tocamos ao vivo. Ozzy estava cantando ao mesmo tempo
                que tocavamos os instrumentos, nós apenas o colocamos em uma cabine separada e lá fomos nós. Nós nunca tivemos uma segunda exibição
                da maioria das coisas."
            </p>
