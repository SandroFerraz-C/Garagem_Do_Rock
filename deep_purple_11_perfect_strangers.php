            <h1>Perfect Strangers (1984)</h1>
            <br />

            <p>
                Perfect Strangers é o décimo primeiro álbum da banda Deep Purple, lançado em outubro de 1984.
            </p>
            <br />

            <p>
                Foi o primeiro álbum de estúdio do Deep Purple em nove anos Ritchie Blackmore e Roger Glover chegaram do Rainbow,
                Ian Gillan do Black Sabbath, Jon Lord de Whitesnake e Ian Paice de Gary Moorebanda de apoio. Apenas uma música do
                novo repertório reformado do Deep Purple, "Nobody's Home", seria creditada a todos os cinco membros da banda.
                Gillan e Glover, tentaram devolver o assunto aos créditos do all-for-one das primeiras gravações da banda, mas
                Blackmore se manteve firme. Foi só quando Blackmore deixou o grupo em 1993 que o problema foi finalmente resolvido.
            </p>
            <br />

            <p>
                As versões em CD e cassete do álbum continham a faixa extra "Not Responsible" (uma das poucas músicas do Deep
                Purple com letras profanas, aqui "a porra"). O álbum foi remasterizado e relançado em 22 de junho de 1999 com a faixa
                instrumental bônus "Son of Alerik". Este último estava disponível anteriormente como lado B no single "Perfect Strangers"
                em 1984.
            </p>
            <br />

            <p>
                O álbum foi um sucesso comercial, alcançando a posição #5 nas paradas do Reino Unido e #12 na Billboard 200 nos
                Estados Unidos. Perfect Strangers foi apenas o segundo álbum de estúdio do Deep Purple a receber disco de platina
                nos Estados Unidos, depois de Machine Head, de 1972. A turnê foi tão bem sucedida que a banda teve que reservar
                muitas datas adicionais para a turnê nos Estados Unidos, pois os ingressos esgotaram muito rapidamente. Sua turnê
                pelos Estados Unidos em 1985 superou todos os artistas naquele ano, exceto Bruce Springsteen .
            </p><br /><br />

            <p>Duração do album: 45 min. 01 seg.</p>
            <p>Gravadora(s): Polydor.</p>
            <p>Produção: Roger Glover & Deep Purple.</p>
