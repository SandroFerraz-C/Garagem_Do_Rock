            <h1>Presence (1976)</h1>
            <br />

            <p>
                - Presence é o sétimo álbum do Zeppelin, lançado pela Swan Song Records em 31 de março de 1976.
            </p>
            <br />

            <p>
                Ele foi escrito e gravado durante um período tumultuado da história da banda, como vocalista Robert Plant estava se recuperando de
                ferimentos graves que sofreu em um recente acidente de carro. O álbum recebeu críticas mistas dos críticos e é também o disco com
                as vendas mais lentas da banda (além do álbum póstumo Coda), só conseguindo alcançar o certificado de platina tripla nos Estados
                Unidos. No entanto, o guitarrista Jimmy Page descreve - Presence como o álbum "mais importante", provando que a banda iria
                continuar apesar de sua crise.
            </p>
            <br />

            <p>
                O álbum foi lançado dia 31 de março de 1976, depois de ter sido adiado para fazer a capa. Na Grã-Bretanha teve um recorde de
                encomendas, já sendo disco de ouro no dia de seu lançamento. Nos Estados Unidos o álbum chegou a número #1 nas paradas da billboard
                em duas semanas.
                <br />
                O álbum não vendeu tanto quanto os anteriores e ficou subestimado.
            </p>
            <br />

            <p>
                A capa e o encarte deste álbum, criados por Hipgnosis, apresentam várias imagens de pessoas interagindo com um objeto negro na
                forma de um obelisco. No interior do álbum o obelisco é chamado simplesmente de "The Object" e representa a força e presença do
                Led Zeppelin. Nas notas do Led Zeppelin remasters, Jimmy Page explica:
                <br />

                "O álbum não tinha um título provisório". O designer da capa disse "Quando eu penso em vocês, eu sempre penso em poder e força.
                Tem uma clara presença aqui. E foi assim. Ele queria chamar de Obelisco. Para mim, era mais importante o que estava por trás do
                obelisco. Essa capa é muito irônica, para ser honesto. Meio que uma brincadeira com o filme 2001. Eu achei muito divertido".
                <BR /><BR />
                O fundo usado na capa é de uma marina artificial instalada dentro do London's Earl's Court Arena para o anual Earl's Court Boat Show
                que aconteceu no inverno de 74-75. É o mesmo local onde a banda fez uma série de concertos em maio de 75.
                <BR />

                Em 1977 o álbum foi nomeado para um Grammy Award na categoria de melhor capa.
            </p>
            <br /><br />

            <p>Duração do album: 44 min. 25 seg.</p>
            <p>Gravadora(s): Swan Song Records.</p>
            <p>Produção: Jimmy Page.</p>
