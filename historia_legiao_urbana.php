            <p>
                Em 2010, a Legião Urbana havia vendido catorze milhões de cópias (incluindo discos solo de Renato Russo), segundo informações da
                gravadora EMI cedidas ao jornal O Estado de S. Paulo. No mesmo ano, a revista Veja falava em quinze milhões de cópias (levando em
                conta a discografia da banda e os dois primeiros discos solo de Renato, - The Stonewall Celebration Concert e - Equilíbrio Distante);
                A revista afirmou também na mesma ocasião que eles comercializavam uma média de 250 mil discos por ano. Em 2015, O Estado de S.
                Paulo afirmou que a banda vendeu 25 milhões de cópias em seus catorze anos de atividade. É o segundo grupo musical brasileiro que
                mais vendeu discos de catálogo no mundo, além de fazer parte do chamado "quarteto sagrado" do rock brasileiro, juntamente aos
                grupos musicais
                <a href="barao_vermelho.php" target="blank"> Barão Vermelho</a>, <a href="titas.php" target="blank"> Titãs</a> e
                <a href="os_paralamas_do_sucesso.php" target="blank"> Os Paralamas do Sucesso </a>.
            </p>


            <p>
                Entre outubro de 2015 e dezembro de 2016, Dado Villa-Lobos e Marcelo Bonfá, os dois integrantes remanescentes do grupo, apresentaram
                juntamente a André Frateschi nos vocais principais e diversos músicos convidados a turnê Legião Urbana XXX Anos, que consistiu na
                comemoração dos trinta anos de seu primeiro álbum de estúdio. Devido à boa aceitação do público, o grupo resolveu retomar o projeto em 2018,
                desta vez executando o repertório do segundo e do terceiro álbuns do grupo, que continua até os dias atuais.
            </p>

            <p>
                A banda foi formada em agosto de 1982, pouco menos de um ano após o fim da banda Aborto Elétrico, desencadeado por atritos
                existentes entre os integrantes Renato Russo e Felipe Lemos. O primeiro atrito foi durante uma execução ao vivo da canção
                "Veraneio Vascaína" (na ocasião, Renato errou a letra e levou uma baquetada em pleno show). O segundo atrito foi quando Renato
                Russo apresentou "Química" em um ensaio do Aborto e Felipe Lemos criticou a canção com veemência. O fim da banda foi no final de
                1981, quando Renato Russo cortou pacificamente o seu vínculo com seus colegas de grupo, com direito a uma última reunião poucos
                meses depois motivada pela negligência de Ico Ouro Preto ao assumir uma apresentação do grupo. Com o fim da banda, Felipe e seu
                irmão Flávio Lemos (baixo), fundaram o <a href="capital_inicial.php" target="blank"> Capital Inicial</a>, com sua formação
                estabelecida juntamente a Dinho Ouro Preto (voz) e Loro Jones (guitarra).
            </p>

            <p>
                - A Tempestade, lançado em 20 de setembro de 1996, foi o último álbum da banda publicado com Renato Russo
                em vida. O mesmo foi marcado por canções muito introspectivas e depressivas, alternando entre as sonoridades pesadas de "Natália"
                e "Dezesseis", o lirismo de "L'Avventura" e "O Livro Dos Dias", o experimentalismo de "Música Ambiente", ao sofrimento de "Aloha"
                e "Esperando Por Mim" e à leveza de "Soul Parsifal" e "1º De Julho". As letras, em geral, abordam temas como solidão, passado, amor,
                depressão, soropositividade, intolerância e injustiça, dito como um disco "melodramático" e de alma triste. "A Via Láctea" e
                "Dezesseis" foram seus principais singles.
            </p>

            <p>
                Por conta de um processo de recuperação judicial do grupo iniciado em 1985 (pelo qual um oportunista registrou a marca do grupo
                antes de seus integrantes), quando o processo foi concluído, em 1991, o poder da marca Legião Urbana teria que ser centralizado em
                apenas um integrante, cujo escolhido foi Renato Russo. Um mês depois da morte dele, Dado Villa-Lobos e Marcelo Bonfá receberam
                uma ligação movida pelos pais de Renato, através da qual foi anunciado que os dois perderam o direito de dizerem publicamente que
                participaram da Legião Urbana. Este direito foi recuperado permanentemente em 27 de março de 2015, dia em que Renato faria 55 anos
                se estivesse vivo.
            </p>
