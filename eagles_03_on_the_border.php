            <h1>On the Border (1974)</h1>

            <p>- On the Border é o terceiro álbum da banda Eagles.</p>

            <p> Além de duas canções produzidas por Glyn Johns, o álbum foi produzido por Bill Szymczyk porque o grupo queria um som mais orientado
                para o rock em vez do country-rock sensação dos dois primeiros álbuns. É o primeiro álbum dos Eagles a apresentar o guitarrista
                Don Felder. On the Border alcançou a posição #17 na parada de álbuns da Billboard e vendeu mais de dois milhões de cópias.</p>

            <p>Três singles foram lançados do álbum: "Already Gone", "James Dean" e "Best of My Love". Os singles chegaram ao número 32, 77 e 1,
                respectivamente. "Best of My Love" se tornou o primeiro dos cinco primeiros lugares das paradas. O álbum também inclui "My Man", um
                tributo de Bernie Leadon a seu falecido amigo Gram Parsons . Leadon e Parsons tocaram juntos na pioneira banda de country rock Flying
                Burrito Brothers, antes de Leadon se juntar aos Eagles.</p>

            <p>Os primeiros dois singles lançados eram mais voltados para o rock; Frey estava relutante em lançar "Best of My Love", produzido por
                Johns, como single, e adiou o lançamento por alguns meses. No entanto, quando foi finalmente lançada, a gravadora truncou a música
                - sem o conhecimento ou aprovação da banda - para que fosse mais amigável ao rádio. "Best of My Love" se tornaria seu maior sucesso
                até agora, e seu primeiro número #1 nas paradas.</p>
