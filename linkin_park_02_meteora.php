            <h1>Meteora (2003)</h1>
            <br />

            <p>
                - Meteora é o segundo álbum do Linkin Park.
            </p>
            <br />

            <p>
                O álbum Foi lançado em 25 de março de 2003 pela Warner Bros. Records, seguindo - Reanimation, um álbum de colaboração que 
                trazia remixes de canções incluídas em seu primeiro álbum - Hybrid Theory. O álbum foi produzido pela banda ao lado de Don Gilmore. 
                O título "Meteora" é retirado dos mosteiros ortodoxos gregos que originalmente carregavam o nome. - Meteora tem um som semelhante ao
                -  Hybrid Theory, conforme descrito pela crítica, e o álbum levou quase um ano para ser gravado. 
            </p>
            <br />

            <p>
            - Meteora estreou em #1° lugar na Billboard 200, vendendo mais de 810.000 cópias em sua primeira semana. O Linkin Park lançou 
            singles de - Meteora por mais de um ano, incluindo "Somewhere I Belong", "Faint", "Numb", "From the Inside" e "Breaking the Habit".
             O álbum recebeu críticas geralmente positivas, embora os críticos notassem que o estilo musical do álbum era semelhante ao de seu
              predecessor, - Hybrid Theory.
            </p>
            <br />

            <p>
            - Meteora vendeu cerca de 16 milhões de cópias em todo o mundo, tornando-se um dos álbuns mais vendidos do século 21. É certificado
             7x Platinum pela Recording Industry Association of America (RIAA). Foi classificado em #36º lugar na Billboard Top 200 Albums of 
             the 2000.  Algumas canções do álbum foram remixadas com algumas canções de Jay-Z para o EP - Collision Course (2004). "Session" foi 
             indicada para Melhor Performance Instrumental de Rock no #46º Grammy Awards.
            </p>
            <br />

            <p>
            Liricamente, o álbum contém elementos que incluem emoções deprimentes, raiva e recuperação. Explicando à MTV , Bennington disse: 
            "Não falamos sobre situações, falamos sobre as emoções por trás das situações. Mike e eu somos duas pessoas diferentes, então não
             podemos cantar sobre as mesmas coisas, mas ambos sabemos sobre a frustração e raiva e solidão e amor e felicidade, e podemos nos 
             relacionar nesse nível". Na mesma entrevista, Shinoda explicou: "O que realmente queríamos fazer era nos empurrar e empurrar uns 
             aos outros para realmente encontrar novas maneiras de ser criativos". Ele continuou: "Queríamos que cada música
              fosse algo que pudesse animar seus ouvidos - algo que você talvez nunca tenha ouvido antes."
            </p>
            <br /><br />

            <p>Duração do album: 36 min. 35 seg.</p>
            <p>Gravadora(s): Warner Bros..</p>
            <p>Produção: Don Gilmore e Linkin Park.</p>
