            <h1>Kiss (1974)</h1>
            <br />

            <p>
                - Kiss é o primeiro álbum da banda de rock americana Kiss, lançado em 18 de fevereiro de 1974.
            </p>
            <br />

            <p>
                Muito do material do álbum foi escrito por Gene Simmons e Paul Stanley, como membros de sua banda pré-Kiss, Wicked Lester.
                Simmons estimou que todo o processo de gravação e mixagem levou três semanas, enquanto o co-produtor Richie Wise afirmou que levou
                apenas 13 dias.
            </p>
            <br />

            <p>
                O álbum foi gravado no Bell Sound Studios em Nova York, que pertencia à empresa proprietária da Buddah Records. Neil Bogart,
                fundador da Casablanca Records, era um executivo da Buddah antes de formar a Casablanca. A Casablanca Records deu uma festa no
                Century Plaza Hotel em Los Angeles para celebrar o lançamento do Kiss na Costa Oeste e para apresentar a gravadora à imprensa e
                outros executivos da indústria fonográfica.
            </p>
            <br />

            <p>
                Seguindo o tema de Casablanca, a festa incluiu palmeiras e um sósia de Humphrey Bogart . Kiss apresentou seu show de palco
                barulhento e bombástico de sempre, que virou a Warner Bros. Records (distribuidora de discos da Casablanca) contra o grupo.
                Logo após o show, a Warner Bros. contatou Neil Bogart e ameaçou encerrar seu contrato com a Casablanca se o Kiss não removesse sua
                maquiagem. Com o apoio do empresário Bill Aucoin, Kiss recusou. Pouco depois do lançamento de Kiss, a Warner Bros. Cancelou o
                contrato com a Casablanca.
            </p>
            <br />

            <p>
                Apesar da promoção e da turnê da banda, o Kiss vendeu aproximadamente 75.000 cópias após seu lançamento inicial, sem a presença de
                um single de sucesso. Foi certificado ouro em 8 de junho de 1977, tendo enviado 500.000 cópias. O álbum foi relançado em 1997 (junto com
                a maioria dos álbuns anteriores do Kiss) em uma versão remasterizada.
                <br />
                Gene Simmons disse que Kiss é seu álbum favorito do Kiss.
            </p>
            <br /><br />

            <p>Duração do album: 35 min. 11 seg.</p>
            <p>Gravadora(s): Casablanca Records.</p>
            <p>Produção: Kenny Kerner e Richie Wise.</p>
