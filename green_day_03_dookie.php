                empresário até mesmo convidando o grupo para a Disneylândia. A banda recusou esses acordos até conhecer o produtor e representante
                da Reprise, Rob Cavallo. Eles ficaram impressionados com seu trabalho com a banda californiana The Muffs, e mais tarde comentaram
