            <p>
                A primeira colaboração da banda com o produtor Rob Cavallo, foi gravada no final de 1993 no Fantasy Studios em Berkeley, Califórnia.
                Escrito principalmente por Billie Joe Armstrong, o álbum é baseado principalmente em suas experiências pessoais, com temas como tédio,
                ansiedade, relacionamentos e sexualidade. O álbum foi promovido com cinco singles: "Longview", "Basket Case", uma versão regravada de
                "Welcome to Paradise".
            </p>

            <p>
                - Dookie foi aclamado pela crítica após seu lançamento e ganhou um Grammy de Melhor Álbum Alternativo de 1995. Também foi um sucesso
                mundial, alcançando o segundo lugar nos Estados Unidos e os #5 primeiros em vários outros países; é creditado por trazer o punk rock
                para a popularidade e impulsionar o Green Day para a fama mundial. Posteriormente, foi certificado como diamante pela RIAA e vendeu
                cerca de 20 milhões de cópias em todo o mundo, tornando-o o álbum mais vendido da banda e um dos álbuns mais vendidos em todo o mundo.
                Em 2003, a Rolling Stone colocou Dookie no número #193 em sua lista dos "500 melhores álbuns de todos os tempos", mantendo a classificação
                em uma lista revisada de 2012. Em 2020, a Rolling Stone re-classificou o álbum no número #375 em outra lista revisada.
            </p>

