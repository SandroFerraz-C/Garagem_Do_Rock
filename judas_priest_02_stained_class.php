            <p>
                - Stained Class é o #4° álbum* de estúdio lançado pela banda Judas Priest a 10 de Fevereiro de 1978.
            </p>
            <br />

            <p>
                É o #1° de três álbuns do Judas Priest a apresentar o baterista Les Binks, bem como o #1° a apresentar o agora conhecido logotipo
                da banda. O álbum traz uma versão cover de "Better by You, Better than Me", de Spooky Tooth, que atraiu à banda uma grande
                atenção negativa vários anos após seu lançamento, quando dois fãs foram supostamente influenciados pela música para cometer um
                suicídio pacto. No entanto, - Stained Class é considerado por alguns como um dos melhores álbuns do Judas Priest.
            </p>
            <br />

            <p>
                "Better by You, Better than Me" foi uma adição de última hora ao álbum quando a CBS Records insistiu em incluir algo com potencial
                comercial para animar um álbum que eles sentiam ter um tom muito sombrio e sinistro. A música foi a ultima a ser gravada, foi
                produzida por James Guthrie separadamente do resto do álbum. A banda ficou tão impressionada com a produção de Guthrie em
                "Better by You, Better than Me" que eles pediram a ele para produzir seu próximo álbum, - Killing Machine .
            </p>
            <br />

            <p>
                A arte da capa do artista Rosław Szaybo introduziu o agora clássico logo do Judas Priest, substituindo o logo do Gothic Script
                que apareceu em todos os álbuns anteriores da banda. - Stained Class foi o primeiro álbum do Judas Priest a estourar na parada
                Billboard 200 e acabou sendo certificado ouro nos Estados Unidos.
            </p>
