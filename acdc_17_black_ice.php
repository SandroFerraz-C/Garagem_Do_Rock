            <h1><span lang="en-US">BLACK ICE</span> (<time datetime="2008">2008</time>)</h1>

            <p>- <span lang="en-US">Black Ice</span> é o 15° álbum de estúdio da banda australiana. É seu primeiro álbum em oito anos.</p>

            <p>Lançado internacionalmente em <time datetime="2008">20 de outubro de 2008</time>, foi produzido por Brendan O'Brien. Isso marcou as primeiras gravações originais da banda desde - <a href="acdc_16_stiff_upper_lip.php" target="blank"><span lang="en-US">Stiff Upper Lip</span></a> de <time datetime="2000">2000</time>, com
                o intervalo de oito anos sendo o mais longo entre os sucessivos álbuns de estúdio do AC/DC. - <span lang="en-US">Black Ice</span> tem o tempo de execução mais longo de qualquer álbum de estúdio da banda.</p>

            <p>O desenvolvimento do álbum foi atrasado porque o baixista Cliff Williams se machucou e a banda mudou de gravadora, da <span lang="en-US">Elektra Records</span> para a <span lang="en-US">Sony Music Entertainment</span>. As primeiras sessões de composição entre os guitarristas / irmãos Angus e Malcolm Young
                foram em Londres em <time datetime="2003">2003</time>. As gravações aconteceram durante <time datetime="03">março</time> e <time datetime="2008-04">abril de 2008</time> no <span lang="en-US">The Warehouse Studio</span> em Vancouver. O'Brien tentou recapturar o som <span lang="en-US">Rock</span> dos primeiros trabalhos da banda, em oposição à orientação <span lang="en-US">Blues-Rock</span> de -
                <a href="acdc_15_ballbreaker.php" target="blank"><span lang="en-US">Ballbreaker</span></a> e - <a href="acdc_16_stiff_upper_lip.php" target="blank"><span lang="en-US">Stiff Upper Lip</span></a>, com sugestões como adicionar "Soul cantarolando" ao som de Brian Johnson. As músicas foram
                gravadas principalmente ao vivo no estúdio; o engenheiro Mike Fraser usou apenas <span lang="en-US">overdubs</span> e efeitos esparsos para manter as faixas o mais próximo possível dos originais.
            </p>

            <p>- <span lang="en-US">Black Ice</span> fez história ao estrear em #1° nas paradas de álbuns de 29 países, e é o maior álbum de estreia da <span lang="en-US">Columbia Records</span> desde que Nielsen SoundScan começou a rastrear dados de vendas para a <span lang="en-US">Billboard</span> em <time datetime="1991-03">março de 1991</time>. - <span lang="en-US">Black Ice</span> foi o segundo
                álbum mais vendido em todo o mundo em <time datetime="2008">2008</time>, atrás <span lang="en-US">Coldplay's</span> - Viva La Vida <span lang="en-US">or Death and All His Friends</span>. Em <time datetime="2008-12">dezembro de 2008</time>, ele tinha vendido seis milhões de cópias em todo o mundo, e ganhou certificações de vendas em 24 países, com
                status de multi-platina em oito, platina em mais doze e ouro nos quatro restantes.</p>

            <p>
            <blockquote>O cantor Brian Johnson temia que este álbum não fosse lançado haja visto o espaço de tempo de oito anos dentre o - <span lang="en-US">Black Ice</span> e o último trabalho de estúdio, e comentou: <q>Na primeira faixa do disco os pêlos dos meus braços arrepiaram. Eu pensei, ‘é uma banda boa ou não é?‘</q>.</blockquote>
            </p>
