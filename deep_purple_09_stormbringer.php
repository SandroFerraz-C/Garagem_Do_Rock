                e foi usada e editada para a capa do álbum. A mesma fotografia foi usada para o álbum de Miles Davis , Bitches
                Brew, em 1970, e para o álbum de Siouxsie and the Banshees , Tinderbox, em 1986.</p>

            <p>Stormbringer é o nome de uma espada mágica descrita em muitos romances e histórias em quadrinhos de Moorcock
