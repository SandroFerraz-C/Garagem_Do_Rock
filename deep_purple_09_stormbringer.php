            <h1>Stormbringer (1974)</h1>
            <br />

            <p>
                Stormbringer é o nono álbum do Deep Purple, lançado em novembro de 1974
            </p>
            <br />

            <p>
                Nesse álbum, os elementos de funk e soul, que foram apenas insinuada em Burn, são mais proeminentes, o que
                incomodou bastante ao guitarrista Ritchie Blackmore, que não era entusiasta destes elementos musicais. Após a
                turnê européia de divulgação do álbum, Blackmore deixou o grupo em 7 de abril de 1975.
            </p>
            <br />

            <p>
                A imagem da capa de Stormbringer é baseada em uma foto. Em 8 de julho de 1927, um tornado perto da cidade de
                Jasper, Minnesota, foi fotografado por Lucille Handberg. [2] Sua fotografia se tornou uma imagem clássica,
                e foi usada e editada para a capa do álbum. A mesma fotografia foi usada para o álbum de Miles Davis , Bitches
                Brew, em 1970, e para o álbum de Siouxsie and the Banshees , Tinderbox, em 1986.
            </p>
            <br />

            <p>
                Stormbringer é o nome de uma espada mágica descrita em muitos romances e histórias em quadrinhos de Moorcock
                e outros que tiveram um enorme sucesso nas décadas de 60 e 70. David Coverdale negou saber disso até pouco
                depois de gravar o álbum. Em uma entrevista com Charles Shaar Murray no New Musical Express, ele afirmou que
                o nome era mitológico
            </p>
            <br />

            <p>
                De acordo com Glenn Hughes, o jargão arrastado que é falado por Coverdale no início da faixa-título pouco
                antes do primeiro verso é o mesmo diálogo invertido que a personagem de Linda Blair profere no filme O
                Exorcista, quando é questionada pelo sacerdote.
            </p>

